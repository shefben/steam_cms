<h1>APRIL CYBER CAF&Eacute; PROMOTION</H1>
<h2>VALVE'S<em> OFFICIAL CYBER CAF&Eacute; PROGRAM</em></h2><img src="img/Graphic_box.jpg" height="6" width="24" alt=""><br>
<br>
During the month of April 2004, Valve is extending a special offer to Cyber Caf&eacute;s. <b>During this time, a 12-month cyber caf&eacute; license for Valve&apos;s games is being offered at a savings of 33%</b>. The license includes all of Valve&apos;s available Steam content, including Counter-Strike: Condition Zero, Counter-Strike, Half-Life, Day of Defeat, Team Fortress Classic, and more. Licensed Cyber Caf&eacute;&apos;s also receive product key protection, promotional materials, tournament licenses, and priority access to Valve support.<br>
<br>
This offer ends at 11:59 pm PST on April 30, 2004, is subject to change and is not available in every territory. For more information, please email <a href="mailto:cafe@valvesoftware.com">cafe@valvesoftware.com</a>.
