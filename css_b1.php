<a name="q1"></a><li style="list-style-image:url(/img/square2.gif);"> <p><strong><em>When will Counter-Strike: Source beta 1 be released?</em></strong></p>

<p>Starting later this summer, Valve will be conducting a limited-time beta of Counter-Strike: Source via Steam.</p>

<a name="q2"></a><li style="list-style-image:url(/img/square2.gif);"> <p><strong><em>How can I get Counter-Strike: Source beta 1?</em></strong></p>

<p>The beta will first be open to subscribers of the Valve Cyber Caf&eacute; Program, and then extended to owners of Counter-Strike: Condition Zero and owners of ATI Half-Life 2 vouchers.</p>

<a name="q3"></a><li> <p><strong><em>I am a member of the Valve Cyber Caf&eacute; Program, or I own Condition Zero.  How will I know when Counter-Strike: Source beta 1 is available?</em></strong></p>
 
<p>When the Counter-Strike: Source beta begins, it will appear in your Steam <em>My Games</em> list.</p>

<a name="q8"></a><li> <p><strong><em>If I purchased the ATI/Half-Life 2 Video Card bundle, how do I participate in the Counter-Strike: Source Beta??</em></strong></p>

<p>If you do not already have a Steam account, you can create a Steam account and use your ATI/Half-Life 2 product key printed on the coupon that came with the video card.  Once you have entered the key, you will be prompted to join the beta.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="getsteamnow.php">Click here to install Steam</a></p>

<p>If you have a Steam account and have not yet registered your ATI/Half-Life 2 product key printed on the coupon that came with the video card, you can register your key below and join the beta.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="steam://open/registerproduct/">Click here register your ATI key</a></p>

<a name="q4"></a><li> <p><strong><em>Can I get access to the beta if I am not part of the Valve Cyber Caf&eacute; Program, and do not own Condition Zero or have an ATI/Half-Life 2 product key?</em></strong></p>
 
<p>No.</p>

<a name="q5"></a><li> <p><strong><em>How will Counter-Strike: Source be different from the Half-Life 1 based Counter-Strike?</em></strong></p>
 
<p>CS: Source blends the intense teamplay of Counter-Strike with the advance technology of Valve&apos;s Source engine.</p>

