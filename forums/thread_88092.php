<b>Installation</b>: Just extract it to your Condition Zero Deleted Scenes directory. To start it, type map changelevel cz_trainrun in the console.<br />
<b>Description</b>: Kiltron's third release, this map reminds me of a bit in Poke646 and a Sven Co-op custom map - you're on two parallel moving trains, and you must move down them both, informing your collegues of hostage locations, defusing warheads (:P) and eventually stopping the trains. This was a pretty tough map - I blew all my health and ammo at the start, and more wasn't very readily forecoming. At one point you must take down a helicopter, and I felt that some people might not have enough ammo to do that at that stage of the level, which I thought was harsh. The time limit of 15 minutes isn't too tight, but you do have to be aware of it - time limits haven't been used much before, and here it adds a feeling of panic whilst playing the map. The foggy atmosphere fitted perfectly, and despite being under 15 minutes (:P) I feel that this is one of the better maps by far.<br />
