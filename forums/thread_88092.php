	<p><font face="verdana, arial, helvetica" size="2" ><b>This is the old <i>CZ Mission Packs List</i> thread, please go <a href="/forums/showthread.php?s=&amp;threadid=119505" target="_blank">here</a> for the new one <img src="images/smilies/smile.gif" border="0" alt=""> (mark 3)<br />
