Also, the list is now hosted online at <a href="http://www.sandwiregames.com" target="_blank">www.sandwiregames.com</a> - this is where this Mission Packs project will eventually be managed.</b><br />
<br />
Here's a list of all the CZ mission packs I've found so far, in rough order of release.<br />
If you know of any I've missed, or have new ones, reply and I'll edit them into my first post in order to keep things orderly...<br />
<br />
<b>Name</b>: <font color="orange"><b>The Counter-Strike: Condition Zero Mission Pack</b></font><br />
<b>Author</b>: Turtle Rock Studios<br />
<b>Website</b>: <a href="http://www.turtlerockstudios.com/" target="_blank">http://www.turtlerockstudios.com/</a>, <a href="http://www.counter-strike.net/" target="_blank">http://www.counter-strike.net/</a>, <a href="http://cs-conditionzero.com/" target="_blank">http://cs-conditionzero.com/</a> and <a href="/" target="_blank">/</a><br />
<b>In-Game Name</b>: CONDITION ZERO<br />
<b>Directory Name</b>: TurtleRockCounterTerrorist<br />
<b>Terrorist or Counter-Terrorist?</b>: Counter-Terrorist<br />
<b>Map List/Order</b>: de_dust_cz, cs_office_cz, de_prodigy_cz, de_tides_cz, cs_italy_cz, de_inferno_cz, de_dust2_cz, cs_militia_cz, de_stadium_cz, de_chateau_cz, cs_havana_cz, de_cbble_cz, de_aztec_cz, cs_downed_cz, de_airstrip_cz, de_pirannesi_cz, de_fastline_cz and de_torn_cz<br />
<b>Release Info/Changelog</b>: Updated every time CZ is patched - go <a href="/platform/update_history/Condition%20Zero.html" target="_blank">here</a> for details.<br />
<b>Description</b>: This is the official pack - it comes with CZ. As such, it includes the 18 official maps that came out when CZ was released - but none of the official ones which have appeared since. My main issue with this pack is that it appears twice in the pack list...<br />
<b>Bugs/Glitches</b>:<br />
- Appears twice in the list<br />
- The web link is wrong (does not need the /CZ/ extension, and there are more appropriate sites as well)<br />
- There is no in-game link to czmissiondoc.rtf<br />
<br />
<b>Name</b>: <font color="orange"><b>Havok's Terrorist Mission Pack</b></font><br />
<b>Author</b>: Havok<br />
<b>Website</b>: <a href="http://www.3chavok.netfirms.com/" target="_blank">http://www.3chavok.netfirms.com/</a> (nothing about the pack though)<br />
<b>In-Game Name</b>: Havoks T Missions<br />
<b>Directory Name</b>: havokTerrorist<br />
<b>Terrorist or Counter-Terrorist?</b>: Terrorist<br />
<b>Map List/Order</b>: de_dust_cz, cs_office_cz, de_prodigy_cz, de_tides_cz, cs_italy_cz, de_inferno_cz, de_dust2_cz, cs_militia_cz, de_stadium_cz, de_chateau_cz, cs_havana_cz, de_cbble_cz, de_aztec_cz, cs_downed_cz, de_airstrip_cz, de_pirannesi_cz, de_fastline_cz and de_torn_cz<br />
<b>Download Link</b>: You can get it <a href="http://www.3chavok.netfirms.com/Havokstpack.zip" target="_blank">here</a>.<br />
<b>Release Info/Changelog</b>: This is the first and only version of this pack, released on 05/01/04.<br />
<b>Installation</b>: When installing, use the readme, but don't follow what it says about the  "CareerGame****" files (**** being the difficulty level) - rename the "CareerGame****" files to just say "****", and then put them in the MissionPacks\havokTerrorist area with the other files. Also you may want to delete the save file that comes with this pack.<br />
<b>Description</b>: The first ever pack. Everything is the same as in Turtle Rock's campaign, but you are a terrorist. Things start to get  tricky when you have to get a kill with a CT gun, but it gets impossible when you have to rescue a hostage...<br />
<b>Bugs/Glitches</b>:<br />
- Gramatical errors (pack name)<br />
- No pack description in the pack selection menu (it just says #Career_MissionPackt instead)<br />
- The website link has not been changed<br />
- Missions on easy difficulty involving rescuing hostages are impossible<br />
- Missions on easy difficulty involving using CT weapons are hard/impossible<br />
- Certain files are named wrong, and must be renamed (see above)<br />
<br />
<b>Name</b>: <font color="orange"><b>The Terrorist Mission Pack</b></font><br />
<b>Author</b>: Anders Konradsen<br />
<b>Website</b>: <a href="http://www.kenand666.tk/" target="_blank">http://www.kenand666.tk/</a> (nothing about the pack though)<br />
<b>In-Game Name</b>: Terrorist Missions v0.2<br />
<b>Directory Name</b>: Terrorist (BigDaddyK changed this to KonradsenTerrorist when he installed, you can if you like too)<br />
<b>Terrorist or Counter-Terrorist?</b>: Terrorist<br />
<b>Map List/Order</b>: de_dust_cz, de_aztec_cz, de_dust2_cz, ds_italy_cz, de_tides_cz, cs_havana_cz, de_inferno_cz, de_cbble_cz, de_torn_cz, de_prodigy_cz, de_chateau_cz, cs_militia_cz, de_airstrip_cz, cs_downed_cz, de_piranesi_cz, de_fastline_cz, de_stadium_cz and as_tundra<br />
<b>Download Link</b>: You can get it <a href="http://www.sandwiregames.com/missionpacks/terrorist.zip" target="_blank">here</a>.<br />
<b>Release Info/Changelog</b>: This is version 0.2, released on 05/06/04. Here is the changelog:<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr>v0.0<br />
- Changed the original pack to a T pack<br />
v0.1<br />
- Minor bugs fixed<br />
- Added a new overview.tga<br />
v0.2<br />
- New story line<br />
- New map order<br />
- Eddit bug fixed (mispelled name, should be Eddie)<br />
- Minor bugs fixed<hr></blockquote><b>Installation</b>: Unzip all of this to your czero directory - apart from the Terrorist folder, which you should put in your czero\MissionPacks directory (like it says in the readme)<br />
<b>Description</b>: This is what the Havok' pack should have been like. The new missions mean you don't get 'rescue the hostage' missions, but 'plant the bomb' missions instead - much better than Havok's pack. This is the first pack to use a map that are not  the 18 in Turtle Rock's campaign - as_tundra<br />
<b>Bugs/Glitches</b>:<br />
- Gramatical errors (pack description)<br />
<br />
<b>Name</b>: <font color="orange"><b>The National Insecurity Mission Pack</b></font><br />
<b>Author</b>: D-Link<br />
<b>Website</b>: There isn't one<br />
<b>In-Game Name</b>: National Insecurity Tour of Duty<br />
<b>Directory Name</b>: NationalInsecurityMissionPack<br />
<b>Terrorist or Counter-Terrorist?</b>:  Counter-Terrorist<br />
<b>Map List/Order</b>: de_dust_cz, de_aztec_cz, de_dust2_cz, ds_italy_cz, de_tides_cz, cs_havana_cz, de_inferno_cz, de_cbble_cz, de_torn_cz, de_prodigy_cz, de_chateau_cz, cs_militia_cz, de_airstrip_cz, cs_downed_cz and de_piranesi_cz<br />
<b>Download Link</b>: You can get it <a href="http://mysite.verizon.net/res1g4li/NationalInsecurityMissionPack.zip" target="_blank">here</a>.<br />
<b>Release Info/Changelog</b>: This is the second version, updated 05/04/04. You can still get the first version <a href="http://www.3chavok.netfirms.com/NIMissionpack.zip" target="_blank">here</a>.<br />
<b>Installation</b>: Put everything in your NationalInsecurityMissionPack folder, then put that in your MissionPacks folder<br />
<b>Description</b>: Basically a CT pack with new missions, but the same maps as in the Turtle Rock campaign.<br />
<b>Bugs/Glitches</b>:<br />
- The website link has not been changed<br />
- The pack description refers to the maps in their old order, not their new one<br />
<br />
<b>Name</b>: <font color="orange"><b>-=[Sj]=-'s Original CS Mission Pack</b></font><br />
<b>Author</b>: -=[Sj]=- Ice Killer<br />
<b>Website</b>: <a href="http://www.clansj.com/" target="_blank">http://www.clansj.com/</a><br />
<b>In-Game Name</b>: ORIGINAL CS MISSION PACK V03<br />
<b>Directory Name</b>: OriginalCS<br />
<b>Terrorist or Counter-Terrorist?</b>: Counter-Terrorist<br />
<b>Map List/Order</b>: de_dust, de_dust2, cs_office, de_prodigy, de_italy, de_inferno, cs_militia, cs_havana, de_cbble, de_aztec, de_torn, de_airstrip, de_train, de_vertigo, de_survivor, de_nuke, cs_seige and cs_747<br />
<b>Download Link</b>:You can get the full install <a href="http://www.clansj.com/downloads/MissionPackv03.zip" target="_blank">here</a>, or you can get the v0.2-v0.3 patch <a href="http://www.clansj.com/downloads/quickfixv03.zip" target="_blank">here</a>.<br />
<b>Release Info/Changelog</b>: This is version 0.3, released 05/21/04 and last revised 05/22/04. You can still get version 0.2 <a href="http://www.clansj.com/downloads/MissionPackv02.zip" target="_blank">here</a> and version 0.1 <a href="http://www.clansj.com/downloads/MissionPackv01.zip" target="_blank">here</a> if you want, just for reference purposes. The goals were changed and improved between the three releases, and a custom bot skin was added between the second and third releases.<br />
<b>Installation</b>: Inside this zip is an exe - just point it towards your Condition Zero directory when installing and it does the work for you <img src="images/smilies/smile.gif" border="0" alt=""><br />
<b>Description</b>: Instead of the CZ maps, this pack uses 18 of the original CS maps, hence the pack's name. It is also the first pack to use custom bot models. This is well worth getting.<br />
<b>Bugs/Glitches</b>:<br />
- Gramatical errors (pack description)</font></p>
	
