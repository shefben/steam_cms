	<p><font face="verdana, arial, helvetica" size="2" >Can anyone from VALVe or the community tell me why STEAM benefits me, as an end user, over the classic manual patching/WON system.<br />
<br />
I assume you are forcefully migrating users to this platform to help _them_ as well as yourselves (VALVe). Can anyone outline the details of why end users should be seeing STEAM as the way forward instead of juat a pain in the arse?<br />
<br />
My own personal experience with STEAM has been so bad I have ended up stopping developing under HalfLife and haven't played since the Steam 1.0 final was released, but rather than "fl@m3z0r" VALVe,  I would prefer to hear the other side of the argument as to why users aren't being given the choice of either patching themselves, or using STEAM. I personally see absolutley no reason why STEAM authentication cannot be brought out as a standalone patch for 1.1.1.0 users to download and enable play on steam servers, I used to enjoy being able to set up my debugger to run "hl.exe" and pressing F5 for a quick boot of the game to check small changes to code. With STEAM this simply cannot be done, it takes about 5-10 minutes to boot the game for the majority of users, including myself, and waiting 5 minutes to test simple changes in code is just not acceptable, other engines have suddenly become more attractive.<br />
<br />
The two main points I see given in STEAMs favour are:<br />
<br />
+ Anti Cheat<br />
+ Automatic patching<br />
<br />
Firstly, I dont tihnk STEAM has anything to do with anti cheat itself, you get a group of "noobs" who are under the impression STEAM will scan your hard disk for cheats and leaked HL2 files.. I really doubt this is the case. VAC was working before STEAM and, afaik, is totally seperate, so I dont see how STEAM is benefitting anyone in this way.<br />
<br />
Next I dont think the tradeoff between having to wait 5 minutes to load the game, and having patches automatically downloaded to my client is worth it. VALVe don't release patches that often, and to be quite honest the last few patches have casued a lot of hassle, a lot of internal engine stuff has been changed, TFC has had a few half-hearted fixes thrown in, which have obviously not been properly playtested. I understand why VALVe would want to concentrate on CounterStrike, but I hate to see a good game like TFC decline like it has, I have even offered to work on TFC for VALVe as an unpaid volunteer, VALVe simply did not reply to my E-Mail, which suggests to me thay dont _want_ anyone working on TFC, why I am not sure.<br />
<br />
Can anyone give anything that STEAM does that will make me say "wow yeah, I never thought of that, we really need STEAM after all"? If you can, or you can help me to actually get a game started under STEAM, you're welcome to reply.<br />
<br />
Thanks for reading.<br />
<br />
- Rob.</font></p>
	
