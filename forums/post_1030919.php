	<p><font face="verdana, arial, helvetica" size="2" >I'm surprised nobody's been talking about this yet (can't find any threads with a search, anyway).<br />
<br />
The flashbangs: Yes they look really pretty, and the audio muffling is a nice addition, but it seems the old face-the-other-way FB dodge no longer works. I'm guessing this is an intentional change to the game, but I think it's a bad one.<br />
<br />
I have two beefs with this:<br />
<br />
1) It's difficult to get close enough to your own exploding flashbang to take advantage of it when it goes off. Lobbing one around a nearby corner, intending to charge in after it detonates, will often get you blinded.<br />
<br />
2) Flashbanging your own team is too likely, because even if you throw it beyond their line of sight, they could still be in the radius. It's very difficult to judge and makes them dangerous to use unless you're right at the front of the rush (in which case, you're probably team-flash fodder yourself).<br />
<br />
So: I'd like to see the flash effect reduced according to line-of-sight, like it was in CS. I do concede that the new "bang" effect should mess up your hearing whether you're looking at the explosion or not.<br />
<br />
In pre-emptive response to one obvious argument: I appreciate that the fuzzy-vision could be attributed to a general symptom of concussion, rather than just staring at the flash itself, but at the same time I think an avoidable flash effect - as used in CS - makes for more well-rounded gameplay.</font></p>
	
