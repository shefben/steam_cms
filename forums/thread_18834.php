	<p><font face="verdana, arial, helvetica" size="2" >Dispite this being old news, I keep seeing these coming up repeatedly and felt the need to reiterate that they are false or have been changed.<br />
<br />
<b>1. <u>You will NOT need to be online to use Singleplayer or LAN</b></u><br />
Despite the multiple erroneous listings in the FAQ, Valve has stated that eventually they will have it so you do NOT need to be online to use singleplayer or lan.  Yes, I KNOW it says differently in the FAQ, that's because the FAQ is out of date at the moment.<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr>Chris Deeming: Will Steam be required to have an internet connection active when starting up a singleplayer game or a LAN game?<br />
<br />
<b>Gabe Newell: No, it won't.<br />
<br />
There will be a brief transition (like two weeks) when it is necessary, but then it will go away. It's a temporary issue.</b><hr></blockquote><br />
<br />
Source:  <a href="http://www.halflife2.net/forums/showthread.php?s=&amp;postid=143746#post143746" target="_blank">http://www.halflife2.net/forums/sho...3746#post143746</a><br />
<br />
<b>2. <u>Until Steam is working 100%, you CAN go back to WON and 1.5 for AS LONG AS YOU LIKE</b></u><br />
<br />
If you don&apos;t like Steam in it&apos;s current state, you CAN go back to Half-Life 1.1.1.0, Counterstrike 1.5 and Won.net.  No one is forcing you to use an improperly working program. Only AFTER Valve gets all the bugs out of Steam, they&apos;ll start migrating everyone to the new system, not before.<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr> A Note About WON and Counter-Strike 1.5<br />
September 17, 2003, 11:35 am · Chris Bokitch <br />
 <br />
There seems to be a lot of confusion over the proposed plan to pull the plug on WON. WON will be available until the Steam service is working the way it was intended to. We will not simply pull the plug. If you wish to, you can reinstall Half-Life (if necessary) and play Counter-Strike 1.5 the way you always have.<br />
<br />
I hope that eases some fears.<hr></blockquote><br />
Source: <a href="/news.htm" target="_blank">/news.htm</a><br />
<br />
<b>3.  <u>Steam is NOT Spyware</b></u><br />
This was an ugly rumor started when some people found files like “D:\ftp\mp3s\” and stuff like “Kazaa” in their Steam caches.  This has since been found to be an issue with the FAT32 file system not “cleaning” out the harddrive space of old data before creating the cache file.  Here&apos;s the exact quote from Valve<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr> The cache file gets created on the client using the Windows CreateFile() command. This command will create the file but not initialize it. Due to the large size of the file, we don't bother initializing the empty data blocks in it as this can take an appreciable amount of time. On NTFS machines, files created with this method happen to have the property of appearing empty (initialized to zero) but for FAT32 machines, it shows up as whatever old data was sitting on drive in the sectors that just got allocated for the cache. The people that are reporting this behavior are actually seeing old data from their hard drives.<hr></blockquote><br />
Source: <a href="http://www.halflife2.net/forums/showthread.php?s=&amp;postid=127676#post127676" target="_blank">http://www.halflife2.net/forums/sho...7676#post127676</a><br />
<br />
EDIT: Changed "bug" to "issue" because I got schooled <img src="images/smilies/smile.gif" border="0" alt=""><br />
<br />
EDIT2: Updated the first "number" for more clarity..<br />
<br />
Sincerely,<br />
Jeremy Dunn</font></p>
	
