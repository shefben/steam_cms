******* Check the Second Post to get the <font color="#D8DED3">Fonts</font> + Updated Optimal TrackerScheme File!********<br />
<br />
Check the screenshot to see how you can probably change the <font color="#D8DED3">fonts</font> and sizes to the maximum in CS 1.6<br />
<br />
It's really easy. You can use -ANY- font that you have in your <font color="#D8DED3">fonts</font> foler (usually c:\windows\fonts) and I've tested it with a lot of resolution, it works great.<br />
<br />
First, you'll need to use the GRAY skin in Steam. So load it in your steam and close steam totally.<br />
<br />
Now go to x:\steam\skins\gray\resource<br />
Open the TrackerScheme.res file with Notepad<br />
<br />
There will be a lot of text with comments (comments = preceeding with //) So you'll get your way around it.<br />
<br />
Go down to<br />
	//////////////////////// <font color="#D8DED3">FONTS</font> /////////////////////////////<br />
(you can change all the other colors like menu color and stuff like that if you want and are capable)<br />
<br />
