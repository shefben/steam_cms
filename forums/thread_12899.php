<br />
I tried out many font sizes and configurations and here's what's the best:<br />
<br />
Set all the <font color="#D8DED3">fonts</font> that are originally ranging from 8 to 14 to 12<br />
Set all the <font color="#D8DED3">fonts</font> that are originally ranging over 14 to 14.<br />
<br />
So a font size 8 would become 12, and 18 would become 14. This has the nicest and cleanest impact on your steam console, the CS console and everything else. It might seem a bit smaller in the consoles but who cares, you can still see very clearly.<br />
<br />
**The screenshot is an example of one of my tries where <font color="#D8DED3">fonts</font> under 14 were set as 10  and <font color="#D8DED3">fonts</font> over 10 were set as 12. A bit too small, but it looks really nicer**<br />
<br />
The only <font color="#D8DED3">fonts</font> that rest unchanged are the <font color="#D8DED3">fonts</font> in the Game Scores. (while pressing TAB)<br />
<br />
Also, If you wonder what font I used, it's Square 721 BT. It really fits Counter-Strike (and it's easily findable)<br />
<br />
Enjoy!<br />
<br />
Update: If you don't want to use the gray skin (I just use it to keep the original font sizes) you can always use the x:\steam\resource\Trackerscheme.res which will edit the <font color="#D8DED3">fonts</font> for the default steam skin.<br />
