<br />
It also exposes a fair few holes that can easily be exploited in online play if put into the wrong hands - which it no doubt already is. The most worrying thing about all this is its likely to delay HL2 for even longer because they've got to patch these holes in some way. The worst thing that happened to the current run of Half-Life games was the release of the SDK, armed with this kind of indepth knowledge of the engine a skilled coder can do pretty much what they please. <br />
<br />
