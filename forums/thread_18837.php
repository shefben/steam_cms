	<p><font face="verdana, arial, helvetica" size="2" ><b>STEAM &amp; FIREWALLS</b><br />
<b>Overview:</b> This post details how to enable your software firewall (hopefully) to work with Steam.  It might help to print the section that pertains to your individual firewall for easy reference (highlight it and choose "Print Selection" in the print menu).<br />
<br />
<b>Note: For hardware firewall help... go here<br />
<a href="/forums/showthread.php?s=&amp;threadid=25027" target="_blank">/forums/show...;threadid=25027</a></b> - thx Artic<br />
<br />
<b>Background (i.e What is going on here???) : </b><br />
Initially, the only program to attempt to connect to the internet that my firewall saw was "Steam.exe".  When I gave it full access, I thought I was done.  I could start Steam, open the Servers window and see excellent connections.  When I tried to play any game, however, it would at least triple the ping and begin to have major packet loss and lag.  My commands would take at least 30 seconds or longer to result in effect on the screen.<br />
<br />
When I fixed my firewall rules to include the <b>HL.exe</b> that the mod uses to access the Steam service, I found the hidden culprit.  Steam, at this time, uses a seperate HL.exe program for EACH MOD/PROGRAM.  This is causing a lot of connection problems for people. So try this out, it might just fix your problems too.<br />
<br />
<i>Please note that any software firewall will sacrifice a bit of ping (typically 10-20 ms) even if perfectly configured... but in my humble opinion, it is well worth it.</i><br />
<br />
Without further delay, here are some steps for Norton, McAfee,  ZoneAlarm &amp; BlackIce.  The others are discussed in general at the end.  Each one (except Black Ice) assumes that you have installed Steam.  Further they assume you can at least get to the main Steam menu to register or play games.  Black Ice can actually prevent Steam from running at all.<br />
<br />
<b>For Norton Internet Security Professional &amp; Personal Firewall</b><br />
1. Open up Internet Security Professional/Personal Firewall (right-click the icon and choose Internet Security or Firewall)<br />
2. Click "Internet Access Control"<br />
3. Create a rule in Internet Access Control for "Steam.exe" giving it Permit access.  This allows Steam to update and do it's thing easily.<br />
- a. Click "Add"<br />
- b. Navigate to "Steam.exe" (located in C:\Program Files\Steam\Steam.exe)<br />
- c. To give it full permission: in the Internet Applications menu, right-click the permission level for Steam.exe and choose "Permit this application to access the Internet"<br />
4. Create a rule in Internet Access Control for the mod's (DoD, CS) "HL.exe" giving it Permit access.  This allows the individual mod to send, receive and do it's thing easily. <br />
- a. Click "Add"<br />
- b. Navigate to "HL.exe" (located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe)<br />
- c. To give it full permission: in the Internet Applications menu, right-click the permission level for HL.exe and choose "Permit this application to access the Internet"<br />
5.  If you want to play any other mod, <b>you will need to create a rule for each HL.exe associated with the mod.</b> giving it full internet access.<br />
-----<br />
<br />
<b>For McAfee Personal Firewall &amp; Personal Firewall Plus</b><br />
1. Open up McAfee Personal Firewall/Plus (right click the icon, point to Personal Firewall, Click on "View Applications" or "Internet Applications")<br />
2. Create a rule for "Steam.exe" giving it "Allow Full Access" permission.  This allows Steam to update and do it's thing easily.<br />
- a. Click "New Allowed Application"<br />
- b. Navigate to "Steam.exe" (located in C:\Program Files\Steam\Steam.exe)<br />
- c. Click "Open"<br />
- d. To give it full permission: in the Internet Applications menu, right-click the permission level for Steam.exe and choose "Allow Full Access"<br />
3. Create a rule for the mod's (DoD, CS) "HL.exe" giving it "Allow Full Access" permission.  This allows the individual mod to send, receive and do it's thing easily. <br />
- a. Click "New Allowed Application"<br />
- b. Navigate to "HL.exe" (located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe)<br />
- c. Click "Open"<br />
- d. To give it full permission: in the Internet Applications menu, right-click the permission level for HL.exe and choose "Allow Full Access"<br />
4.  If you want to play any other mod, <b>you will need to create a rule for each HL.exe associated with the mod.</b> giving it full internet access.<br />
-----<br />
<br />
<b>For McAfee Firewall 4.0 or Internet Security 5.0</b><br />
1. Open up McAfee Firewall or Internet Security<br />
2. Create a rule for "Steam.exe" giving it "Allow Full Access" permission.  This allows Steam to update and do it's thing easily.<br />
- a. Configuration Assistant may be able to help you find the program, if not you will need to search in the Help file for adding a program to the internet access rules.<br />
- b. Navigate to "Steam.exe" (located in C:\Program Files\Steam\Steam.exe)<br />
- c. Give it full permission<br />
3. Create a rule for the mod's (DoD, CS) "HL.exe" giving it "Allow Full Access" permission.  This allows the individual mod to send, receive and do it's thing easily. <br />
- a. Configuration Assistant may be able to help you find the program, if not you will need to search in the Help file for adding a program to the internet access rules.<br />
- b. Navigate to "HL.exe" (located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe)<br />
- c. Give it full permission<br />
4.  If you want to play any other mod, <b>you will need to create a rule for each HL.exe associated with the mod.</b> giving it full internet access.<br />
-----<br />
<br />
<b>For ZoneAlarm Pro/Plus</b><br />
1. Open up ZoneAlarm<br />
2. Create a rule for "Steam.exe" giving it "Allow Full Access" permission.  This allows Steam to update and do it's thing easily.<br />
- a. Select Program Control | Programs, then click "Add"<br />
- b. Navigate to "Steam.exe" (located in C:\Program Files\Steam\Steam.exe)<br />
- c. Select Program Control | Programs, then click "Allow".  Be sure to give it full access<br />
3. Create a rule for the mod's (DoD, CS) "HL.exe" giving it "Allow Full Access" permission.  This allows the individual mod to send, receive and do it's thing easily. <br />
- a. Select Program Control | Programs, then click "Add"<br />
- b. Navigate to "HL.exe" (located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe)<br />
- c. Select Program Control | Programs, then click "Allow".  Be sure to give it full access<br />
4.  If you want to play any other mod, <b>you will need to create a rule for each HL.exe associated with the mod.</b> giving it full internet access.<br />
<br />
(Edit)<br />
"Adding custom ports in ZoneAlarm" <br />
You can allow communication through additional ports at High security, or block additional ports at Medium security by specifying individual port numbers or port ranges. <br />
To specify additional ports: <br />
Select Firewall-&gt;Main. <br />
In Internet Zone area, click Custom. <br />
The Custom Firewall settings dialog appears. <br />
Scroll to the security level (High) . <br />
Select the desired port type: incoming UDP, outgoing UDP, <br />
incoming TCP, or outgoing TCP. <br />
Type the port or port ranges you want to allow or block in the Ports field, separated by commas. For example, 139, 200-300 <br />
Click Apply, then click OK. <br />
(This is from ZoneAlarm Help...on UDP.Ports) <br />
I added in the INTERNET ZONE: <br />
Outgoing UDP:1200,27000-27015 <br />
Outgoing TCP:27030-27039 <br />
That was all i had to do to make it work...with my settings...otherwise i´ll get the "Error Server Verification Failed" <br />
