<br />
<b>For Black Ice</b><br />
1. Make sure the Application Control settings are all set to "Ask me what to do".  This will allow you to specify if the program can have access and not just block/terminate it on the spot.<br />
2. On the Tools Menu, select Advanced Application Protection Settings<br />
3. Click the Known Applications tab (list of applications Black Ice has detected on your computer)<br />
- a. Find the name of the application "Steam.exe"  <br />
- b. Check that the path leads into the Steam directory.  <br />
This is located in C:\Program Files\Steam\Steam.exe)<br />
- c. Make sure the Application Control &amp; Communication Control columns for that application are blank.  Black Ice regards a blank column as an authorization for this application.<br />
4. To allow the DoD game to Execute<br />
- a. Find the name of the application "HL.exe"  <br />
- b. Check that the path leads into the correct Mod in the Steam directory.  <br />
This is located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe)<br />
- c. Make sure the Application Control &amp; Communication Control columns for that application are blank.  Black Ice regards a blank column as an authorization for this application.<br />
5. If you want to play any other mod, <b>you will need to create a rule for each HL.exe associated with the mod.</b> giving it full internet access.<br />
6. Once you've made all the changes you want, click "Save Changes"<br />
<br />
FYI: <br />
Application Control - controls whether the application is even allowed to start<br />
Communications Control - controls whether the application is allowed to access the internet once it has started<br />
<br />
<i>Note: I have even less experience with Black Ice, if you see or know of any changes, please pm me. </i><br />
-----<br />
<br />
<b>For other firewalls (or) for Hardware Firewalls</b><br />
(Note: I am not familiar with these, but I am working on it)<br />
1. Open the Firewall Program/Configuration<br />
2. Research and learn how to add a program to the Firewall Rules<br />
3. Add a rule that allows the "Steam.exe" (located in C:\Program Files\Steam\Steam.exe) full access to the internet<br />
4. Add a rule that allows the "HL.exe" (located in C:\Program Files\Steam\SteamApps\YOUR@E-MAIL\Name of Mod\HL.exe) full access to the internet<br />
5.  If you want to play any other mod, you will need to repeat the step above and create a rule for each HL.exe associated with <br />
