	<p><font face="verdana, arial, helvetica" size="2" >More information on the new hostages:<br />
<br />
-------<br />
<br />
Movement and Navigation<br />
Hostages can now crouch, use ladders, and break windows as well as walk, run, and jump, allowing them to go just about anywhere a player can.<br />
<br />
Rescue from Far Away<br />
The “use” range of Hostages has been greatly increased, allowing CT’s to rescue Hostages from across the room.  If the Hostage is waving at you, you can “use” him!<br />
<br />
Emotions<br />
Hostages can be quite emotional.  They will fidget if bored, watch other players moving near them, and show fear and terror when gunfire erupts.<br />
<br />
Escaping<br />
If the Hostages find themselves alone and unguarded after about one minute into the round, they will sneak off and attempt to escape on their own.  Hostages will not try to escape if they have seen a Terrorist recently, or a Terrorist is anywhere in the nearby area (visible to them or not).<br />
<br />
Waving to CTs<br />
Un-rescued Hostages will wave at far away CTs they happen to see, unless that Hostage also sees a Terrorist at the same time.  If a nearby Terrorist is looking at a Hostage, the Hostage will not wave to a CT.  If a Hostage sees a CT and simultaneously sees nearby Terrorists who are not looking at him, he will shake his head "no" to the CT to warn him.<br />
<br />
Warning CT’s about Terrorists<br />
When a Hostage is rescued by a CT and there are Terrorists in the area, the Hostage will warn the CT of their presence.<br />
<br />
Fear and Fleeing<br />
Hostages can become afraid and even terrified.  A terrified Hostage will sometimes flee to a nearby hiding spot.  Hostages are afraid of gunfire, people being killed, and loud noises such as breaking windows.  They become terrified if they witness someone die, or are shot.  A Hostage will usually not flee if there is a Terrorist in the vicinity.<br />
<br />
Hostages and "Home" Spots<br />
The location of a Hostage at the beginning of a round is considered to be its "home" spot.  Hostages tend to stay in their home spots, and Terrorists can return Hostages to their home spots if they are caught while escaping or fleeing.  A Hostage will only leave its home spot if it is rescued by a CT, is injured, sees another Hostage die, or is left alone long enough to try to escape.<br />
<br />
Returning Hostages to their "Home" Spots<br />
Terrorists can return escaping or fleeing Hostages to their home spots by moving close to them while pointing their weapon towards them.  This intimidates the Hostage, who will turn around and walk back to his home spot – even if it is all the way across the map.  Hostages that are returning home will stop if they spot a CT, however.<br />
<br />
Technical Notes:<br />
<br />
Nav Files<br />
The new Hostage behaviors use data from the CS Bot .nav files.  If a map does not have an associated .nav file, the Hostages will revert to their legacy behavior system.<br />
<br />
Models<br />
The new hostage system uses models HostageA.mdl, HostageB.mdl, HostageC.mdl, and HostageD.mdl. These models are assigned via code, and override whatever hostage models were placed in the actual map.</font></p>
	
