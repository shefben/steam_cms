<br />
f.e. your steaminstall is in c:\steam<br />
<br />
==========================<br />
1. Exit Steam<br />
<br />
2. Delete c:\steam\steamapps\your@email.com\counter-strike source beta<br />
<br />
3. Start Steam<br />
<br />
4. Run CSS =&gt; memory error. OK, so far, so fine.<br />
<br />
5. Exit Steam and wait until its fully unloaded. (give them some seconds and make sure steam.exe ist not running (verify with taskmgr))<br />
<br />
6. COPY (not rename!) c:\steam\servers\serverbrowser.dll to c:\steam\servers\serverbrowser.org (for backup purposes)<br />
<br />
7. COPY c:\steam\steamapps\your@email.com\counter-strike source beta\bin\ServerBrowser.dll to c:\steam\servers\serverbrowser.dll and OVERWRITE it<br />
<br />
8. Make a new Directory(Folder) called "Servers" in c:\steam\steamapps\your@email.com\counter-strike source beta\<br />
<br />
9. COPY c:\steam\steamapps\your@email.com\counter-strike source beta\bin\ServerBrowser.dll to c:\steam\steamapps\your@email.com\counter-strike source beta\Servers\ServerBrowser.dll<br />
<br />
10. Start Steam<br />
<br />
11. Start CSS hopefully without memory error.<br />
==========================<br />
<br />
The error is caused by serverbrowsers.dll thats almost sure. There is some strange behaviour when trying the different DLLs that (i think) caused by windows and that the old dll is still in memory. Cause auf that you should overwrite the old file and not renamed. In my tests I was able to rename a locked file, but not to delete ist.<br />
You can try to restart the PC after the copy action, too.<br />
All Serverbrowsers.dll should be the same version.<br />
<br />
<br />
Update 1:<br />
Please remember that there is sometimes a strange behaviour with the windows file cache and/or the loaded dlls in memory.<br />
<br />
Cause of that, OVERWRITE ..\steam\servers\serverbrowser.dll and do NOT rename the file. (Make a backup first)<br />
<br />
One again: you must copy the original ..\steam\steamapps\your@email.com\counter-strike source beta\bin\ServerBrowser.dll<br />
<br />
 to:<br />
..\steam\Servers\ServerBrowser.dll<br />
 AND<br />
..\steam\steamapps\your@email.com\counter-strike source beta\Servers\ServerBrowser.dll<br />
<br />
Then restart you PC. Thats the safest method I figured out. I'm sorry for all those, where the fix doesn't work.<br />
