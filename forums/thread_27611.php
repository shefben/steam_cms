	<p><font face="verdana, arial, helvetica" size="2" >I have been dealing with Steam for awhile now, &amp; have a major concern about their Log In system that seems to be bound to your CS key.<br />
<br />
At first, it was a little annoying, but then I heard it could be used to set all your personal controls simply by creating your own login name may have made it worth it.  <br />
<br />
BUT: Then I notice it asks for you CD key.  This is where the problem comes in.  If you for example let your friend make an account with steam, &amp; pull out you CD key when it asks you, you in effect are giving your buddy your CD for free.<br />
<br />
So now, when your friend goes home to play counterstrike &amp; gets all logged in as he normally would, he is now playing on YOUR CD Key.  Meaning you would not be able to play until he is finished &amp; logs out.<br />
<br />
Still:  Don't forget that steam set it up to auto load at windows startup as well as auto log you in, so your friend may actually have your CD key used up for quite some time.<br />
<br />
I did go back and look for info of steam telling us of this issue, but so NO info warning us when creating accounts.  <br />
