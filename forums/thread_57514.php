	<p><font face="verdana, arial, helvetica" size="2" >In CS the sound suppressor is seemingly slightly less accurate (looser crosshairs with the sound suppressor on) and typically does less <font color="#D8DED3">damage</font> but in turn reduces the distance that the full noise can be heard and also has the quieter noise that can be replaced to a louder noise IIRC.<br />
<br />
Strangely enough in 1.3 it seemed that the sound suppressor against an armoured chest actually did more <font color="#D8DED3">damage</font> strangely enough but either way not enough to make a difference in the number of shots to kill.<br />
<br />
The <font color="#D8DED3">M4A1</font> either way kills in about 4-5 (haven't tested in a while, <font color="#D8DED3">damage</font> is affected by range as well) shots to an armoured chest suppressed or unsuppressed and 2 shots to an armoured helmet (unsuppressed does about 92, suppressed is in the 80's range) regardless of distance.<br />
<br />
In addition, the ROF is unaffected from what I can tell.<br />
<br />
<br />
In real life a sound suppressor typically doesn't affect accuracy in any real tangible way at short ranges (generally CS's ranges which are about 100-150m at most) but at longer ranges the loss of speed begins to dramatically affect the bullets trajectory.<br />
<br />
On the other hand it does typically lower recoil and pressure reduction sound suppressors can lower it by 20-30% or more depending on the weapon and the type of suppressor.<br />
<br />
At the same time the <font color="#D8DED3">damage</font> done by the round due to its slower speed is lower.<br />
<br />
IIRC a sound suppressor either bleeds off the gasses, uses wipes or the like to actually slow down the bullet or a combonation of both.<br />
<br />
The biggest sources of noise made by the bullet are the super sonic crack of it breaking the sound barrier and the noise of the gasses rushing out after the bullet as it leaves the barrel (the latter being the main issue).<br />
<br />
What most sound suppressors do is delay the release of the gas and allow more room for expansion so the pressure is lower and thus the sound of the gasses rushing out behind the bullet are lower. This in turn typically reduces the speed of the bullet but not by all that much. Things like washers and wipes are what reduce a bullets speed down sufficiently to lower it below supersonic speeds and in turn can affect the accuracy of the weapon a fair bit.<br />
<br />
The reduction of pressure system can significantly reduce the sound of the muzzle blast and thus the main source of noise that gives away a shooters position and this can be used with Super Sonic ammunition fine.<br />
<br />
Combined with subonic ammunition the reduction of pressure system can lower the noise of the weapon down to a very quite noise that in some cases is no longer than opening a can of coke or the noise of a hammer clicking.<br />
<br />
The <font color="#D8DED3">M4A1</font> in CS as far as I can tell uses just a pressure reduction system as you can still hear the crack of the shot as it flies by you when they are a good distance away but you cannot tell the direction easily.<br />
<br />
It should have the same accuracy if not slightly lower, it should do slightly less <font color="#D8DED3">damage</font> and have lower recoil.<br />
<br />
<br />
In reply to Mortal Monkey:<br />
A sound suppressor typically does not add to the barrel length and thus doesn't make it more accurate and the longer the barrel the lower the recoil and greater the accuracy. Why on earth would a longer (and thus heavier) barrel allowing for the bullet to spin even more increase the recoil?</font></p>
	
