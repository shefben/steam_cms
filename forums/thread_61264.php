	<p><font face="verdana, arial, helvetica" size="2" >Hi,<br />
<br />
A lot of people have recently created new accounts and tried to re-use their cd-keys. <font color="yellow">This will not work</font> - even if you use the same email address that as your old account name.<br />
<br />
The confusing part (sorry) is that before Wednesday's update, your account name and email address were the <i>same thing</i> In Steam. All old accounts are still that way, unless you go in and change the email address field (via "Change Contact Email" in the Settings page). <br />
<br />
If you make a new account now, and set the "email address" field to your same email, it is still a <b>different account</b>.<br />
<br />
So, here is an example.<br />
<br />
- Long ago, Joe created an account "Joe@email.com" on Steam, and registered his cd-key with it<br />
<br />
- Yesterday, Joe created an account "JoeRulz", on Steam, and set it to use his "Joe@email.com" email address. He verified the key, then tried to play. It asked him for his cd-key, but then told him it was already in use.<br />
<br />
Joe will need to login to his <b>old</b> account. This means typing "Joe@email.com" in the Steam Login dialog where it says "Account Name". This account will still have his cd-key registered, his Friends list, etc. <br />
<br />
