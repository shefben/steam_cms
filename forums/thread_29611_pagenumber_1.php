	<font face="verdana,arial,helvetica" size="1" > <b>If Steam asks for your CD-KEY again</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The Steam system has begun filtering through accounts that used duplicate cd-keys to subscribe to the games. When it finds a duplicate, it unsubscribes <font color="red"><b>but does not delete</b></font> that account. Only the first account which used a given cd-key will remain subscribed.<br />
<br />
If you are using one of the duplicate accounts, you'll see the games get moved to Available Games in the Games window, and Steam will ask you for your cd-key again if you try to play one. <br />
<br />
If you created multiple Steam accounts using the same key, then you'll need to use the first one.<br />
<br />
repeat: the first account to use a given cd-key will REMAIN subscribed and can be used to play the games.<br />
<br />
The system <font color="red"><b>will not</b></font> automatically delete any accounts.</font></p>
	
