<br />
Halo DM sucks, HL2 DM heavily abids by a Halo DM rule set. HL2 DM doesn't suck. I'm too much of a fan of Valve to say it for myself. However, drastic changes need to be made to the gameplay, or HL2DM will go down as fast as Halo DM did... and hey... and least in Halo DM you could easily set the Heath to half which evened out most of it's imbalances... what can you say about HL2 plaguing issues?<br />
