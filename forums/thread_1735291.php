Half-Life 2 DM: You spawn with a accurate pistol, a useless spammy machine gun, and grenades...<br />
<br />
What in the hell were you doing when designing your death-match... honestly, you went from the best DM game ever (HL1DM) to this, this thing....<br />
<br />
It's a tragedy. I'm going to walk into Microsofts Office and kill them for popularizing Halo. It destroyed my favorite DM game of all time.<br />
<br />
I played Halo Valve. Most of us here have... and most of us think it was over hyped bull beans and that the DM was the worst DM to come to gaming since... well never. It's the worst MP game in history.<br />
<br />
Why would you base your model off of it... I'm shellshocked that you would think of doing it, not to mention taking the time and effort to accually go through with such an appalling idea. I hope this wepaon setup is some kind of joke and will be change when the user community, if they haven't allready forgotten you like you have of them, produces some maps.<br />
<br />
What else is bad beside such Halo-ish copycating? <br />
<br />
Useless --Halo style-- hand grenade spam. Unless you combine it with the gravity gun to make it into a timed rocket launcher, there entirely useless, and player who doesn't have the stamina to run away will simply blast away the grenade with the gravity guns primary fire. In MP games, nobody dies of grenades anymore. There a useless toy.<br />
<br />
No crowbar. WTF were you thinking...<br />
<br />
The pistol is meak, oddly enough where it was over-usefull in SP game it's underusefull in the MP game. Really, it's made as a backup for after you've emptied an entire SMG clip into an opponet, which you will often do because of the total uselessness of the SMG.<br />
<br />
I don't know why you dicided to go to CS 1.5, take the glocks damage, the machine guns accuracy, and smake 'em together with a new model and call it a SMG. It's an abomination of the gun. The orignal was a PICK UP and it' KICKED @$$, and the grenades, of which you could hold 10 of them, where  awsome fun to use, and combo.<br />
<br />
The only counter to that is the Plasma gun, which is much more rarely used, but also kicks @$$. I like this gun in SP, and I like it more in MP, to bad you didn't know what to do with two automatic guns and instead made one crap and another a underavailable weapon. You have 1 on each map....<br />
<br />
They next tragedy is the shotgun... OMG.... what have you done to my favorite weapon in FPS history. I kid you not. It was a amazing weapon that when properly utilized blew people away. Now, unless your point black, it takes 5 shots to kill. Good luck doing that when someone with a tiney 357 comes around the corner and blasts you away with a 100% accurate 100 DMG shot... its' not easy, it's not fun, and it's more of a relief to accually kill someone in that situation that a sense of accomplishment. Even the SMG counters the shotgun in close range combat.<br />
<br />
The next gun is the 357. I won't say to much, as there is a official hate thread for it already ( and maybe soon a website ) that points out that taking this weapon, that requiring 2 shots would have been overpowered against any of the other guns already, and turning it into a one shot kill machine, is mindnumbing. Honestly, you have a spammy machine gun, a meak pistol, a drasticly underpowered shotgun, and what do you throw it to even it out... a insta gib pistol... does it make any sense to you in retrospect.... did you even play this before you shipped it out?<br />
<br />
