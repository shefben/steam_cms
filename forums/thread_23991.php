	<p><font face="verdana, arial, helvetica" size="2" >With the recent hl2 code leak, I'm concerned about HL2 and what steps valve will take to ensure proper security of the gaming experience and of everyone's future purchase.<br />
<br />
In particular, I'm hoping that Valve will make a public statement that will satisfy the community that proper measures have been taken.<br />
<br />
What I'm most concerned about:<br />
<br />
- That the cd-key is never transmitted to a privately run game server in a form that can be reversed to get the original cd-key--what I'm thinking about here is, now that the code is out, it would be simple for someone to break their algorithm and get the key.  What would be better is either to transmit part of the key that can be verified without the rest, or to transmit a hash.  If the full key is ever sent directly to the game server, cheat and hacker groups will just open key-collector servers so they can gather thousands of cd-key's and then proceed to cheat and do other things with impunity.<br />
<br />
- That Steam hasn't been compromised by this leak, and thus will be able to authenticate hl2 clients to ensure that people are not using customized cheat versions, and also that the steam system hasn't had enough code exposed to enable a hacker to hijack it to install malware on thousands of peoples' PC's.<br />
<br />
- That the servers which handle billing for Steam are not directly connected to the Valve dev. network in a way in which "trusted" PC's that could potentially be compromised in the future can access them with lower security restrictions.  I really don't want to buy myg0t and friends a brand new video card with my credit card info.<br />
<br />
If you're concerned about issues like this, and want Valve to address these issues publically or at least give us some reasonable assurance, please add your comments.</font></p>
	
