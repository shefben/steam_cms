	<p><font face="verdana, arial, helvetica" size="2" >ok. All my other posts have lead me to make this thread. I called the tech support of my router and we worked on this together through trial and error. The main problem is that steam lists a range of ports to open up on your router to get steam to work. What they fail to tell you that "port forwarding" will only forward certian ports to one ip address, not to. So to get by this problem this is what you have to do:<br />
<br />
Opened ports for computer A (HLDS computer) <br />
27010-27012  UDP<br />
27015-27016 UPD<br />
27040 TCP, UPD<br />
5100-7500 TCP<br />
<br />
Opened ports for computer B (Steam CS computer)<br />
27000-27009 UPD<br />
27013-27014 UPD<br />
27030-27039 TCP<br />
1200 UPD<br />
27056   TCP, UPD  (this port is for steam ip address verification if open it will allow steam to see the ip address of the Ethernet card, not the router.)<br />
<br />
ok, now everybody is looking going why only those 3 ports for the server. the reson for that is when you create a server and it logs into the Steam network it athenticates on ports 27010 and 27040. (noticed this from when you start HLDS and look in the console at the ip addresses it lists, the numbers after the colon are the port info example:<br />
Adding auth server 65.73.232.251:27040<br />
Adding auth server 65.73.232.253:27040<br />
Adding master server 207.173.177.11:27010<br />
Adding master server 207.173.177.12:27010).<br />
<br />
 Now the reason for the 27016 port on the server machine is because when you creat the server you need to change the upd port to 27016 because you need to enable port 27015 for the gaming machine. whith this configuration I can log into my server by lan connection, my friends can log in through favorites, and it will post on the server list. (due to the port being allowed to go through the server. I know there is a lot of useless information in this but I figured you would like to know how I can to this realization. But the only way this will work for a server is if you dowload the steam HLDS program and not run the HLDS from the steam client itself.<br />
(<a href="/getsteamnow.php" target="_blank">getsteamnow.php</a> ) and select dedicated server. With this install you can run HLDS and also if you all ready have a server setup all you nave to do is cut and past your dedicated server info over the stuff that gets installed and it will work. (basically the machine running the server does not need to be logged into steam. the server will log itself when you connect it to the steam network)<br />
<br />
Revison: Oct. 25, 2003 (Added/changed some ports after further testing with different firewalls and also doing more research on what ports steam/WON used.)<br />
<br />
If I have confused anybody with this let me know. I can try and talk you through it from AIM. Thanks!!</font></p>
	
