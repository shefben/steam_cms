	<font face="verdana,arial,helvetica" size="1" > <b>NVIDIA &amp; Half-life 2</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The highly-anticipated Half-Life 2 game will have a major bug with current DirectX 9.0 hardware resulting in impossibility in enabling Full-Scene Anti-Aliasing, a popular feature that dramatically improves image quality in games. Apparently, there is a limitation in DirectX 9.0 and/or DirectX 9.0-compliant hardware that will not allow the function to be enabled on certain graphics cards if the workaround is not found.<br />
<br />
According a Valve officials quoted in forums at HalfLife2.net web-site, there are problems with the way that current hardware implements FSAA. If you enable it, you will see a lot of artifacts on polygon boundaries due to the way that current graphics processors sample texture subjects with FSAA enabled.<br />
<br />
Valve continued that this is a problem for any application that packs small textures into larger textures. The small textures will bleed into each other if you have multi-sample FSAA enabled. <br />
<br />
Currently both leading graphics chips designers use multi-sampling or hybrid multi-sampling + super-sampling methods to for FSAA.<br />
<br />
The developers of the legendary Half-Life game said that drivers are not likely to solve the problem, however, it still can be solved for graphics cards based on VPUs from ATI Technologies, such as RADEON 9500-, 9600-, 9700- and 9800-series. As for NVIDIA GeForce and GeForce FX-series, there are practically no chances to find a workaround, according to Valve. <br />
<br />
Some industry sources indicated that the problem with such FSAA is a known one and is to be addressed in DirectX 9.1 and next-generation graphics processors with Pixel Shaders 3.0 and Vertex Shaders 3.0, such as ATI Technologies&apos;s code-named R420 and NVIDIA&apos;s code-named NV40 VPUs and derivatives. Both next-generation products will come later than the Half-Life 2 that is expected to be available by October. <br />
<br />
You can find the thread on the matter with quotes from Valve officials here. <br />
<br />
Stay tuned with us because we are looking forward to bring you some comments from ATI Technologies and NVIDIA about the situation.<br />
