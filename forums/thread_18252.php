	<p><font face="verdana, arial, helvetica" size="2" >Well i did some more searching and found this...<br />
Home &gt; Error Messages &gt; "NET_SendPacket ERROR: WSAEINTR"<br />
<br />
<br />
This error may be caused by the following things:<br />
<br />
1.) Bad Winsock dll<br />
2.) Bad TCP/IP stack<br />
3.) Conflicting Ports<br />
4.) Firewall<br />
5.) VIA KT133A motherboard chipset w/o proper 4-in-1 patch<br />
6.) Windows 2k w/o service pack 2<br />
7.) Punkbuster conflict<br />
8.) Wrong vCache settings<br />
<br />
Also, this MS Knowledge Base Article may solve this issue.<br />
<br />
Possible solutions:<br />
<br />
1. Bad Winsock dll: <br />
Contact Microsoft Technical Support for step-by-step instructions on how to reinstall Winsock for your operating system. <br />
2. Bad TCP/IP stack: <br />
Remove all TCP/IP instances from Network Properties. <br />
Go to Control Panel &gt;&gt; Add/Remove Programs &gt;&gt; Windows Setup and remove all of the Communications components. Restart then repeat the same procedure, but instead check the box and install the components. Next readd the TCP/IP protocol in your network properties.<br />
<br />
3. Conflicting Ports: <br />
Try adding this to the end of your command line:<br />
+clientport 27055 <br />
4. Firewall: <br />
Disable your firewall. <br />
If you run BlackICE setup an allow set for ports 27005 thru 27030. <br />
If you have Windows XP disable the internal firewall, or if you use ICS disable that. <br />
5. VIA KT133A motherboard chipset w/o proper 4-in-1 patch: <br />
Visit <a href="http://www.via.com" target="_blank">www.via.com</a> and get the latest 4-in-1 patch. <br />
6. Windows 2k w/o service pack 2 <br />
Read the following Microsoft Knowledge Base article here. <br />
7. Punkbuster conflict: <br />
Disable the PunkBuster client on your machine and try non-PunkBuster servers.<br />
<br />
8. Wrong vCache settings <br />
To fix this problem you need to go to the system.ini file and check the [vcache]. <br />
If there is nothing there, add these lines under the word [vcache]:<br />
MinFileCache=****<br />
MaxFileCache=****<br />
<br />
It should look like this.<br />
[vcache]<br />
MinFileCache=****<br />
MaxFileCache=****<br />
<br />
<br />
Replace **** with one of the following values:<br />
<br />
If you have from 16-32 MB of RAM:<br />
MinFileCache=2048<br />
MaxFileCache=2048<br />
<br />
If you have from 32-96 MB of RAM:<br />
MinFileCache=4096<br />
MaxFileCache=4096<br />
<br />
If you have 128 MB of RAM or more:<br />
MinFileCache=8192<br />
MaxFileCache=8192 <br />
<br />
Another way would be setting the maximum amount of RAM HL/CS is allowed to use. <br />
Right click on your CS shortcut and select "Properties". Right now the Target line should read something like this:<br />
<br />
CS retail version: C:\Sierra\Counter-Strike\cstrike.exe -console<br />
CS mod version: C:\Sierra\Half-Life\hl.exe -game cstrike -console<br />
<br />
Change it to this: <br />
CS retail version: C:\Sierra\Counter-Strike\cstrike.exe -console -heapsize ***** <br />
CS mod version: C:\Sierra\Half-Life\hl.exe -game cstrike -console -heapsize *****<br />
<br />
Replace ***** with 75% of your total memory in KB, ex. you have 128 MB RAM then heapsize should be 98304. <br />
1 MB = 1024 KB.<br />
