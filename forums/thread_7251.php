	<p><font face="verdana, arial, helvetica" size="2" >Okay, how do it...<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr><font size="3">HOW TO USE CUSTOM SPRAYS!</font> <br />
<br />
<b><i>1) Take your custom spray, which should be a file called pldecal.wad.<br />
2) Paste it under c:\...\steam\steamcache\emailaccount\counter-strike\cstrike\<br />
3) Select overwrite<br />
4) Delete custom.hpk and custom.hp2(If it exists)<br />
5) Right click on pldecal.wad, and select properties.<br />
6) Here, set it's attribute to READ ONLY. This will prevent Steam from overwritting the file EVER AGAIN! Neat huh?<br />
</i></b><hr></blockquote><br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr><font size="3">EXAMPLE OF MAKING A SPRAY (Read more into thread for BETTER detail...)</font> <br />
Here is visual proof of a spray in the making.<br />
This is one i made last night, works, and kicks alot of ass!<br />
<br />
<br />
<a href="http://dumbasstm.com/silenthunters/mylogo_noedit.jpg" target="_blank">A picture you want to make into a spray...</a> <br />
<br />
I then took this pic, resized it to 64x128pixels, and reduced color depth to 256 colors (using the optimized octree(sp) method). <br />
<br />
Finally, I set the 255th color on the pallet to pure blue(0 0 255), and since this pic has no other pure blue colors in the pallet, I am done! All i need to do is compile it.<a href="http://dumbasstm.com/silenthunters/mylogo.bmp" target="_blank"> The new spray formatted pic should look like this...</a> <br />
<br />
And there you have it! Visual proof that manual works better than those crappy spray makers!<hr></blockquote><br />
<br />
<br />
There ya go, enjoy!<br />
