	<p><font face="verdana, arial, helvetica" size="2" >The changes in this release are directed at reducing the problem some users are experiencing with sound stuttering. The sound stuttering is not indicative of a sound problem, sound stuttering is only a symptom of texture thrashing on your video card or AGP memory. For information on how to reduce texture thrashing visit this link on our support site:<br />
<br />
<a href="/support.php/cgi-bin/steampowered.cfg/php/enduser/std_adp.php?p_faqid=280" target="_blank">/support.php/cg...php?p_faqid=280</a><br />
<br />
This update will fix sound stuttering that users were experiencing that would last for longer than a few seconds during normal gameplay. This update will also eliminate this same behavior following a quicksave or autosave. There will still be a short pause while the autosave happens, but not the more drawn out stuttering behaivior.<br />
<br />
We are still investigating another performance problem on some hardware, which will manifest where the game is getting into a state where performance drops to less than 5 fps and does not recover or crashes.<br />
<br />
Next week we will be releasing the Source SDK, along with a surprise for the community.</font></p>
	
