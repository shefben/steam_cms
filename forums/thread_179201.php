<br />
Once you have registered your CD-KEY with Valve through steam, If you can be bothered to download all the HL2 and CS:source content you can play without the DVD. Just create a new steam install in a different location, open that install of steam and download the content through steam. HL2 and CS:source will appear is the "My Games" list and you can install them from there. Same way ATI vouchers had a CD Key your CD Key is registered to your Steam account and it's that key that allows you to download the content that you have purchased.  This should install it the same way as everyone who purchased it through steam meaning you won't have to use the DVD to play it.<br />
