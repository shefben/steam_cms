	<font face="verdana,arial,helvetica" size="1" > <b>If you never get the Validation Email</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >If Steam asks you for a validation code when you try to install the games (for instance, when you double-click on Counter-Strike and it says "You have chosen to add <font color="yellow">Half-Life Platinum Pack</font>), it says that it sent you an email with the validation code in it.<br />
<br />
Due to a bug, that email might not be sent. If you don't have the email in 15 minutes, go to the main Steam window and do this:<br />
<br />
- click Settings<br />
- click Verify Email Address<br />
- click Next<br />
<br />
This will send the email. It should arrive in a couple of minutes. If it doesn't, the system may be loaded down - but it will definitely send it (no bug in this one <img src="images/smilies/smile.gif" border="0" alt=""> ). When it arrives, enter the code into the box. Steam will then let you proceed to the window where you enter your cd-key to subscribe to the games.<br />
