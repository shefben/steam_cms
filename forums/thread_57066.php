	<p><font face="verdana, arial, helvetica" size="2" >I grabbed this from the other post so that people could view it easier, thanks to the original author.<br />
<br />
EDIT: I have just released software for you to easier use this feature! Check out my website: <a href="/forums/showthread.php?s=&amp;threadid=51410" target="_blank">/forums/...;threadid=51410</a><br />
<br />
--<br />
the "sv_downloadurl" cvar <br />
<br />
============================================ <br />
Half-Life Fast HTTP Asset Downloading Instructions <br />
V1.02 January 22, 2004 <br />
<br />
<br />
SUMMARY <br />
------- <br />
<br />
Half-Life now supports the download of custom content (maps, <br />
skins, sounds, <br />
and so on) from a HTTP server (ie: webserver) when connecting, <br />
instead of <br />
downloading directly from the game server itself. <br />
<br />
HTTP downloading has three major benefits: <br />
<br />
1) There is no impact on the game server when downloading <br />
custom content, as <br />
a completely separate HTTP content server does the transfer <br />
<br />
2) Players will download custom content at their maximum <br />
transfer rate, <br />
resulting in *much* faster downloads <br />
<br />
3) Players who are downloading custom content do not take up a <br />
player slot <br />
on the server while they wait <br />
<br />
<br />
WHAT PLAYERS CONNECTING TO A SERVER WITH CUSTOM CONTENT WILL SEE <br />
---------------------------------------------------------------- <br />
<br />
When a player connects to a server utilizing custom content, one <br />
of two <br />
things will occur: <br />
<br />
1) If the server is not configured for HTTP download, the game <br />
server <br />
will use the normal "trickle" download mechanism <br />
<br />
2) If the server is configured for HTTP downloading, the player <br />
will <br />
see an enhanced download dialog, and much faster downloads. <br />
While <br />
this content is downloading, they are not connected to the <br />
game <br />
server. Once the download finishes, they are automatically <br />
reconnected to the game server, and ready to play. <br />
<br />
<br />
CONFIGURING YOUR SERVER TO USE FAST HTTP ASSET DOWNLOADING <br />
---------------------------------------------------------- <br />
<br />
1) Make a list of all the custom files (bsp, wav, mdl, etc) <br />
used on your server <br />
<br />
2) Put these files on a HTTP server (ie: webserver), keeping <br />
the folder <br />
layout intact <br />
<br />
3) OPTIONAL: To display a "banner image" to players that <br />
download content, <br />
place a 340x56 pixel image in the file &lt;custom content <br />
directory&gt;/gfx/banner.gif <br />
<br />
4) Set the sv_downloadurl cvar to <br />
"http://yourserver/custom_content_directory/" <br />
<br />
<br />
Example: <br />
<br />
Your server uses the non-standard map "de_generic", which <br />
requires the following files: <br />
<br />
C:\Program <br />
Files\Steam\SteamCache\your@email.address\Counter-Strike\cstrike\maps\de <br />
_generic.map <br />
C:\Program <br />
Files\Steam\SteamCache\your@email.address\Counter-Strike\cstrike\de_gene <br />
ric.wad <br />
C:\Program <br />
Files\Steam\SteamCache\your@email.address\Counter-Strike\cstrike\sound\a <br />
mbience\de_generic_a.wav <br />
C:\Program <br />
Files\Steam\SteamCache\your@email.address\Counter-Strike\cstrike\sound\a <br />
mbience\de_generic_b.wav <br />
C:\Program <br />
Files\Steam\SteamCache\your@email.address\Counter-Strike\cstrike\sound\a <br />
mbience\de_generic_c.wav <br />
<br />
<br />
You have web space (perhaps through your ISP) with the URL <br />
"http://www.turtlerockstudios.com/~matt" that you will use to <br />
host your <br />
custom content. <br />
<br />
To do this, you copy the files needed for "de_generic" to your <br />
webspace, <br />
placing them in a folder called "my_cstrike" in your webspace <br />
top-level directory, <br />
keeping their directory layout intact. <br />
<br />
You now have the following files in your webspace: <br />
<br />
<br />
<a href="http://www.turtlerockstudios.com/~m.../de_generic.map" target="_blank">http://www.turtlerockstudios.com/~m.../de_generic.map</a> <br />
<br />
<a href="http://www.turtlerockstudios.com/~m.../de_generic.wad" target="_blank">http://www.turtlerockstudios.com/~m.../de_generic.wad</a> <br />
<br />
<a href="http://www.turtlerockstudios.com/~m...mbience/de_gene" target="_blank">http://www.turtlerockstudios.com/~m...mbience/de_gene</a> <br />
ric_a.wav <br />
<br />
<a href="http://www.turtlerockstudios.com/~m...mbience/de_gene" target="_blank">http://www.turtlerockstudios.com/~m...mbience/de_gene</a> <br />
ric_b.wav <br />
<br />
<a href="http://www.turtlerockstudios.com/~m...mbience/de_gene" target="_blank">http://www.turtlerockstudios.com/~m...mbience/de_gene</a> <br />
ric_c.wav <br />
<br />
You now set the "sv_downloadurl" cvar on your game server to <br />
"http://www.turtlerockstudios.com/~matt/my_cstrike/". Now, <br />
players who connect <br />
to your server will automatically download the map "de_generic" <br />
and its required <br />
files from your webspace when they connect (unless they already <br />
have it, of course). <br />
<br />
<br />
TECHNICAL DETAILS <br />
----------------- <br />
<br />
* The maximum length of the sv_downloadurl is 127 characters <br />
<br />
* A username and password can be specified for the HTTP server <br />
by embedding <br />
them in the sv_downloadurl like so: <br />
<br />
<a href="http://usernameassword@www.somewhere.com/" target="_blank">http://usernameassword@www.somewhere.com/</a><br />
<br />
( Note - recent IE patches make disable this functionality ) <br />
<br />
* If a connecting player has an existing file of the same name, <br />
it will not <br />
be overwritten. The download file will be skipped. <br />
<br />
* Certain game-crtical and/or dangerous files cannot be <br />
downloaded (ie: *.exe, *.vbs, etc)</font></p>
	
