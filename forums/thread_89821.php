<br />
<b>Give the shield a life span of 250 armour points and improve the animations, so:</b><br />
<br />
<b>1.</b>  The shield can sustain damage and eventually become useless, much like regular armour.<br />
<br />
At the moment the shield is the only inprenitrible object in the game. We can shoot through almost any map wall (wood, brick, concrete and even steel), boxes of all types and sizes. But, we can't shoot through a shield?!?<br />
<br />
