<br />
The Steam beta kept the user identification information (your Steam usernamd and password) in a special directory (.steam) in your account's home directory.<br />
<br />
When the new version came out, Valve didn't tell us that it wan't 'beta-aware', and when you ran the new Steam, it looked in ~/.steam/ for the Steam account information, which, since the Steam user database was reset, is no longer valid.  This is what causes the 'Connection Reset' problem- it's more of a 'Bad Password' problem, just badly labeled, or an overgeneralized error message.<br />
<br />
~ is Unix-ese for 'home directory', and since the Steam data was kept in a directory '.steam' in your home, its path is '~/.steam/'.<br />
<br />
As another side note, files and directories with names that start with a '.' are hidden- they won't show up unless you ise the 'a' switch on the ls command, like this:</font><blockquote><pre><font face="verdana,arial,helvetica" size="1" >code:</font><hr>ls -a<hr></pre></blockquote><font face="verdana, arial, helvetica" size="2" ></font></p>
	
