	<font face="verdana,arial,helvetica" size="1" > <b>Problems with the new Steam after running the beta?  Solution within!</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The new Linux Steam client has a couple problems that can crop up on a lot of Linux-based computers.  It took me and a few others a while to track down the problem, but here are the problems, and their corresponding solutions:<br />
<br />
<b>Broken Pipe error</b><br />
After downloading the hldsupdatetool.bin file, and executing it, it may abort something like this:</font><blockquote><pre><font face="verdana,arial,helvetica" size="1" >code:</font><hr>Enter 'yes' to accept this agreement, 'no' to decline: yes
sh: line 1: uncompress: command not found
Broken pipe
[hlds@funball hlds_l]$ <hr></pre></blockquote><font face="verdana, arial, helvetica" size="2" ><br />
<br />
If it does, the error actually is telling you ecactly what the problem is- you lack the 'uncompress' tool, which is rather common, but some Linux distros (more notable some Red Hat versions) lack.  You can get the RPM you need <a href="http://rpmfind.net/linux/rpm2html/search.php?query=uncompress" target="_blank">here</a>.  Once you have it, su up to root, go to where the file is, and run this command:</font><blockquote><pre><font face="verdana,arial,helvetica" size="1" >code:</font><hr>rpm -i ncompress-4.2.4-33.i386.rpm<hr></pre></blockquote><font face="verdana, arial, helvetica" size="2" > Assuming you get no error messages, you should now be able to log out as root, log back in with your HLDS user, and successfully run the .bin file.  It'll extract one file, 'steam'.<br />
<br />
<b>The 'Connection Reset' Problem</b><br />
At first, this one was a bear to nail down, but it seems that we've got it.<br />
<br />
You'll know you ahve this problem when, when you try to run steam to update a server, or to create an account, this happens:<br />
</font><blockquote><pre><font face="verdana,arial,helvetica" size="1" >code:</font><hr>[hlds@funball hlds_l-steam]$ ./steam -update tfc . myusername mypasswd
Checking bootstrapper version ...
Getting version 0 of Steam HLDS Update Tool
Connection Reset <hr></pre></blockquote><font face="verdana, arial, helvetica" size="2" ><br />
<br />
If this happens, all you need to do is run this command...<br />
</font><blockquote><pre><font face="verdana,arial,helvetica" size="1" >code:</font><hr>rm -rf ~/.steam/<hr></pre></blockquote><font face="verdana, arial, helvetica" size="2" >...and you should be all set.  Run that command, and start from the 'create an account' step in the Readme file.<br />
<br />
I hope this helps!</font></p>
	
