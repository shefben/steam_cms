	<p><font face="verdana, arial, helvetica" size="2" ><b><u>The LunchTime Digest</u><br />
August 10, 2004</b><br />
<br />
I&apos;m doing a polka-dance to <a href="http://www.bitstorm.org/happyjoy/" target="_blank">Stinky Wizzleteats' "Happy Happy Joy Joy."</a><br />
"Why ever are you doing that, Lunchtimemama?"  you ask?<br />
"Because Half-Life 2 starts preloading TODAY, you eediot!" I reply.  Yes sirs and madams, <i>THAT</i> 'Half-Life 2!'  It&apos;s really gonna happen.  Right now.  FOR REAL!<br />
<br />
While you probably aren&apos;t getting jiggy with your <a href="http://www.amazon.com/exec/obidos/tg/detail/-/B000008JY8/qid=1092197757/sr=8-4/ref=sr_8_xs_ap_i4_xgl15/002-5368362-9720859?v=glance&amp;s=music&amp;n=507846" target="_blank">"You Eediot" CD</a>, I&apos;m sure everyone is as excited as I at this step toward <u>RELEASE</u>.  I&apos;ve been following a lot of the "Will you buy HL2 retail or through Steam?" threads and it seems that a majority of gamers still like to have a box they can ogle.  This puts quit a few people in an odd situation.  <i>If you plan to buy the box, should you preload?</i>  The answer is an unyielding <b>yes</b>!<br />
<br />
If you were to purchase the boxed game without preloading, you would be spending a good 3-5 minuets waiting for the game to copy from your 24-52x CD ROM.  And if HL2 is anything like CZ, you will need to wait 3-5 minuets to convert the installation to the Steam cache (if you want multiplayer).  That&apos;s a good 6-10 minuets which could have been spent <i>playing Half-Life 2!!!</i><br />
<br />
On the other hand, if you preload the game, you need only spend the 10-20 seconds inputting the CD key before you&apos;re ready to go!  The only reason not to preload is if you don&apos;t plan to register your copy with Steam, which in turn makes me wonder why you are reading the Steam forums.<br />
<br />
So my friends, you see, <i>even if</i> you are planning to buy the retail version of HL2, you should <u>still preload Half-Life 2</u>.  There&apos;s no good reason not to!  Now, if you&apos;ll excuse me, there&apos;s a song about a whaleЕ NOЕ a song about <i>being happy</i> with my name on it.  Until next time friends!<br />
