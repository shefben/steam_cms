	<font face="verdana,arial,helvetica" size="1" > <b>**How To.. Backup Your Steam Folder and Games onto CD or DVD**</b></font>
	<p><font face="verdana, arial, helvetica" size="2" ><font size="3">Steam <b>MUST</b> be <font color="red">closed</font> before beginning!</font><br />
-----------------------------------------------<br />
<b><font size="4"><font color="red">*Using WinRAR to backup your games in Steam*</font></font></b><br />
<br />
Follow the first set of instructions, in order to compress the Steam folder and make them ready to burn <img src="images/smilies/smile.gif" border="0" alt=""><br />
<br />
1a) Find the Steam folder, by going through the folders on the harddrive &gt;&gt;&gt;<br />
<font size="1"><font color="orange">Found here; "My Computer" &gt;&gt;&gt; "x:\" &gt;&gt;&gt; "Program Files" &gt;&gt;&gt; "Valve" &gt;&gt;&gt; "Steam" &gt;&gt;&gt;</font></font> <br />
<br />
<font size="3"><font color="red"><i>OR</i></font></font><br />
<br />
1b) Open WinRAR and click the "Browse" button to search for the SteamApps folder &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(This might be easier if you have used WinRAR before and/or familiar with file tree structures)</font></font><br />
Here is an <a href="http://212.112.186.18/steam/pickweretosave.gif" target="_blank"> Example</a><br />
<br />
2) Right-click on the folder and select "add to archive" from the popup shell &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(This is the menu that appears when you right click)</font></font><br />
<br />
3) Select a RAR <font size="2"><b><font color="red">NOT</font></font></b> ZIP &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(This is an important step)</font></font><br />
Here is an <a href="http://212.112.186.18/steam/4rarnotzip.gif" target="_blank">Example</a><br />
<br />
4) Select the "Store" compression method &gt;&gt;&gt; <br />
<font size="1"><font color="orange">(You can select best etc but using "store" will ensure that nothing gets corrupt during compression</font></font><br />
Here is an <a href="http://212.112.186.18/steam/5selectstore.gif" target="_blank">Example</a><br />
<br />
5) Split in to bytes &gt;&gt;&gt;<br />
<font color="orange"><font size="1">(This is where you input 4500000000 bytes for dvd)</font></font><br />
Here is an <a href="http://212.112.186.18/steam/7splitintobytes.gif" target="_blank">Example</a> <br />
<br />
<br />
6) Select ok &gt;&gt;&gt;<br />
<font color="orange"><font size="1">(This may take a while, depending on the compression levels)</font></font><br />
<br />
7) The created SteamApps.rar will be created here;<br />
<br />
"My Computer" &gt;&gt;&gt; "x:\" &gt;&gt;&gt; "Program Files" &gt;&gt;&gt; "Valve" &gt;&gt;&gt; "Steam" &gt;&gt;&gt; <br />
<font size="1"><font color="orange">(Where x is the primary installation partition. C:\ is the default for most users!)</font></font><br />
<br />
------------------------------------------------<br />
<b><font size="4"><font color="red">*Burning Games to CD or DVD*</font></b></font><br />
<br />
Open up Nero and burn all the backed up RAR files on to CD's or a DVD by following the next set of instructions <img src="images/smilies/smile.gif" border="0" alt=""><br />
<br />
1) Start Menu &gt;&gt;&gt; Program Files &gt;&gt;&gt; Nero Burning ROM <br />
<font size="1"><font color="orange">(Some users might want to use NeroExpress?)</font></font><br />
<br />
2) Select "CD ROM (ISO)" and type a name suitable &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(Preferrably use sequential name like SteamBackup1 , 2 etc)</font></font><br />
<br />
3) Click New &gt;&gt;&gt;<br />
<br />
4) Find location of saved "SteamApps1, SteamApps2.rar etc, then drag and drop the files on to the cd &gt;&gt;&gt;<br />
<br />
5) Select Burn Icon and confirm!<br />
<font size="1"><font color="orange">(You can backup and burn the barebones files for Cs: Source , and HL2, instead of the whole SteamApps folder.. However; This involves <b>ONLY</b> backing up the relevant .gcf files! Do this at your own risk!)</font></font><br />
<br />
=============================================<br />
<br />
<b><font size="4"><font color="red">*Re-Installing files back into Steam folder*</font></font></b><br />
<br />
Follow this final set of instructions in order to re-install your games in to Steam <img src="images/smilies/smile.gif" border="0" alt=""><br />
<br />
1) Re-Install Steam &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(Log-Out and Exit after installation)</font></font><br />
<br />
2) Find SteamApps1.rar, SteamApps2.rar etc &gt;&gt;&gt;<br />
<font size="1"><font color="orange"> (These will be on your CD's or DVD that you burned earlier)</font></font><br />
<br />
3) Right click and select "Extract to" &gt;&gt;&gt;<br />
<font size="1"><font color="orange">(This is where Steam is installed) e.g </font>"My Computer" &gt;&gt;&gt; "x:\" &gt;&gt;&gt; "Program Files" &gt;&gt;&gt; "Valve" &gt;&gt;&gt; "Steam"</font><br />
<br />
4) WinRAR will ask for next archive &gt;&gt;&gt; <br />
<font size="1"><font color="orange">(Just select the next archive or disc (as WinRAR calls it) when it asks i.e The SteamApps2.rar would be the next "disc" or "archive" in this case)</font></font><br />
<br />
5) Thats it! You're Done!<br />
<br />
