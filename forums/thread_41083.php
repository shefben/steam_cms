<br />
<b><font size="large">How do “accounts” and “CD keys” work in Steam?</b></font><br />
<br />
When you sign up for Steam, you create a Steam account, which contains all of your Steam games.  You can use your Steam account on any computer, so that you can play your games no matter whether you&apos;re at home, at work, or on a friend&apos;s computer.  If you buy a new computer or need to re-format your old computer, you can keep using your old account—Steam will automatically download all of your information to the new computer.  Although you can use your account at any computer, it can only be used at one computer at a time.<br />
<br />
 <br />
<br />
CD keys are special serial numbers printed on retail copies of Half-Life and other games in the Half-Life family.  Before Steam, this key was used as an anti-piracy measure, and became the user&apos;s unique ID code when playing the game online.  With Steam, the CD key can be used as a “Proof of Purchase” code that will grant your Steam account access to some of the available games through Steam.<br />
<br />
 <br />
<br />
Each CD key may only be used with one Steam account. Once used, it is permanently bound to that account, and may not be used with another. Deleting the account will not free the CD key to be used again.  One Steam account, however, may have more than one CD key registered to it. For instance, if you have an account with a Counter-Strike CD key registered to it, and you purchase HL2 in the store, you may use the HL2 key on your Steam account to grant access to HL2. <br />
<br />
 <br />
<br />
<font size="large"><b>When I try to enter my CD key, Steam says my key is already in use.</b></font><br />
<br />
<b>Have you previously used this CD key with another account?</b><br />
<br />
A CD key can only be used with one account.  To play this game, you&apos;ll need to use the account you originally registered your CD key with.  See the accounts section below if you need help accessing your old account.<br />
<br />
 <br />
<br />
<b>Has anyone else already used this CD key?</b><br />
<br />
A CD key can only be used with one account.  If anyone else has used this key, you won&apos;t be able to use it.  This can happen if you&apos;ve shared your key with a friend, if you&apos;ve been given this key by a friend, or if you bought the game used.<br />
<br />
 <br />
<b>Did you just buy this game?</b><br />
<br />
If you just bought this game in the past few days and were unable to register the CD key, please contact us.  Send mail to <a href="mailto:support@steampowered.com">support@steampowered.com</a> with the CD key and information about when and where you bought the game.<br />
<br />
 <br />
<font size="large"><b>I created a Steam account a while ago, and now I can&apos;t access it.</b></font><br />
<br />
<b>Do you know your account name?</b><br />
<br />
If you know your account name but have forgotten your password, you can reset your password using the “Reset Password” button on the login screen.<br />
<br />
 <br />
<font color="yellow"><b>Have you forgotten your account name?</b><br />
<br />
Your account name is likely to be your email address, or perhaps an old email address that you used in the past.  Try logging in using any old email addresses you might have used.<br />
<br />
</font><br />
<b>Are you still unable to access your account?</b><br />
<br />
Unfortunately, if you can&apos;t remember your account name or are unable to reset your password, you will not be able to access your account.  You will need to create a new Steam account.<br />
<br />
 <br />
<b>I&apos;ve tried everything, and I still can&apos;t get my CD key to work.<br />
Do you still have your original retail package with the printed CD key?</b><br />
<br />
If you still have your original printed CD key, we can reset your key.  This will remove it from the account that is currently using it, and add it to your account.  To do this, you will need to have a working Steam account (create a new one, if you don&apos;t have access to your old account).  You will need to mail us the original printed CD key (we do not need the cds, or the case, just whatever the cd-key is printed on), along with your email address and the Steam account that you want the CD key assigned to.  Please note that we need the original CD key—we are unable to accept copies or photos.  Send your package to:<br />
<br />
CD key reset<br />
Valve Software<br />
PO Box 1688<br />
Bellevue WA 98009<br />
USA<br />
<br />
Note:<br />
<br />
- we will not return the printed cd-key to you (but we will keep records of having reset yours)<br />
<br />
- this is not a way to get your key un-banned<br />
<br />
<b>Have you lost the original retail package?</b><br />
<br />
If you don&apos;t have the original retail package with the printed CD key and you&apos;re unable to get access to your Steam account, we are unable to help you.</font></p>
	
