	<font face="verdana,arial,helvetica" size="1" > <b>How to stop Steam from loading when your computer boots up.</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >I wrote this as an e-mail to my semi-computer illiterate friend (so please don't take offense to the hand-holding nature of it) since Steam caused major instability by just running on his computer.  I figure at least a few people out there might want to know how to do this too.<br />
<br />
---------------------------------------------------------------------------------<br />
1.  Go to the start menu and pick the "Run" option.<br />
<br />
2.  Type in "regedit."  It'll bring up a window with a bunch of "HKEY" blabbity blah folders.<br />
<br />
3.  The main directory for everything is "My Computer" so the path you want to go to is:<br />
<br />
"My Computer\HKEY_CURRENT_USER\Software\Microsoft\<br />
Windows\CurrentVersion\Run"<br />
<br />
4.  In there you'll see a list of stuff that starts up as your computer first enters Windows.  Just delete the one that has the path to "...steam.exe" and that should stop Steam from starting up from now on.<br />
<br />
That's it, just close the Registry Editor and the next time you start your computer, you won't accidentally start Steam up with it.</font></p>
	
