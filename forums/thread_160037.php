	<p><font face="verdana, arial, helvetica" size="2" >Since I was curious about how fast you actually run with each weapon I decided to create a chart detailing the movement speed, both crouched and running, with each weapon.  Many would call this a waste of time, but in the middle of a boring class lecture, nothing is a waste of time <img src="images/smilies/wink.gif" border="0" alt=""><br />
<br />
So here it is:<br />
<br />
<a href="http://www.bakednet.com/images/games/movspeed.JPG" target="_blank">Movement Speed Graphical Chart</a><br />
<br />
<b>Update:</b> Added Knife to chart...heh. =)<br />
<b>Update:</b> Added zoomed movement speeds for Scout, Aug, Commando, AWP, 552 Sniper, G3/SG-1.<br />
<b>Update:</b> Fixed typo with x/y labels.<br />
<br />
<br />
A few points of interest:<br />
<ul><br />
<li> The Scout does help you run faster! You run at about 250 with a  knife, and 260 with a Scout!<br />
<li> All pistols are the same running speed as the knife, you rather then running defenseless with a knife, run with your pistol!<br />
<li> With the FAMAS you'll run at the same speed as an AWP, even though it's half it's weight!<br />
<li> Other guns as slow as the AWP: Galil, FAMAS, G3/SG-1.<br />
<li> You run at *almost* the same speed with a Para then you do with an Aug!<br />
<li> The only SMG which makes you run slower then the knife is the P90.<br />
<li> You can run faster with the automatic shotgun then with the pump-action version.<br />
<br />
New points since update:<br />
<br />
<li> The G3/SG-1 and Aug have NO movement penalty when zoomed in!<br />
</ul><br />
<br />
<br />
Note: No equipment (armor, grenades) affect running speed.<br />
<br />
Thanks for your interest!<br />
<br />
EDIT: Sorry, I should have meantioned how I got this.  If you type cl_showanimstate 1 in console it'll show your movement speed, amongst other things.</font></p>
	
