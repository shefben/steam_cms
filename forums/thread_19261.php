	<p><font face="verdana, arial, helvetica" size="2" >Yes, I have a lowend computer, 300mhz Celeron, <font color="#D8DED3">Voodoo3</font> 2000, and 384mb memory. I got CS 1.6 Work pretty good. The only thing is that I can't play clan matches no more, because have a fps of 15 in de_aztec. My FPS is now 30-50, at it's low, 15. I can play dust1 & 2 properly, and prodigy runs well too.<br />
<br />
Now I want to tell you what I did. First time I opened CS 1.6, I noticed that MiniGL drivers aren't usable anymore. So I knew I have to tweak a bit to get it work. First thing was that I installed WickedGL FULL version, 3.0. Then, put opengl32.dll from C:\Prog Files\Wicked\ to C:\Windows\System and overwrited the old file, (I suggest a backup copy) then I put my WickedGl minidriver to C:\Games\Steam\SteamApps\myemail@provider.com\counter-strike\gldrv\ And overwrited the old file. Then I copied the hole **** (GLDRV folder) in C:\Games\Steam\SteamApps\myemail@provider.com\gldrv. Then, opened Steam &gt; Games &gt; rightclick on Counter-Strike &gt; properties &gt; launch options &gt; write there ”–gldrv 3dfxgl.dll” without ””.<br />
<br />
Now I had a working CS with opengl. So, that&apos;s not all. Put resolution to 640x480, texture depth to 16bit, smokesprites to medium, max smokepuffs to 1 and max shells to 1. I wasn&apos;t still happy and I put corpsestay to 1 (1= one second, 0 = corpses stay). FPS didn&apos;t still satisfy me. So I Rebooted, put bios, overclocked my Celeron 300mhz to 374mhz (4.5 x 83mhz) and my computer started. So Now my CS runs ”fine” and I can play it barely! If you want more fps, you should open your console, and write ”gl_texturemode gl_nearest” without ”” and textures come ugly, but fps raises. You can also write ”gl_texturemode gl_linear” without ”” but your fps won&apos;t raise so much, but walls are not awful big pixels then. To reset gl_texturemode, change server or quit CS. It will automatically reset gl_texturemode to default.<br />
<br />
Thanks for listening, I Hope that I have helped 3dfx users. If you want to know where to get WickedGL 3.0, Private message me.<br />
<br />
And you should NEVER overclock your CPU with fsb like 83mhz, because it can **** up your hdd, pci and agp cards, but I was desperate. Usually you only have to format your drives if you fuc]&#123; those things up.</font></p>
	
