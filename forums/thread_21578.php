	<p><font face="verdana, arial, helvetica" size="2" >Some of you have had this problem and none of the solutions have worked for you.  I'm going to tell you why:<br />
<br />
Steam has a rather shortsighted design approach to authentication.  It tries to tie your authentication to your IP address.  When your client PC is using NAT and has direct private access to the HLDS via LAN, you wil NOT be able to connect to your server.<br />
<br />
Look at the console of your HLDS.  When you try to connect, you will see that you're connecting to the server using your private IP address, not your public NAT IP address.  Your authentication is tied to your public IP address because you had to connect to Valve before you were able to play.  When the HLDS compares your authenticated IP to the IP you connected with, they DO NOT MATCH and you get kicked off.<br />
<br />
I have yet to come up with a technical workaround.  The only way to overcome it is to find a way to force NAT to translate the private IP before it gets to the HLDS.<br />
<br />
This is a stupid stupid stupid stupid stupid approach to authentication.  They could have simply created a token that is generated using the public IP address of the client and the client Steamid.  This token could be passed to the client and requested by the server when the client connects for authentication.  Unless this type of scheme is used, many LAN players are going to be screwed so long as they have direct LAN access to the server on a private IP.<br />
<br />
In the meantime, go back to 1.5.  As it stands, you will not be able to connect to your 1.6 servers if you are using the same approach to networking that I am.<br />
<br />
<br />
Dean Connell</font></p>
	
