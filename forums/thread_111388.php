	<p><font face="verdana, arial, helvetica" size="2" >Ok, First off I been hearing from alot of different ppl that they "lag" or get very "low fps" in Counter-Strike 1.6 So this guide should boost your FPS (<font color="#D8DED3">Frames</font> Per <font color="#D8DED3">Second</font>) and lower your ping a bit. To view your FPS / Loss / Choke just type "net_graph 3" in game in the console.<br />
<br />
Video Card Drivers &amp; Settings<br />
<br />
1, First you want to check to see if you have your latest video card drivers installed. Go to your video card website and make sure to check. <br />
<br />
ATi Video Card Drivers<br />
<br />
NVidia Video Card Drivers<br />
<br />
2, Once you have downloaded and installed your latest video card drivers you will want to do a few minor changes to the video card settings. <br />
<br />
Vertical Vsync - This makes your game look better but can suck down your FPS really bad. Vertical Vsync will need to be turned off. Below shows how.<br />
<br />
Right Click Desktop &gt; Propertise &gt; Settings &gt; Advanced &gt; Your Video Card &gt; Performance &amp; Quality Settings. Your Vsync should be in there. Just simply set it to "off" and apply settings.<br />
<br />
Antialiasing - This also makes your game look better but it's not needed and by turning it off your game play will be much smoother and you will gain some FPS. This setting is in the same place as your "Vysync" setting so turn it "off".<br />
<br />
16 Bit Color - Counter-Strike was meant to be played in 16 bit color and if you want to reach your max FPS (<font color="#D8DED3">Frames</font> Per <font color="#D8DED3">Second</font>) then be sure to set it to 16 bit color. 32 bit color may make your Counter-Strike graphics a bit nicer but you wont tell a difference setting it to 16 bit color. Below is how to set it to 16 bit color.<br />
<br />
Right Click Desktop &gt; Propertise &gt; Settings (Find Color Quality)<br />
<br />
Monitor Refresh Rates - This setting REALLY helps you reach your max FPS (Frams Per <font color="#D8DED3">Second</font>) and should be set as high as your monitor can handle. Most people have there's set to 60 Hertz which is NOT good. Try to set this to atleast 85 Hertz. How to change refresh rates below.<br />
<br />
Right Click Desktop &gt; Propertise &gt; Settings &gt; Advanced &gt; Monitor <br />
<br />
Open GL Mode - Be sure to run Counter-Strike in OpenGL mode. DONT ever run Counter-Strike in Software mode or Direct3D mode. It can be changed ingame options.<br />
<br />
Config Tweaks &amp; Tricks<br />
<br />
Config Commands<br />
<br />
Below is a list of useful config tweaks. Your config is located STEAM/SteamApps/username/Counter-Strike/cstrike/config.cfg NOTE: Be sure to back up your old config.<br />
<br />
mp_decals "20" <br />
gl_affinemodels "0" <br />
gl_alphamin "0.25" <br />
gl_clear "0" <br />
gl_dither "1" <br />
gl_flipmatrix "0" <br />
gl_flashblend "0" <br />
gl_lightholes "0"<br />
precache "1" <br />
developer "1"<br />
cl_weather "0"<br />
fps_max 150<br />
cl_download_ingame "0"<br />
cl_himodels "0"<br />
hpk_maxsize "0.5"<br />
<br />
FPS Booster Trick<br />
<br />
Go to the Windows\System32 directory and look for your opengl.dll <br />
If you're using a nvidia card the nvoglnt.dll is the right one. For Ati cards it's the atioglxx.dll. And for the rest the opengl32.dll. <br />
- copy that file to your ..\SteamApps\user@user.de\counter-strike\gldrv directory. <br />
- open the drvmap.txt in that directory and change the text to the following: <br />
Default Default gldrv/YOURFILENAME.dll OpenGL Driver<br />
<br />
High FPS Player Models - If your computer is really bad and your still cant seem to get better FPS (<font color="#D8DED3">Frames</font> Per <font color="#D8DED3">Second</font>) you might want to try an replace your player models. If your playing in big servers lanywhere from 18 players to 32 players you will get a FPS (<font color="#D8DED3">Frames</font> Per <font color="#D8DED3">Second</font>) drop due to all the player models your Counter-Strike has to load. I myself downloaded these and they have helped my Counter-Strike alot. Below is the program I used to download the High FPS Models. It gives you instructions on how to install your models and it also has good FPS tweaks / Ping Tweaks.<br />
<br />
<a href="http://www.frag-wire.com/downloads/hltsetup.zip" target="_blank">http://www.frag-wire.com/downloads/hltsetup.zip</a><br />
<br />
Well hope every liked this guide and hope it helps you as much as it helped me. If admin/mod could pin this it would be great.</font></p>
	
