<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr>Today I was able to get a World Exclusive Interview with the man that reviewed Half-Life 2 first, Senior Editor Chuck Osborn from <a href="http://www.pcgamer.com/" target="_blank">PCGamer</a>. So let’s just get straight to it.<br />
<br />
<br />
Icarus: What was your reaction when you found out you would be the first to review Half-Life 2? <br />
Chuck Osborn: It was a mixture of excitement and trepidation, because of the responsibility of being the first one to review it. <br />
<br />
<b>Icarus:</b> Was Half-Life 2 what you expected? <br />
<b>Chuck Osborn:</b> More<br />
<br />
<b>Icarus:</b> Was it worth the wait?<br />
<b>Chuck Osborn:</b> Absolutely<br />
<br />
<b>Icarus:</b> How long is the review?<br />
<b>Chuck Osborn:</b> Nine pages, plus we have a four page story telling you what you need to know about running Half-Life 2 on your system. <br />
<br />
<b>Icarus:</b> Did it seem dated?<br />
<b>Chuck Osborn:</b> Not at all. <br />
<br />
<b>Icarus:</b> Are there any spoilers in the review?<br />
<b>Chuck Osborn:</b> There are some mild spoilers, because you can’t review it without giving away something. I say very clearly when to stop reading if you don’t want to know anything. If you read the entire review, there are a lot of surprises I don’t talk about, because I want you to be surprised. This is not a spoiler review. <br />
<br />
<b>Icarus:</b> Does it have multiplayer, if so what are they? <br />
<b>Chuck Osborn:</b> Counter-Strike Source is the multiplayer. <br />
<br />
<b>Icarus:</b> Only CS:S, no Deathmatch? <br />
<b>Chuck Osborn:</b> Only CS:S<br />
<br />
<b>Icarus:</b> Has it (CS:S) changed any from Beta?<br />
<b>Chuck Osborn:</b> It’s what they say it’s going to be, but with a few surprises<br />
<br />
<b>Icarus:</b> Who did you get to play it with? <br />
<b>Chuck Osborn:</b> Valve as well as playing in within our own offices<br />
<br />
<b>Icarus:</b> Did you get to play HL Source?<br />
<b>Chuck Osborn:</b> Yes. <br />
<br />
<b>Icarus:</b> Can you tell me anything about HL:Source?<br />
<b>Chuck Osborn:</b> Nope<br />
<br />
<b>Icarus:</b> Is it (HL:S) included in the review?<br />
<b>Chuck Osborn:</b> Yes, it's discussed in a sidebar along with a few screenshots.<br />
<br />
<b>Icarus:</b> Anything you want to say the users before I go?<br />
<b>Chuck Osborn:</b> I'm just glad I don't have to keep my playing Half-Life 2 a secret anymore! It was killing me. Trust me...you won't be disappointed. <br />
