	<font face="verdana,arial,helvetica" size="1" > <b>*HL2 RC Sent out Yesterday (The 15th)*</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >According to <a href="http://Eurogamer.net" target="_blank">Eurogamer.net</a>, upon yesterday confirming that Half-Life 2 (and also Counter-Strike: Source and Half-Life: Source) had been sent to Vivendi for release candidate testing, they also managed to get some information from Doug Lombardi regarding the release date:<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr>We don't have a release date from VU at this time, but we're hoping they will put out a release date soon.<hr></blockquote><br />
<br />
And of course, why wouldn't it be soon? Valve are reknowned for their perfection when producing games, something that's clear from the six year development cycle to get us Half-Life 2. With Half-Life taking a mere 48 hours from release candidate stage to going gold, it's pretty clear that we're on the final stretch and the waiting game is nearly over.<br />
<br />
You can find the full news post <a href="http://www.eurogamer.net/article.php?article_id=56634" target="_blank">here</a>.<br />
<u>----------------------------------------------------------------------------------</u><br />
 __________________________________________________<br />
___<br />
<br />
<a href="http://www.halflife-2.nl/" target="_blank">HalfLife-2.nl</a> has reported that Belgian/Dutch <a href="http://www.pcgameplay.com/" target="_blank">PC Gameplay Magazine</a> will, in their issue out on October 5th, be reviewing Half-Life 2.<br />
<br />
According to the news post at HalfLife-2.nl, the magazine says they have played through the full game and will be releasing a full review with 50 brand new screenshots.<br />
<br />
Once again, this shows that the game is getting ever closer, and soon we'll be able to find out if the long wait has been worth it.<br />
<br />
We'll keep you posted on any further developments.<br />
<br />
Thanks to a forum member at halflife2.net, Xrenity, for posting the information in this <a href="http://www.halflife2.net/forums/showthread.php?t=38220&amp;page=1&amp;pp=15" target="_blank">forum thread</a>.<br />
<u>----------------------------------------------------------------------------------</u><br />
 __________________________________________________<br />
