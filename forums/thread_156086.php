<br />
Recently I really have contemplated you're mistakes. The episode of when you released a small crosshair, were given huge negative feedback from the community, so decided to force everyone to use large crosshairs? You have to have an IQ of less than 30 if you thought this would have been an efficient fix. You have replicated this type of so called 'fix', or 'update', and your reasoning (i'd prefer the term "excuse) is pathetic. Hopefully you will see sense.<br />
<br />
To the point:<br />
<br />
<u>Movement:</u><br />
Firstly, there is a huge problem with the movement. For anyone that has a clue about FPS games, you should know that movement is a key factor for your play and style, more noticeable in DM games like Quake, but still. <br />
<br />
I don't know if the physics engine is the cause of this, but there is a bug where if you're next to a wall, crouched and try moving forward it jolts/lags. Whilst jumping, you can no longer bunny hop (without accelerating), like you could in 1.6, to any gamer that incorporates jumping into their style (fisker for one), this is very irritating; being a direct port of 1.6, this is obsolete and should be removed. <br />
<br />
The removal of 'peeping tom', or whatever you want to call it - when you tap crouch you get a small jump to see over crates etc - this should be reinstated to CS:S. <br />
<br />
The last major issue with movement is the lagginess of it. With 1.6 we had smooth, direct response to your input movement, now it seems delayed and, like someone on another thread put: "You're skating on an ice-rink".<br />
<br />
<u>Weapon Handling/Behaviour:</u><br />
No matter what you say, or whatever graphs you produce, the weapon behaviour is not the same. It feels more like pre 1.3, where you could hold the trigger down and create a constant stream of spray, which is easily contollable. In CS, recoil control was a huge asset of a player's skill, this has been taken away and thus depleting the feel of CS.<br />
<br />
Shooting through walls, commonly known as 'wall banging' was a huge element of skill for CS, it really did make this a key part of CS. You have taken out one of the few skilled elements left in CS, its like removing rocket jumping from Quake.. unthinkable.<br />
<br />
The AWP has been recently debated many times, and was clearly ruined by adding a delay to it's zoom. If you return the fast switch of the AWP, you will be returning a very valuable skill to CS.<br />
<br />
<u>Gameplay/Misc</u><br />
The gameplay has slightly increased in speed, which is about the only good thing you have added, or theoretically, returned, to CS. Obviously you will have to work on making CS:S a skilled game thats suited to competetive, and professional play. You honestly cannot continue to make the game easier, so the younger and less talented public players will stop whining. In the long run, it really is better to make a skilled game, that new players can learn to respect better players, like CS used to be.<br />
<br />
Please take note, before you totally destroy CS. You have a very good platform to base a potentially excellent next generation game, which those who play on UK2 publics can enjoy, as well as those competing at tournaments for a г30,000 prize.</font></p>
	
