	<font face="verdana,arial,helvetica" size="1" > <b>Heres the story, people: An FAQ about Steam</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The Official Steam Client was released, along with the final of CS 1.6, and currently Valve is finalizong the Steam Content Network servers in anticipation of the relase of Half-Life 2. <br />
<br />
<b><font size="5">This thread is for Steam users that are having difficulty with the program. If you continue to spam this thread, <i><font color="RED">I will delete it</font></i>, as I have no need to use the answers posted here. This means no more all caps posts, no more spammimg, no more useless crap. GOT IT?</font></b><br />
<br />
The following is a FAQ for Steam.<br />
<br />
-----------------------------------------------------------------------------------<br />
1] <b>Will my cd key still work?</b><br />
<br />
Yes, it will. Steam will prompt for it during installation.<br />
<br />
<br />
2] <b>Is WON offline?</b><br />
<br />
No, it will stay up for about 2 weeks, and then the WON servers will be shut down permanently.<br />
<br />
<br />
3] <b>Can I hold a LAN game?</b><br />
<br />
Yes, you can, but, and this is quite a but, every system <b>has</b> to be online and connected. The likelyhood this will change is great, however.<br />
<br />
<br />
4] <b>Will my MOD work under steam?</b><br />
<br />
It should, but chances are that if it isnt a mainstream mod i.e. NS or DoD, then you should wait before installing it untill the next patch is released. Full MOD support <b>is</b> included with Steam, however, so wait before spamming the forums with complaints.<br />
<br />
<br />
5] <b>I was in the beta for steam.....it will update itself, right?</b><br />
<br />
No, a new steam client was released and the beta program will <b>not</b> work anymore. You must delete the original beta install and run the new installer.<br />
<br />
<br />
6] <b>Can I still play CS 1.5?</b><br />
<br />
Again, yes, for two weeks untill WON goes offline.<br />
<br />
<br />
7] <b>When will CS 1.6 become final?</b><br />
<br />
The Counter-Strike version 1.6 final has been released.<br />
<br />
<br />
8] <b>Is there spyware in Steam?</b><br />
<br />
No, there has <b>never</b> been any spyware of any kind in Steam, anything you may have heard to the contrary are baseless rumors.<br />
-----------------------------------------------------------------------------------<br />
<br />
Any other questions have more than likely had threads dedicated to them, and all one has to do is <i>search</i> for them.<br />
<br />
If there are any more questions you think I may have missed, feel free to add them <b>with</b> the answers.<br />
<br />
