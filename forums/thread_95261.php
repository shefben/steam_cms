	<p><font face="verdana, arial, helvetica" size="2" >An update to the Steam client and HLDS update tool has just been released. Steam will update itself automatically when you restart. Here is a list of the changes:<br />
<br />
<br />
STEAM CLIENT CHANGES / ADDITIONS:<br />
<br />
Steam will now generate a recipt when you purchase a product, instead of sending it via e-mail <br />
Removed email verification <br />
All Steam error messages are now localized <br />
Improved offline behavior if no SteamUI.dll or Steam.dll present <br />
Improved content server responsiveness <br />
Disabled background preloaders when any game is running <br />
Preloader checks to see if an item will fit in the cache (improves going offline) <br />
Improved behavior of content "tickets" (thrown out if system clock more than 4 weeks off) <br />
Improved loading progress bar time calculation <br />
Improved startup speed of Steam <br />
Improved responsiveness of user cancel <br />
Changed legal characters in new account names to only be 'a'-'z', 'A'-'Z', '0'-'9', and '_' <br />
<br />
HLDS UPDATE TOOL CHANGES / ADDITIONS:<br />
<br />
<br />
Added "-retry" flag -- auto-retry to get content every 30 seconds</font></p>
	
