	<p><font face="verdana, arial, helvetica" size="2" ><i>8.  Change the IDE/ATAPI controller settings (ie. DMA and PIO)</i><br />
<br />
I don't know what the significance of this is or how it's supposed to help as I've yet to hear anyone who's solved their problem this way.  Right-click <b>My Computer</b> and select <b>Properties</b>.  In the ensuing window, select the <b>Hardware</b> tab and click on the <b>Device Manager</b> button.  A Device Manager window should appear.  Expand (click the +) the <b>IDE ATA/ATAPI Controllers</b> device, right-click <b>Primary IDE Channel</b> and select <b>Properties</b>.  From the ensuing window, select the <b>Advanced Properites</b> tab.  This tab window should show you all the devices connected to the IDE channel.  Assuming you have more than one device attached to the IDE Channel, find the one that is attached to your CD/DVD drive.  (You can determine this by going back to the Device Manager window, expanding the DVD/CD-ROM Drives device, and right-clicking and selecting Properties for the drive.  Under the General tab of the enusing window it should tell you where the device is connected (Location 0, 1, 2 etc).  This location number is the corresponding device number in the Primary IDE Channel window).<br />
<br />
Change the Transfer Mode option from DMA/PIO to PIO/DMA respectively.  You may have to restart the computer for the changes to take affect.  In any case, once you've done this, trying running the game.  If it ultimately doesn't work, make sure to return the settings to how you found them.<br />
<br />
<br />
<i>9.  Make sure you don't have any CD/DVD cloning software on your machine</i><br />
<br />
Apparently, some games, like Doom 3, were written in such a way that the game will refuse to boot/install unless you remove all CD/DVD cloning software.  Examples of such software are programs like Alcohol 120%, CloneDVD, etc.  As far as I know, regular CD/DVD burning programs like Nero, Roxio and Sonic do not count as cloning software, but feel free to experiment with uninstalling your burning software.<br />
<br />
The problem here is that when booting games like Doom 3, it will tell you explicitly (or implicitly) with a warning message to remove any clone software from your machine before proceeding.  The HL2 no-start problem, however, gives no error message of any sort anywhere.  In fact, at the very least, neither Valve nor Vivendi have said anything regarding a no-boot clause in the game if there is any clone software on your machine, so we can only assume that HL2 is not set to detect such programs.<br />
<br />
Furthermore to the point, according to <a href="http://www.securom.com" target="_blank">SecuROM</a>'s website, it is quite impossible at this time to replicate it's copy protection, so Valve/Vivendi have little to worry about.  In addition to that, it states that cloning and CD/DVD drive emulation software <b>should not</b> interfere with the operation of it's copy protection system, though they admit that it's not impossible for conflicts to happen.<br />
<br />
<br />
<i>10.  Update your CD/DVD drive's firmware</i><br />
<br />
Go to either your system manufacturer's or your CD/DVD drive manufacturer's website to obtain the latest firmware updates for your drive.  To find out what kind of drive you have, you can obtain it from Device Manager (see #7 above) or using <b>System Information</b>, found in <b>Start &gt; Programs &gt; Accessories &gt; System Tools</b> and then selecting <b>Components &gt; CD-ROM</b>.  If you are lucky enough to have a firmware update available for your drive, follow the instructions on flashing your drive as provided by the firmware provider.<br />
<br />
If no firmware update is availabe for your drive, you're out of luck and will have to skip this step.  If you happen to have a Philips DVD+RW 8601 drive, there may be a way of crossflashing your drive's firmware and allowing you to play HL2 (see <b>UNOFFICIAL SOLUTION #2</b> below).<br />
<br />
<br />
<i>11.  Use MSCONFIG to find any programs that may be interfering with the operation of Steam/HL2 </i><br />
<br />
MSCONFIG lets you select and modify what programs are loaded during start-up of Windows.  You can find information on how to do this by searching <b>Windows Help</b> for <b>MSCONFIG</b>.  However, to give you a quick run through, go to <b>Start &gt; Run</b>, type in <b>"MSCONFIG"</b> and hit Enter/OK to load the utility.  I suggest you DO NOT touch any of the .INI tabs as altering them will alter those files, and the less you mess around with Windows, the better.  From the <b>Startup</b> tab select the <b>"Disable All"</b> option and then click OK.  All the startup items will be disabled the next time you restart Windows.  Once Windows has restarted, try running any of the HL2 games.  If they do not work, reset the MSCONFIG Startup settings to what they were originally (you can do this by selecting <b>"Select All"</b>, clicking OK, and restarting Windows).<br />
<br />
If, however, for some reason the games do run, go back to MSCONFIG and enable a small number of Startup items (about four), reboot and try launching the games again after the system reboot.  In this way, you should be able to determine which program it is that is interfering with Steam/HL2.<br />
<br />
<br />
<i>12.  Uninstall and reinstall the game</i><br />
<br />
An error of some kind could have occurred during your install of the game.  Best bet is to uninstall and reinstall the game.  To do this, go to <b>Start &gt; Programs &gt; Valve &gt; Half-Life 2 &gt; Uninstall Half-Life 2</b> (or you can do it using the Add/Remove Programs via Control Panel).  However, this does not completely uninstall the game.  It only tells Windows and Steam that the game is no longer available, but most of the files will still be on your harddrive.  To remove the game content off the drive, start <b>Steam</b>, go to the <b>Play Games</b> menu, go to the <b>Properties</b> of each game and select the <b>"Delete Local Game Content"</b>.  The Properties of each game should now say "0% acquired".<br />
<br />
Once all that is done, reboot the system and reinstall the game from the CDs/DVD.<br />
<br />
<br />
<i>13.  Defragment the harddrive</i><br />
<br />
Double-click <b>My Computer</b>, select the drive where HL2 is installed, right-click it and select <b>Properties</b>.  From the ensuing window, select the <b>Tool</b> tab and then <b>Defragment Now</b>.  The Defragment program will start.  If you can, select <b>Analyze</b> to have the program analyze the amount of fragmention on your drive.  This process can take anywhere from a few seconds to several minutes, depending on the amount of fragmentation on your drive, so please be patient.  When the analysis is complete, select <b>View Report</b>.  The report window will open telling you how fragmented your drive and it's files are.  What is most important here is to check it's recommendation.  If the report recommends you to defragment the drive, then exit the report and select <b>Defragment</b> to defragment the drive (if you have more than one harddrive, make sure the proper drive is selected before you proceed).  Defragmenting the harddrive can take a considerable amount of time, depending on the size of the drive, the amount of fragmentation, your CPU speed, and amount of RAM, so be prepared to wait several hours for it to finish.<br />
<br />
If, on the other hand, the analysis does not recommend a defrag, you don't have to do one.  If you have a small amount of fragmentation and want to defrag anyway, go right ahead, it won't hurt.<br />
<br />
<br />
<i>14.  Contact the Customer Service/Tech Support of your system's manufacturer</i><br />
<br />
Let them know what your problem is and ask for any assitance they can give you.  They'll likely suggest a lot of the same suggestions in this very thread.  Let them know all of the things you've tried.  They may be able to help you with special updates for your system or specific system hardware.<br />
<br />
But chances are, they won't be able to help, so don't be too disappointed.<br />
<br />
