	<font face="verdana,arial,helvetica" size="1" ><img src="images/icons/icon4.gif" alt="Exclamation" border="0"> <b>Official HL2/CS:S/HL:S does not start/load/run/boot thread... (Part 1 of 4)</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >Ok people, I've had enough.  Every day I log on to Steam to see what's new, and every day I see several new people coming to the forums crying about the no-start problem.  As such, I've decided to start an official thread where we can post all suggestions that we've tried (and failed at) and any new suggestions that come our way in addition to our specs.  This thread will be an enourmous help to not only get Valve's attention regarding this rather gargantuan problem but also in getting them to fix it.  Before I start, I'd just like everyone to know that I have to break this post into FOUR parts as the entire thing is much too big to fit into one post.  So, here goes:<br />
<br />
<b>THE PROBLEM</b><br />
<br />
The problem this thread is about is the no-start/load/run problem occurring ONLY with the retail versions of Half-Life 2 (both CD and DVD versions, North American and European).  This problem is characterized by a window appearing and stating:<br />
<br />
<b>"Preparing to load Half-Life 2/Half-Life: Source/Counter-Strike: Source..."</b><br />
<br />
when the respective program is initiated (whether it's initiated from the Steam Play Games menu or from the Desktop shortcut makes no difference at all).  After a few brief seconds, the window will disappear and the CD/DVD drive will spin up the disc, but then nothing will happen.  No game will load and no error message is received.  Nothing happens whatsoever (beyond the window appearing/disappearing and the CD/DVD drive being accessed).<br />
<br />
For some reason, the autorun on the disc(s) work and the game installs without any major problems, but when booting the games, you cannot get past the CD/DVD drive access phase.<br />
<br />
If you are having this problem, please continue below.  <font color="red">If you are also having disc authentication and/or <b>"Please insert disc into drive"</b> errors, please continue below to <b>SUGGSETED SOLUTION #7</b></font>.  <i>If your problem with the game is </i><b>different</b><i> from the aforementioned scenario, </i><b>please do NOT post here requesting help</b>.  <i>Instead, please check out <a href="/forums/showthread.php?s=&amp;threadid=182355" target="_blank">Half-Life 2 Knowledge Base for Retail Owners</a> for further help.</i><br />
<br />
<font color="red"><b>PLEASE NOTE:</b></font>  <font color="yellow">If you are having any of the following problems with the game:<ul><li>the game crashes <i>after</i> loading the game<br />
<li>you are having problems <i>installing</i> the game<br />
<li>you cannot start/load a new/saved game<br />
<li>you are having memory read errors<br />
<li>you are experiencing audio/video stuttering problems<br />
<li>the game crashes at autosave points<br />
<li>you are having problems connecting to Steam</ul></font><br />
<font color="red">THIS THREAD IS <b>NOT</b> FOR YOU.  Please seek help for these problems elsewhere.</font><br />
<br />
<br />
Now, to continue on with the thread:<br />
<br />
This no-start problem, as I will refer to it from now on, has been traced back to a compatability issue between the SecuROM copy protection employed on both the CDs and DVD discs and certain CD/DVD drives.  I am 99.9% sure of this and will detail why below.  I will list all of the suggestions provided to many of us by both Valve and Vivendi Tech Support and by various posters online.  If any of you are having the no-start problem, please go through the entire list of <b>SUGGESTED SOLUTIONS</b>.  If you are still experiencing the no-start problem, you can try the <b>UNOFFICIAL SOLUTIONS</b>, but do it at your own risk.<br />
<br />
<font color="red">Please Note:  If at any time while you are trying the below suggestions the game actually boots up and works, make a note of the circumstances under which the game booted.  When you eventually exit the game, try restarting the game again using the same conditions.  If it boots, exit the game and try again.  Keep doing this until you are sure that you can get the game to load consistently.<br />
<br />
The reason for this is that every once in a while, the CD/DVD drive will be able to get past the SecuROM protection and boot.  However, this is very rare and unpredictable, so if you find that you've managed to get the game to boot once but not again under the same circumstances, it means you haven't achieved anything in terms of solving the greater problem, you just got lucky that one time.</font><br />
<br />
<br />
<b>SUGGESTED SOLUTIONS</b><br />
<br />
<i>1.  Make sure your computer meets the system requirements to play the game</i><br />
<br />
Make sure your computer meets ALL the system requirements to play the game.  However, given how forgiving they are for this game, I'm sure most of us don't have a problem here.<br />
<br />
<br />
<i>2.  <ul><li>Is the game completely installed and validated?<br />
<li>Has the CD-Key been properly registered?<br />
<li>Have you downloaded all the latest patches?</ul></i><br />
<br />
Make sure you have done all of the above.  When you first install the game, you'll be prompted to register the CD-Key through Steam.  If you get any errors, don't worry, you can always try registering the CD-Key later using the Browse Games function in Steam.<br />
<br />
In the <b>Play Games</b> menu, right-click the game you want to play and select <b>Properties</b>.  It will tell you how much of the game you have acquired and if the game is ready ready for offline mode or not.  Please note: if the game is not 100% ready for offline mode, you probably won't be able to run it.  If the game does not say 100%, there should be a menu item in the Properties window that says <b>Automatic Updates</b>.  Make sure that <b>"Always keep this game up to date"</b> is selected.  Then, right-click the Steam system tray icon (the one next to the clock) and select <b>Monitor</b>.  A window should open with the game's name, % acquired and the download rate for it.<br />
<br />
If you're game is stuck at less than 100% acquired and no updates are being downloaded, go to <a href="/index.php?area=news&amp;id=351" target="_blank">Steam Client Update Released Nov 19/04</a> and click on all the relevant links.  Your games will be validated and updated.  At the end of it all, your games should be 100% acquired and ready to play offline.<br />
<br />
If you still haven't acquired 100% of the game and/or it's still not ready for offline mode, please consult the <a href="/support.php/cgi-bin/steampowered.cfg/php/enduser/entry.php" target="_blank">Steam Support Centre</a> for further information on how to proceed.<br />
<br />
<br />
<i>3.  Delete ClientRegistery.blob and restart Steam</i><br />
<br />
Exit Steam.  Go to <b>Program Files &gt; Valve &gt; Steam</b> and delete the file <b>ClientRegistry.blob</b>.  Restart Steam.  After a short update, run the games.<br />
<br />
<br />
<i>4.  Make sure you have a clear connection to Steam</i><br />
<br />
If you have any Internet Security or Anti-Virus software running in the background, make sure that they are either disabled or are set to allow Steam/HL2.exe unburdened access to the Internet.<br />
<br />
Of course, if you are trying to play the game offline, this tidbit of info is rather pointless, as the no-start problem affects the game regardless of whether you are playing on- or offline.<br />
<br />
<br />
<i>5.  Update your OS with all the latest updates</i><br />
<br />
Many people have been experiencing problems due to using an older version of their OS.  If you are running any version of Windows and experiencing problems, make sure you run Windows Update and get all of the latest updates.  If you are running XP Service Pack 1, upgrade to Service Pack 2 (it's free just in case you were wondering) and get all the latest updates.<br />
<br />
<br />
<i>6.  Update your video and sound card drivers</i><br />
<br />
Make sure you have the latest officially released drivers for your video and sound card.  For ATI video card users, this should be Catalyst version 3.11.  For nVidia users, I don't know, but I'm sure you can check updates for both at their respective sites.<br />
<a href="http://www.ati.com/support/driver.html" target="_blank">http://www.ati.com/support/driver.html</a><br />
<a href="http://www.nvidia.com/content/drivers/drivers.asp" target="_blank">http://www.nvidia.com/content/drivers/drivers.asp</a><br />
Don't bother with Beta versions of drivers as they are <b>not</b> guaranteed to work under any circumstances, let alone running HL2 (that's why they're called Beta versions).<br />
<br />
Same goes for sound cards.  Go to your sound card manufacturers website and check for updates.  If you have an integrated sound card, check your system manufacturers website or your motherboard/chipset's website for any updates.<br />
<br />
<br />
<i>7.  If you have any DLA programs installed on your machine, remove them as they may be interfering with Steam/HL2's access to your optical drive.</i><br />
<br />
<font color="red">Attention:  Anyone who is receiving the <b>"Please insert disc into drive"</b> error even when the disc is inserted or the <b>"Cannot authenticated discs"</b> error, please read!</font><br />
<br />
If you have a CD and/or DVD burner, you probably have a DLA program, such as Sonic DLA, installed on your system.  This program allows you to drag-and-drop write files to writable blank CDs/DVDs.  It also seems to interfere with Steam's ability to communicate with the CD/DVD drive, especially if you have more than one optical drive.  You can uninstall this program in Windows by going to <b>Start &gt; Settings &gt; Control Panel &gt; Add/Remove Programs</b>.  Once the uninstall is complete, reboot the system.  Steam should now be able to access the CD/DVD drive.  Whether it will be able to get past the SecuROM copy protection is another matter entirely.<br />
<br />
<font color="yellow">For those of you experiencing the <b>"Cannot authenticate discs"</b> or the <b>"Please insert disc into drive"</b> error, if the above did not solve your problem, please check the <a href="/support.php/cgi-bin/steampowered.cfg/php/enduser/entry.php" target="_blank">Valve Support</a> and <a href="http://support2.vugames.com/hl2/search.aspx" target="_blank">Vivendi Support</a> centres for further details.  This thread is dedicated to the no-start problem and we cannot help you further with your problem.  Thanks for understanding.</font><br />
<br />
