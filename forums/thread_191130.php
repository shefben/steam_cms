Once you have tried all of the above suggestions and you are still encountering the no-start problem, you can try sending an error report to Valve and Vivendi detailing all the things you've tried but to fix the problem but failed.  It's more than likely they won't be able to help you (beyond "buy a new DVD drive") but then that is exactly why I started this thread.<br />
<br />
Also keep in mind that the more of us that speak up regarding this problem, the better the chance for us to find a resolution to it.<br />
<br />
Additionally, you can also send an error report to SecuROM regarding your problem:  Go to <b>Program Files &gt; Valve &gt; Steam</b> and right-click <b>Steam.exe</b>.  From the drop down menu select <b>Launch Analysis</b>.  A program will load asking for permission to analyze the file and your machine.  Select Yes and the analysis process will start.  After a few minutes, a file called <b>AnalysisLog.sr0</b> will be created in your root directory.  Next, go to <b>Program Files &gt; Valve &gt; Steam &gt; Steam Apps</b> and perform the same process on <b>HL2.exe</b>.  Once complete, compress the two <b>AnalysisLog</b> files into a zip file (highlight both files, right-click, select <b>Send To &gt; Compressed (zipped) Folder</b>), attach it to an email file and send the email with a brief description of the problem to <a href="mailto:support@securom.com">support@securom.com</a>.<br />
