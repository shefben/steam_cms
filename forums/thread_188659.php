	<p><font face="verdana, arial, helvetica" size="2" >You might find that when playing a map after a few rounds things start to get predictable. This is when tactics come into play.<br />
<br />
It's not enough to just be a good aim, though that is a necessity to be good. <br />
<br />
You'll probably find out pretty quickly where the choke points are, where people like to take cover, where they run through etc. Just use your head.<br />
<br />
I racked up a few kills in Italy as a T by dashing down the stairs and around to the corner where the CT's meet before heading up the ramp towards the hostage house. I just tossed a HE grenade in there and if I was lucky it'd land before the CT's got there. Then it'd blow AS they got there. <br />
<br />
Follow up with a quick strike while they're in dis-array and you can get a few good kills in a single round. <br />
<br />
Just be aware of when you're becoming too predictable. Know the map so you can be comfortable taking a different route each time. Watch the routes the opponents take and see if you can find good ambush points. <br />
<br />
Surprise be it by rushing in unexpectedly or ambushing is a big factor in winning a gun battle. <br />
<br />
Also, learn not to be dis-oriented by flashbangs. It's all situational awareness, when the lights go out, you have to know where cover is and how to get there. Experienced players will still get flashed, but they'll run for cover as though it didn't affect them. <br />
That's about all I can add that hasn't been covered yet</font></p>
	
