	<p><font face="verdana, arial, helvetica" size="2" >Upon listening to many different sides of the issue, I believe I have come to a better solution to the delay that was added onto the AWP to protect against the "quickswitch exploit."<br />
<br />
Basically, KillerKoala is right in the sense that while the game has realism factors, it also has other factors that detract from the realism, therefore making the gameplay much more enjoyable. I couldn't agree with that view more. While the game has many factors attached to realism, there are other factors that are added to the game playability that make Counter-Strike one of the best FPS games on the market, even after 5 years.<br />
<br />
Basically what I'm going to try and do, is explain KillerKoala's opinion, Professional player's opinions, as well as the opinions of some of the other players I have talked to. Then I will give my solution to the AWP. While I know that many have tried to argue against the change, and many have argued for the change, this is my arguement; and I feel that this option would best represent the ENTIRE community.<br />
<br />
From what I understand, KillerKoala has said that if you "quickswitched" that you would do the following:<br />
<br />
1) Scope.<br />
2) Shoot.<br />
3) Switch from the AWP to your pistol.<br />
4) Switch from the pistol to your AWP.<br />
<br />
You would then be able to shoot your AWP within a matter of miliseconds. All of this without seeing the pistol animation, the rebolt animation, and the AWP animation, which would mean that you skipped through the animations to be able to shoot the AWP faster.<br />
<br />
So basically, a delay was added to compensate for the animations being skipped after completing a quickswitch.<br />
<br />
Now, talking to a few professional players, I have come to understand that the game has been slowed down in the competitive scene due to the change. Many players feel as though Valve has been making the game slower over the years, mainly starting with the release of 1.4.<br />
<br />
I recently had the opportunity to play 1.3 and I am going to admit right now that the game has indeed become much slower gameplay-wise over the years.<br />
<br />
Of course, that's not nessicarily a bad thing on it's own. However, the changes that have been made that have made the game slower could have been gone about differently, which would make the game more fun.<br />
<br />
This arguement is about the AWP, so I'll continue.<br />
<br />
Overall what I think the community needs, is to have some sort of balancing. While I agree with this statement:<br />
<br />
"You can't be pro at your own game, you can only be pro at a game that everyone plays."<br />
<br />
made by a fellow player, it's getting harder with each update to maintain a good league system. As a competitive player, I would like to offer my input, because I love this game to death, and I feel as though I am being treated like a pawn.<br />
<br />
I have heard one of the creators of Counter-Strike call the competitive community a "vocal minority," and I completely disagree. I view myself as a paying customer, and as a paying customer, I fully believe that I deserve an equal voice.<br />
<br />
My solution is as follows.<br />
<br />
Create a serverside option that allows the creater of the server to decide how the weapon should work. You already have an option that allows for silent-running, so I see no problem with adding an option that gives a sense of balance to the community.<br />
<br />
Basically the option would work like this.<br />
<br />
[  ] AWP Behavior<br />
<br />
Having the box checked would make it so the AWP would have a delay on it. Having the box unchecked would make it so the AWP would have no delay.<br />
<br />
This would cater to the competitive players, as well as the players who feel as though quickswitching is an exploit. Contrary to KillerKoala's belief, the ability to quickswitch was never the reason why many of the top competitive players got so many kills. The facts are that almost every single player who KillerKoala talks about still quickswitch (even though the delay is there) and they still the same amount of kills.<br />
<br />
<br />
The problem with the "exploit" being "fixed" is that it has slowed down the pace of competitive Counter-Strike by a large margin. I'm not asking for a professional mod, I'm just asking for an option. This is a very big issue to a lot of players, and while it might not seem that big to the likes of KillerKoala and people who agree with him, it is big to us.<br />
<br />
I would also like to point out, that beyond being a "vocal minority." We are you're biggest supporters, and we are the reason you're game was ever sucsessful. If you disagree with that, please take note...<br />
<br />
Professional Counter-Strike has been featured on ABC, NBC, CNN, and more. We have multiple leagues and tournaments sponsered by many of the biggest names in the buisness. Intel, Hitachi, etc. Teams are sponsered by Nvidia, ATi, Intel, AMD, etc. We have stuck with Valve through thick and thin, and WE are the "vocal minority" that kept this game alive and well.<br />
<br />
I'd like to know how many of the players in public servers have stuck with this game through the beta versions. I'd like to know how many of these players even play the game for more than a couple months. I'd like to know how many of these players need to complain before you (Valve) decides to make the game slower, easier, and less fun.<br />
<br />
Above all, I'd like to know how many of these useless updates it will take for you to realize that you're ruining the game.<br />
<br />
Worry about VAC, not non-existant exploits.</font></p>
	
