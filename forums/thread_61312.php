	<p><font face="verdana, arial, helvetica" size="2" >Hi,<br />
<br />
Im the guy you know as "the HLTooLz creator" <img src="images/smilies/tongue.gif" border="0" alt=""><br />
<br />
Anyway, I thought I'd write this because all other Netcode guides miss one important bit in tweaking the netcode.<br />
<br />
<font color="skyblue"><br />
[rate]<br />
</font><br />
This is your download speed in bYtes / second.<br />
If you know your advertised speed in kbIps (like a 56k is actually 56kbIps), then all you have to do is divide your speed by 8 and multiply it by 1024.<br />
Like this:<br />
<br />
<font color="coral"><br />
56 / 8 = 7 kbYtes/sec (1 byte = 8 bits)<br />
7 * 1024 = 7168 bYtes/sec (1 kbyte = 1024 bytes)<br />
</font><br />
<br />
Your rate would be:<br />
<font color="coral"><br />
rate 7168<br />
</font><br />
You should know you will never download @7168, because thats the maximum theoretical limit. Instead, you should be realistic and take 90% of the theoretical limit to get the download speed you would get on most servers.<br />
<font color="coral"><br />
90% of 7168 = 7168 * 0.9 = 6451<br />
<br />
rate 6451<br />
</font><br />
That's about right, and should work best if you have a 56k dialup modem.<br />
<font color="skyblue"><br />
[cl_rate]<br />
</font><br />
This is your upload speed in bYtes / second. It's how fast you send the things you do to a server. But your upload speed is usually not as fast as your download speed, so you need to make different calculations for "cl_rate" than for "rate".<br />
<br />
Let imagine you have DSL, with 128k upload speed.<br />
128k is an advertised speed, so it is in kbIts/sec.<br />
<font color="coral"><br />
128 / 8 = 16 kbYtes/sec<br />
16 * 1024 = 16384 bYtes/sec<br />
</font><br />
Again, you wont upload at that speed, instead your max upload speed will be about 90% of the advertised speed.<br />
<font color="coral"><br />
16384 * 0.9 = 14746 bYtes/sec<br />
</font><br />
So your cl_rate will be:<br />
<font color="coral"><br />
cl_rate 14746<br />
</font><br />
<font color="skyblue"><br />
[cl_updaterate]<br />
</font><br />
This is the amount of times you receive updates from the server per second.<br />
<br />
In a heavy firefight the largest packets can be up to 180 bytes. (A packet is a bit of information that you receive).<br />
<br />
If we divide our rate by the largest packet size, we'll get our cl_updaterate.<br />
<br />
<font color="coral"><br />
6451 / 180 = 36<br />
<br />
cl_updaterate 36<br />
</font><br />
<br />
We dont need to take 90% of the updaterate, because we've already done that in the rate calculation.<br />
<font color="skyblue"><br />
[cl_cmdrate]<br />
</font><br />
This is like cl_updaterate only that it's the amount of times you send updates to the server. The largest packet size that you (the client) send to the server is something like 30 bytes.<br />
<br />
So we should divide cl_rate by the largest packet size we send through:<br />
<br />
<font color="coral"><br />
14746 / 30 = ~491<br />
</font><br />
<br />
Now, you can't send 491 updates per second to the server <img src="images/smilies/tongue.gif" border="0" alt="">. It's almost impossible for the server to handle all those packets. What Half-Life has done, is limit cl_cmdrate to 100<br />
So if you calculate cl_cmdrate and its bigger than 100, change it to 100<br />
<br />
<font color="coral"><br />
cl_cmdrate 100<br />
</font><br />
<br />
That's more than fast enough for most ppl.<br />
<br />
=============================================<br />
=============================================<br />
<br />
That was the first part, I think all I said there has been said before, and perhaps better than I explained it.<br />
<br />
But now comes the bit everyone forgot to mention!<br />
<br />
<br />
<br />
The server you connect to has maximum and minimum rates. What the fu*k does that mean? It means that your specially calculated rates wont work properly and youll get choke and loss.<br />
<br />
How should you fix this?<br />
<br />
Well, currently you can only do this by looking up the max rates of the server, and recalculating rates after youve got the servers max rates.<br />
<br />
Ill explain how:<br />
<br />
1/ First connect to a server<br />
2/ Open console with "~"<br />
3/ Type: sv_maxrate<br />
4/ Press enter<br />
5/ You should see a message in console displaying sv_maxrate xxxx<br />
<br />
<br />
Now that you've got the servers max rate, you know your own max rate aswell, the server limits your rate to his own max rate.<br />
<br />
So now we have to adjust our own rates.<br />
<br />
Lets say sv_maxrate was 10000<br />
<font color="coral"><br />
sv_maxrate 10000<br />
</font><br />
<br />
Our rate should then also be 10000 (if your rate is less than sv_maxrate, you dont need to do any more calculations, you have the best rates for your connection). What we need to change is our cl_updaterate to avoid choke.<br />
<br />
So we need to calculate cl_updaterate based on a rate of 10000<br />
<font color="coral"><br />
10000 / 180 = 56<br />
<br />
cl_updaterate 56<br />
</font><br />
<br />
That will be our new cl_updaterate.<br />
After you've done that, you have calculated the best possible rates for the server you are on. All you have to do is type "cl_updaterate 56" in console.<br />
<br />
If you still experience choke or loss, lower your cl_update and cl_cmdrate, not your rate/cl_rate.<br />
<br />
<br />
<br />
Soon, I'm making a program in which you paste (ctrl+v) a server IP address and it calculates the best possible rates for that server and then it connects to the server for you. Look for it @ hltoolz.com (i havent made it yet though)<br />
<br />
<br />
Cya<br />
