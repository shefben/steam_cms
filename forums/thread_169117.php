<font size="3"><b>HL2 Set to Release Same Time Across World</b></font><br />
<br />
The question that has been on everybody's lips is at what time will Steam unlock on the 16th or at what time can I buy HL2 from retail? HL Fallout member War_Buddy has sent in some interesting news on this subject, reporting from Vivendi Denmark.<br />
<br />
The word is that not only will HL2 be released on the same date across the world, but also the same time. Vivendi are forcing retailers to only start selling HL2 at midnight Seattle time (GMT-8), with taking into account time zone differences. The reason being is that Vivendi are planning to release HL2 across the worldwide simultaneously, for both retail and steam.<br />
