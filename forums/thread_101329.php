	<p><font face="verdana, arial, helvetica" size="2" >Remove the shield or do the following.<br />
<br />
If it must be kept in, fix the hitboxes and make it a Hostage Rescue map only piece of equipment just like the defusal kit is Defusal map only piece of equipment.<br />
<br />
The shield is balanced when the CTs are on the offensive and the Terrorists like on maps such as militia have far better positions and with a good awper or two can dominate the entire map easily enough and start going on the offensive themselves.<br />
<br />
The shields in these situations where campers, AWPers and the like are rife and the CTs need a way to get close without dieing and to enter a room without being taken out on the spot.<br />
<br />
Works good on Hostage Rescue maps, on Defusal Maps is just is unbalancing and is banned in every war due to that fact (and the fact its hitboxes are so buggy). Otherwise ditch the thing.<br />
<br />
The problems with the shield are manifold.<br />
<br />
1. It slows down the gameplay on defusal maps<br />
When the Counter-terrorists are offensive then the shield is nice, lets them get past the AWPer and campers and get those hostages and get out.<br />
<br />
On defusal maps its a different story. It slows things down, makes a rush much harder as a shielder is a threat not easily taken out on your average pub server. If he has a team picking at you then you have to split your attention, one eye on the guy with the M4 bursting at you on Aztec 40-50 metres away and the shielder to your left who will cap you the moment you turn to fight the M4er.<br />
<br />
As a result, it gives the CTs who already typically have the advantage on defusal maps, an even greater advantage and one that can make a rush lose its momentum quite quickly and be driven back.<br />
<br />
2. Buggy Hitboxes and hit detection and generally buggy<br />
Since its introduction the thing has been plagued with problems, many problems. From invisibility bugs to throwing it through walls to being able to shoot the server thinking your shield is still up. Its worst problem is its buggy hitboxes and hit detection.<br />
<br />
Lets just say too many times has a player stopped, crouched and aimed right on a shielders head and watch a "spark" from the shield appear and the noise of hitting the shield sound off. Too many times has a player gotten around a shielders side and still found himself spamming into the shield, too many times then they have reloaded has the player found himself not hitting the guy despite the shield being down.<br />
<br />
Occassionally you'd shoot the shield itself and it would hit the guy's head if he was moving. This is yet even more bugginess but typically the shield stops shots whether they are on the shield or just near it.<br />
<br />
3. It gives too much protection, even when its down.<br />
This is one of the biggest issues which is due partly to the second point I made above, the thing even when you have it down means that half your target is covered when you shoot if not more and its so quick to bring up or drop down.<br />
<br />
Now how the shield is held and moved about and the name of a "Riot shield" which has been thrown about would indicate that the thing shouldn't exacltly be stopping high calibur rifles that are repeatedly hitting the thing in the same spots.<br />
<br />
Shields that strong are mounted on wheels because they are too heavy to hold for any real length of time let alone shoot accurately while holding.<br />
<br />
4. The thing is so cheap<br />
At a bit over two grand you can get it most rounds. With all the problems accociated with it such as slowing down enemy rushes and all, if its there every round then the problem becomes worse.<br />
<br />
5. Too lethal with it<br />
Lets face it, pistols are made overly strong for what they are so then they are actually useful back ups. Its a good thing too that they are but when it comes to combining that mechanism to keep pistols useful with something like the shield, it results in a shielder just being too dangerous when everything is put together.<br />
<br />
A shielder with a DEagler who has good aim can be a nightmare as he only needs one or two shots for a bink and then he can put his shield back up and due to the pistol's accuracy on the move for those first shots, he doesn't have to stop to do it.<br />
<br />
The shield when it was originally being talked about I imagined as something that covered you upper body and if you crouched all your body. I figured fair enough, you could still be shot but if you crouched you could slowly push up, you could crouch and hide an AWPer behind you or if you saw an enemy you could crouch quickly for the protection.<br />
<br />
I didn't envisage a shield that completely covered your body, gave awesome protection even when down and happened to clip through the floor when you crouched.<br />
<br />
I mean, I've thrown a few ideas about before. One idea was to make it so you couldn't crouch with a shield, at least limiting their mobility to main entrances and preventing them from getting into odd camping spots or through certain ways by crouch jumping or from boosting on top of other player's heads easily full stop.<br />
<br />
Another was that when you lower your shield, the guy brings it so the long edge is roughly horizontal with the ground and to the side so his entire body pretty much can be seen by a player standing in front of him thus meaning if the shield comes down, so does most of the protective quality of the shield.<br />
<br />
A lot of ideas have been played with, most like the second one found wanting as they make shielders then camp more. Some had a little promise but didn't quite fix the main issues at hand like the first idea, sure it was alright but it didn't quite solve the problem of shielders being too lethal and too protected and thus slowing down rushes on defusal maps and being a CT item it would be used defensively and for hostage rescue maps where its real use would come into play the crouch thing would simply limit it.<br />
<br />
So, I came upon the conclusion that you either do the following.<br />
<br />
1. Limit it to hostage rescue maps ONLY and fix its bugginess<br />
<br />
OR<br />
<br />
2. Remove it completely.</font></p>
	
