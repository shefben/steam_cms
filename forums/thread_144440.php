	<font face="verdana,arial,helvetica" size="1" > <b>Cafe Administration Release - Version 1.2</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >A new version (V1.2) of the Café Administration Server is now available.  We have added a number of new features and improve overall system performance.<br />
<br />
CAServer:<br />
* Fixed CAS taking anything but ESCAPE to quit<br />
* Added SPACE key to emit current number of consumed licenses on CAServer<br />
* Improved LAN copy speed<br />
* Added MAC address for master server verification for cafes with dynamic IPs<br />
* Added timed update mode -- can be enabled/disabled, and times for update can be set by cafe owner.<br />
* Fixed issue with corrupted caches stopping loading and giving warning about "skipping app"<br />
* Fixed "Cannot logout of Steam" error when another Steam instance is running.  This will no longer cause the CAServer to throw the error on a dialog box thereby stopping its execution. Instead, the error is logged in the log file (it did previously as well).<br />
* Fixed requirement (bug) for having cr/lf at end of passwords.txt file.<br />
* Added timestamp to CAServer to show when updates are processed<br />
<br />
LSClient:<br />
* New configuration parameters for lsclient<br />
* Added ability to force Steam to logout after game is shut down (cafe request)<br />
* Added save/restore capability -- Café customers can create local accounts "cafe login" with passwords and have their game settings restored each time they visit the café.<br />
* Added ability for client to accept user login thereby allowing cafe customers to log into Steam using their own Steam accounts<br />
<br />
Misc<br />
* Created a utility for customers to retrieve all MAC addresses for a particular pc<br />
* Created utility UI for CAServer configuration and cafe account creation<br />
	* Edit CAServer configuration parameters<br />
	* Add/Edit/Remove cafe users for saving saved games and user configuration files<br />
<br />
We will continue to support CAS version 1.1 until the café customer base has moved to the new MAC address base master server.  In order to use the new CAS software, you'll need to download the new CASPackage.zip file from Valve's FTP:<br />
<br />
ftp.valvesoftware.com<br />
User: CAServer<br />
Pass: ValveCAS2112<br />
<br />
Instructions (word doc) are included in the zip for proper installation of the CAS and client software.  If you haven't already, please make sure you email <a href="mailto:casmaster@valvesoftware.com">casmaster@valvesoftware.com</a> and include the following information:<br />
<br />
1) Contact email address<br />
2) External static IP address or MAC addresses reported using the MACUtil found in the CASPackage.zip file<br />
3) Café ID #<br />
<br />
If you have any problems, please contact <a href="mailto:support@valvesoftware.com">support@valvesoftware.com</a>.  Be sure to include any error or warning messages you are seeing and a full description of the problem and your café ID number.<br />
<br />
Mike</font></p>
	
