	<font face="verdana,arial,helvetica" size="1" > <b>Steam Startup and Connection Problem FAQ</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >For those of you having problems getting Steam to install or update<br />
and/or experiencing connection problems, here is a FAQ with a list of things to try.<br />
<font size="1"><font color="orange"><i>last updated 16June2004 by qUiCkSiLvEr</i></font></font><br />
<br />
<a href="http://home.covad.net/~k25125/SteamyThings/Steam_Startup_Connection_Problem_FAQ.htm" target="_blank">http://home.covad.net/~k25125/Steam...Problem_FAQ.htm</a><br />
<br />
This is a work in progress and I'll be updating it with new information as I find it.<br />
<br />
<font size="3"><font color="aqua">MORE HELP FAQs HERE: </font></font><a href="/forums/showthread.php?threadid=85158" target="_blank"><font size="3"><font color="orange">qUiCkSiLvEr Help FAQs </font></font></a><br />
<br />
<font size="2"><font color="yellow">Edit by Taylor: your system clock no longer has to be the same as the Steam clock for Steam to work on your computer.</font></font><br />
<br />
Get this program and RUN IT:<br />
<br />
<a href="http://schmail.com/atomictime/" target="_blank">AtomicTime Program</a><font color="yellow"> &lt;-- smallest Windows Time Program around!</font><br />
<br />
I'll be adding a troubleshooting flow chart and more detailed information about Steam and game port requirements and how to set up firewalls and routers to work with Steam over the next couple of weeks.<br />
<br />
It's very important that if you try something out of the list and it works that you let me know so I can start adding emphasis to try those things first.<br />
<br />
Hang in there everyone, stay positive.<br />
<br />
edit: and (lol) yes, I'll keep working on the color scheme!<br />
<br />
<blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr><font color="orange">Many systems are infected with spyware parasites that <br />
can prevent Steam from installing/updating/running.</font><hr></blockquote>For a quick check of your system go here:<br />
<a href="http://www.doxdesk.com/parasite/" target="_blank">http://www.doxdesk.com/parasite/</a><br />
<br />
<u><font size="4"><font color="orange">Top Notes:</font></font></u><br />
<br />
1. <a href="/forums/showthread.php?s=&amp;threadid=95719" target="_blank"><font color="cyan">Norton Script Blocking Can Keep Steam From Working!</font></a><br />
<br />
2.  <font color="white">MarketScore</font> is not compatible with Steam!<br />
     Other hijacking proxy servers (spyware) are probably bad too.<br />
     here is a work-in-progress FAQ with specific removal help:<br />
<a href="http://home.covad.net/~k25125/fixes/MarketScore_Removal.htm" target="_blank">MarketScore (Netsetter) Spyware Removal</a><br />
<br />
3.  <font color="white">Steganos Internet Anonym</font> (IP masking proxy service) is not compatible with Steam!<br />
<br />
4.  <font color="white">Netlimiter</font> is not compatible with Steam at this time.<br />
<font color="cyan">Update, -  NetLimiter 1.30 has been reported to work with Steam</font><br />
<br />
5.  <font color="white">CyberSitter</font> has been identified to cause serious problems with Steam.<br />
<br />
6.   <font color="white">NetSonic Internet Accelerator or NetSonic Pro (Web3000)</font> are not compatible with Steam.<br />
<br />
<font size="3"><font color="lime">7. ActionTec modem/routers cause Problems!</font></font><br />
Please read this <a href="/forums/showthread.php?s=&amp;threadid=95274" target="_blank">Thread</a> by ShUtY for specific information.<br />
<br />
8.  Only run One Firewall at a time.<br />
<br />
9.  Disabling a firewall is not necessarily removing it from your system!  If in doubt, uninstall it and re-install it.<br />
<br />
10.  If you are having problems, disable Steam from starting up when you boot until your problem is solved.<br />
<br />
11.  <font color="white">Trend Micro PC-cillin</font> (anti-virus) includes a firewall!  Make sure you configure it to work with Steam.exe and HL.exe.<br />
<br />
12.  Make sure Steam.exe and HL.exe <font color="white">are NOT running in compatibility mode</font> under XP.<br />
<br />
13. <font color="white">TweakMASTER DNS</font> may cause problems with Steam.  PM me if you have this program.<br />
<br />
14. You can try exiting from Steam and deleting your ClientRegistry.blob file, especially if you have moved your Steam install.<br />
<br />
15. You can also try exiting from Steam and deleting platform.gcf<br />
<br />
<u><font size="4"><font color="orange">Additional Notes:</font></font></u><br />
As a recommendation, if you are having problems, before doing a Steam Install or update, deactivate/disable any Anti-Virus or <font color="white">Internet Accelerator Programs</font> as well as programs like <font color="white">AtGuard, NetZip or GetRight</font> that may be running.<br />
<br />
It is possible (from what I'm seeing) that some versions of these programs can interfere with the Steam Install/Update  process.<br />
<br />
If you see problems and one of these type of programs is running, they may very well be the cause of the failures you are seeing.<br />
<font color="white"><br />
*==================================*<br />
* Here is a summary of what I'm seeing so far *<br />
*==================================*</font><blockquote><font face="verdana,arial,helvetica" size="1" >quote:</font><hr><font color="orange">Steam can either communicate on the internet or it can't.<br />
If it can't then something is preventing it from doing so.<hr></blockquote></font> <u><font size="3"><font color="orange">The known (big) offenders are:</font></font></u><br />
1.   Firewalls that aren't properly configured<br />
2.   proxy servers<br />
3.   trojans or virus' that scramble the executable code<br />
4.   Spyware programs that interfere with normal internet activity<br />
5.   Anonymous IP programs (another proxy program really)<br />
6.   incompletely uninstalled firewall programs<br />
7.   multiple running firewall programs<br />
8.   multiple running anti-virus programs<br />
9.   programs that are intended to block spam but aren't set up right.<br />
10.  download accelerators (again, another proxy program)<br />
11.  some browser toolbar addons<br />
12.  multiple network interface cards in one computer<br />
13.  P2P programs like Kazaa that are blocking a significant amount of the downlink or uplink bandwidth so Steam transactions get seriously delayed or even lost.<br />
14. Other computers on a home network that are utilizing a significant amount of the downlink or uplink bandwidth causing Steam transactions to get seriously delayed or lost. 15. The player's dsl/ISDN/cable/telephone line is marginal.<br />
16. The Player's computer has a O/S configuration problem causing IRQ/DMA/memory conflicts<br />
17.  The Player's computer actually has bad memory or some other electronic problem.<br />
<br />
<font color="orange">You can d/l a very good free memory test program from here:</font><br />
<a href="http://www.simmtester.com/" target="_blank">Simmtester Free DocMeM Memory Test Program</a><br />
You have to register (free, no spam from them either) to get the program.<br />
<br />
It makes a bootable floppy that will then test your system memory very very thoroughly ... it may take several hours if you have a lot of memory or a slow machine.<br />
<br />
In addition, <font color="white">Internet Connection Sharing Software</font> can also be a problem<br />
(again another form of proxy).<br />
<br />
There are other things to look for but these are the top things I've found so far.<br />
<br />
<u><font color="orange"><font size="2">Things that can cause problems that are somewhat out of player's control are:</font></font></u><br />
1.  Steam network overloaded<br />
2.  Player's ISP having problems<br />
3.  Content provider is overloaded (this is related but independent from Steam itself)<br />
4.  Player's ISP is intentionally blocking certain ports for security reasons (many school and university backbones)<br />
5.  The internet itself is overloaded due to some trojan or virus propagating<br />
<br />
Having System Resources Problems? Here is a good guide on what you can enable and disable (WinXP Home/Pro):<a href="http://service1.symantec.com/SUPPORT/tsgeninfo.nsf/pfdocs/2002010712042239" target="_blank">How to free up Windows XP system resources</a> and if you are adventurous you can try some of these tweaks too: <a href="http://www.blackviper.com/" target="_blank">BlackViper Tweaks</a><br />
<br />
Have a <font size="3"><font color="yellow">Modem?</font></font> then check out:<br />
<a href="/forums/showthread.php?s=&amp;threadid=73708" target="_blank"> Dr Tweak's Advanced 56k help guide (found by w00ters)</a><br />
One think I want to say here though, download managers are important for Modem users but there are also many bad download managers/accelerators that may interfere with Steam, please see item #11 above.<br />
<br />
For those of you with Mouse or Keyboard Problems, try this thread:<br />
<a href="/forums/showthread.php?s=&amp;threadid=76551" target="_blank">w00ter's Mouse Lag Guide</a><br />
<br />
I'll continue to add to these lists over time and add specific solutions if Players will continue to submit good error descriptions and their msinfo32 files.</font></p>
	
