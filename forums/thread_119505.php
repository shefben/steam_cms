	<p><font face="verdana, arial, helvetica" size="2" ><b>This is the new new <i>CZ Mission Packs List</i> thread, replacing the old ones (which can still be found <a href="/forums/showthread.php?s=&amp;threadid=84247" target="_blank">here</a> and <a href="/forums/showthread.php?s=&amp;threadid=88092" target="_blank">here</a> for reference purposes).<br />
