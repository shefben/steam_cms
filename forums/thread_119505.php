<b>Installation</b>: Inside this zip is an exe - just point it towards your Condition Zero directory when installing and it does the work for you <img src="images/smilies/smile.gif" border="0" alt=""><br />
<b>Description</b>: Instead of the CZ maps, this pack uses 18 of the original CS maps, hence the pack's name. It is also the first pack to use custom bot models. This is well worth getting.<br />
