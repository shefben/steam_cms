	<p><font face="verdana, arial, helvetica" size="2" >I contacted ATI tech support about a possible problem with my drivers.  It turns out that ATI is fully aware of a driver uninstall problem with earlier versions of the Catalyst driver package.<br />
<br />
If you have updated your catalyst drivers since 4.6 try re-installing the latest drivers using the following procedure:<br />
<br />
<br />
Uninstall and Reinstall the ATI Display Driver and ATI Multimedia<br />
Center - Windows 2000/XP.<br />
<br />
For more information please refer to <a href="http://www.ati.com/support/faq/win2k/win2kdriverinstallation.html" target="_blank">http://www.ati.com/support/faq/win2...stallation.html</a><br />
<br />
for driver installation steps.<br />
<br />
1. Uninstall the ATI Multimedia Center followed by the ATI Display<br />
Driver. <br />
Both are located in the ADD\REMOVE Programs under the control panel<br />
<br />
2. Restart into SAFEMODE. <br />
<br />
Hold Down the SHIFT key or tap the F8 key as your system restarts.<br />
<br />
3. Remove all monitors and display adapters listed under Device<br />
Manager.<br />
<br />
To Access Device Manager under Windows 2000/XP<br />
<br />
A. Right-Click My Computer<br />
B. Click on PROPERTIES<br />
C. Select the HARDWARE TAB<br />
D. Select the DEVICE MANAGER BUTTON<br />
<br />
4. Select START and then RUN. In the Open Field type REGEDIT followed<br />
by selecting the OK button.<br />
<br />
NOTE: Prior to making any changes please backup your system registry.<br />
<br />
To backup the system registry.<br />
<br />
A. Select and highlight My Computer in the Registry Editor<br />
B. Select File and then Export<br />
C. Export Registry File dialogue box will then appear<br />
D. In the File Name Field type the name of the file (i.e. Backup)<br />
E. Ensure that "ALL" is selected in the Export Range section<br />
F. Select the SAVE button.<br />
<br />
5. Remove the following registry keys: <br />
<br />
NOTE: Not all keys will be available under every OS.<br />
<br />
HKEY_CURRENT_USER\Software\ATI Technologies<br />
HKEY_LOCAL_MACHINE\SOFTWARE\ATI<br />
HKEY_LOCAL_MACHINE\SOFTWARE\ATI Technologies<br />
<br />
6. Close the Registry Editor and restart the system<br />
7. Allow the system to redetect the display adapter. Allow Windows to<br />
install a default VGA graphics adapter. <br />
<br />
NOTE: This will not occur under all operating systems.<br />
<br />
8. Once prompted restart the system.<br />
9. Windows will then redetect the monitor. Allow the system to<br />
install standard monitor driver for your display   <br />
10. Install DirectX and then restart the system (located at <a href="http://www.microsoft.com/directx" target="_blank">http://www.microsoft.com/directx</a>)<br />
11. Install the Video Capture Update - Restart once prompted (located<br />
at <a href="http://www.microsoft.com/directx" target="_blank">http://www.microsoft.com/directx</a>)<br />
<br />
NOTE: If you have DirectX 8.1 installed you do not need to install<br />
the Video Capture Update.<br />
<br />
12. Install the ATI display drivers and then the ATI MMC - Restart<br />
once prompted<br />
13. Test the PC <br />
 ==================================================<br />
=================<br />
Drivers and Frequently Asked Questions (FAQ's) :<br />
<br />
Windows XP: <a href="http://www.ati.com/support/faq/winxp.html" target="_blank">http://www.ati.com/support/faq/winxp.html</a><br />
 ==================================================<br />
=================<br />
<br />
There was something else...  Disable [uncheck] Write Combining in Display properties/ troubleshoot/ hardware acceleration dialog box.  Some motherboards have the same option in their BIOS.  ATI cards in the 9500, 9600, 9700 and 9800 seiries do NOT support it.<br />
<br />
<br />
My FPS had doubled!!!<br />
<br />
I went from 30 FPS during fire-fights to around 50, and my max FPS is around 120 instead of 90ish.<br />
<br />
My rig:<br />
AMD barton 2500+ @ 2.23GHz<br />
512 Mushkin high perf @ 402Mhz<br />
Abit KV7 bios v16<br />
ATI 9600XT 128Mb<br />
WD Raptor 74Gb SATA<br />
<br />
Resolution: 1024x768 High 2XAA 8XAF<br />
<br />
Test D3D capabilities and performance using D3D Right Mark.<br />
Available at <a href="http://3d.rightmark.org" target="_blank">http://3d.rightmark.org</a><br />
<br />
If it doesn't work, well then post a reply.  I did lots of things today, and I think doing the above had the biggest impact.</font></p>
	
