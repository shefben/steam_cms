<br />
I've played cs for over 4 years, and I think the game is great.. I've stuck with it through the recent changes that caused you guys all that grief; awp delay, crosshair fiasco, etc. I've played CS:S at LAN last night, and I mean - the graphics are awesome. The concept of the game is great, but the gameplay is really lacking some of the barebone essentials of counter-strike. I mean, there IS a big competitive community out there who love this game and make money off this game. The CPL/CAL etc are all organizations that rely on this game to keep their respected economys flowing without problems.  I'm suggesting, merely suggesting the following:<br />
<br />
VALVe working together with "higher-up's" in the competitive cs community devloping a cs "pro" mode. This "pro" mode would be used at CPL/CAL and any other LAN event that plans on hosting a CS type tournament. I can assure VALVe just from my experience that a CS pro mode would not be tearing up the community, it'd be making it larger. Theres tons of people who've quit cs due to the massive ammounts of changes and would love to come back to try out a new mode that was designed by gamers. I understand you will question the credibility of the competitive scene but as you can see the CPL (<a href="http://www.thecpl.com" target="_blank">www.thecpl.com</a>) and CAL (<a href="http://www.caleague.com" target="_blank">www.caleague.com</a>) Both have over 10,000 registered players for just CS alone. I've been a gamer ever since I could remember, whether it be sega, n64 or super nintendo.. and what "hooked" me deep into CS was the fact that it was a unique game, slightly fast paced but not too fast; slightly real but not too real. It was a balanced game, a game that I become hooked on. Most games are either too fast and boring, or too real and boring. Counter-Strike was always a real but not game that kept you wanting more time and time again.<br />
<br />
In closing I ask VALVe/Steam to consider the points in the above and please see if anything could be done, It would not be a waste of time/money to fund this project because you'd be helping your communtiy.. the communtiy that made the game what it is now, I think our input means something; So please, do the right thing.</font></p>
	
