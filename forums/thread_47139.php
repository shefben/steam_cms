	<p><font face="verdana, arial, helvetica" size="2" >Writing anti-hack technology &amp; programs that detect foreign objects on a machine is actually a lot like anti-virus, difference being anti-virus companies have a lot more experience at it. <br />
<br />
I'd like to see an anti-virus company take interest in this problem. I believe an anti-virus company (jointly or not) could probly produce the most effective anti-hack software/detection ever seen. An anti-virus company could do this jointly with another (gaming) company, produce their own software/standard (like pb) or even integrate definitions into current/public AV software<br />
<br />
Not only could the anti-hack/anti-virus-like software be built into the gaming client but the definitions could also be passed over to the actual normal/public anti-virus software and/or easily could 'lock'/remove files/modifications from the user, which it already does anyways when 'viruses' are detected. (or even auto report to the associated gaming company that a hack has been detected) <br />
<br />
If you had to uninstall your anti virus completely, previous to putting a hack on your system or using it at all, to get your hack to function, a lot of ppl would think twice. Push come to shove having anti-virus programs auto report a hack could eliminate the possibility of uninstalling the anti-virus to get the hack to function. What if you could only scan your system for viruses if you had the hack completely removed from your system first. Keep in mind every piece of software leaves something behind even after being 'removed'. Current AV software can already detect viruses that have been previously removed. What if it actually came down to not being able to use any anti-virus software at all if you've ever had a hack on your system. This in combo with an AV company working on some kind of AV/AH integration/detection on the actual gaming client could slow this problem way down.<br />
<br />
Another issue to bring up is the fact that a lot of Anti-Virus companies already detect 'hacking programs/exploits' that aren't viruses at all, if it was globally/widely accepted that online gaming hacks are considered to be on the same level of threat, then their definitions could be passed around such globally between AV companies. Even on this (lower) level the file(s) would be locked from the user if the AV was running/installed.<br />
<br />
Whatever anti-virus company makes the move first will surely be bringing in the profits. Lets face it, when it comes to detecting un-wanted software anti-virus companies have been doing it for quite a while, its time to stop trying to make everything from scratch, and team up with people who've been doing this for a very long time. <br />
<br />
If an anti-virus company got involved in any fashion regardless of how (joint or not) i believe would make quite a difference.</font></p>
	
