	<p><font face="verdana, arial, helvetica" size="2" >Alright Here Is a guide to making buy command <font color="#D8DED3">binds</font> on CS:S Beta. It will Also show you the minor differences between 1.6 buy command <font color="#D8DED3">binds</font> and CS:S Beta buy command <font color="#D8DED3">binds</font>.<br />
<br />
First Here Is a link that gives a Basic explanation please read before continuing.<br />
<br />
<a href="http://www.csnation.net/articles.php/cssguide_200/" target="_blank">http://www.csnation.net/articles.php/cssguide_200/</a><br />
<br />
You Can open up the config.cfg files with  mircrosoft notepad.<br />
<br />
After you have read that you should have some idea of what Your new <font color="#D8DED3">binds</font> will look like.<br />
<br />
Next Here is my old CS or CZ 1.6 <font color="#D8DED3">binds</font> they can be found in C:/ProgramFiles/Steam/YourAccount/counter-strike/cstrike/config.cfg<br />
<br />
or for CZ<br />
C:/ProgramFiles/steam/YourAccount/ConditionZero/Czero/config.cfg<br />
<br />
I Like using the F1, F2, etc keys for binding my buy commands.<br />
<br />
bind "F1" "primammo;defuser;"<br />
bind "F2" "mp5;primammo;defuser;"<br />
bind "F3" "famas;galil;primammo;defuser;"<br />
bind "F4" "m4a1;ak47;primammo;defuser;"<br />
bind "F5" "hegren;flash;flash;sgren;defuser;"<br />
bind "F6" "awp;primammo;defuser;"<br />
bind "F7" "vesthelm;"<br />
bind "F9" "snapshot"<br />
bind "F10" "save quick"<br />
bind "F11" "load quick"<br />
bind "F12" "quit prompt"<br />
<br />
(I leave F8 Unbinded as a barrier or buffer between buy commands and other counsol options.)<br />
<br />
As you can see i binded AK47 and M4A1 on the same key. If I'm a CT  I'll get an M4A1 if I'm T ill get an AK47. This is also  commonly done with the Clarion(Famas) and Defender(Galil) <br />
<br />
The Order you put the items In, DOES matter, You buy them in the order the items are typed left to right.<br />
<br />
Now for the CS:S Beta Config<br />
You Can find it at<br />
C:/ProgramFiles/Steam/YourAccount/counter-strike source beta/cstrike/cfg/config.cfg<br />
<br />
With Mine I Set it up basically the same as my cs 1.6 because I like my custom feel for it.<br />
<br />
bind "F1" "buy primammo;buy defuser;"<br />
bind "F2" "buy mp5;buy primammo;buy defuser;"<br />
bind "F3" "buy famas;buy galil;buy primammo;buy defuser;"<br />
bind "F4" "buy m4a1;buy ak47;buy primammo;buy defuser;"<br />
bind "F5" "buy hegrenade;buy flashbang;buy flashbang;buy smokegrenade;buy defuser;"<br />
bind "F6" "buy awp;buy primammo;buy defuser;"<br />
bind "F7" "buy vesthelm;"<br />
bind "F9" "jpeg"<br />
bind "F10" "save quick"<br />
bind "F11" "load quick"<br />
bind "F12" "quit prompt"<br />
<br />
If you Follow the Above format On your keys it will work correctly.<br />
<br />
The few things that you must change to your old <font color="#D8DED3">binds</font> is the "Buy" command. You must add it before every item on the hotkey.<br />
<br />
Also the gernade names changed<br />
<br />
From in 1.6<br />
bind "F5" "hegren;flash;flash;sgren;defuser;"<br />
<br />
To This in CS:S Beta<br />
bind "F5" "buy hegrenade;buy flashbang;buy flashbang;buy smokegrenade;buy defuser;"<br />
<br />
the differences are clear.<br />
<br />
Also If you want to check spelling for adding in other <font color="#D8DED3">binds</font> they can be found by typing "Buy" in counsol on a server.<br />
