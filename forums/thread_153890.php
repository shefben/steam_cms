	<p><font face="verdana, arial, helvetica" size="2" >I belive that dealing moderate damage to an enemy through a wall and knowing certain spam spots to get an edge on the map was something unique to counter-strike. The ability to 'wall' someone was nicely implemented into the game. It adds a different aspect to the game, especially in competative lan tournaments. Getting walled is part of the game. It was part of the game to shoot at someone while they were running behind a wall. That is Counter-Strike.<br />
<br />
I dont think that an excuse like, "Oh, it was removed to help protect against cheating" is a good excuse, because that is what VAC is there for. Valve should be working on VAC to the point that where players could have a high trust in it that other players wouldnt be cheating.<br />
<br />
Source has completely removed traditional walling and made shooting thru doors and thin walls nearly useless. I think we need to bring back part of Counter-Strike.<br />
<br />
<br />
[edit]<br />
I was going to add this to a different post in the future, but I decided to do it now after seeing the first reply.<br />
<br />
Some other things that valve needs to think about to increase gameplay. I've always belived that gameplay should come before beauty and realism.<br />
<br />
When spraying or at farther ranges it is nearly impossible to see where your bullets are landing, so you cannot efficiently control your spray. These should be visible.<br />
<br />
It is nearly impossible to tell when you've hit someone in the head. In traditional CS if they werent equipped with a helmet you could see a trail of blood exit the enemies head. In Source it looks more like if a small can of spray paint was heated and it happened to burst. In traditional CS there was always a loud and rewarding sound to a headshot, the 'dink'.<br />
<br />
If this game wants to replace Counter-Strike in league play and online play, I think it needs to live up to it's ancestors gameplay value.</font></p>
	
