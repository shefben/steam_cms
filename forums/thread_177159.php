	<p><font face="verdana, arial, helvetica" size="2" >A few tips I've gotten to reduce motion sickness:<br />
<br />
* do what you can to increase framerate/reduce framerate fluctuation (reduce screen res, sound and video quality settings)<br />
* setting fps_max to something reasonable may help<br />
* activating mat_vsync may help<br />
* switching from FOV 75 (default) to FOV 90 may help<br />
<br />
Other tips I've received:<br />
<br />
* while in vehicles, run into less things<br />
* throw up... you'll feel better for a while<br />
<br />
<img src="images/smilies/smile.gif" border="0" alt=""><br />
<br />
<br />
<font color="yellow"><b>Edited by MATT. Also try this:</b></font><br />
<br />
(Read more at <a href="http://www.halflife2.net/forums/showthread.php?t=51945" target="_blank">HalfLife2.net</a>.)<br />
<br />
1) Update your monitor's drivers.<br />
<br />
2) Find out the Maximum Refresh Rate that your monitor supports for the resolution that you prefer gaming on (lowering resolution will result in higher possible refresh rates). This step is just for informational purposes. You may skip it since Step 4 completes this.<br />
<br />
3) Download RefreshForce (Google it).<br />
<br />
4) Tell it to "Auto Find" your refresh rates and modes. Click Apply.<br />
<br />
5) Go to Steam --&gt; Right click Half-Life 2 --&gt; select Properties --&gt; Launch Options.<br />
<br />
Now you can specify the width, height and refresh rate of the screen on which you will be playing. Mine, for example is this: -w 1024 -h 768 -refresh 100<br />
<br />
This means I am running a resolution of 1024x768 at a refresh rate of 100hz.<br />
<br />
6) Click OK and close the Properties window.<br />
<br />
7) It's best to restart your computer now, since you applied that RefreshForce fix.</font></p>
	
