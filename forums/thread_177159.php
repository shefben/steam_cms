	<p><font face="verdana, arial, helvetica" size="2" >Motion sickness is caused when there is a difference between what your inner ear is telling your brain and what your eyes see.<br />
<br />
If you're in a car that's moving but you are reading or looking at something inside the car, your ears will perceive motion but your eyes do not.  This causes car sickness.<br />
<br />
Likewise if you're playing a computer game, your eyes perceive motion but your inner ears says your standing still.<br />
I think the problem may be more pronounced in Half Life 2 since it is so immersive.<br />
<br />
If you watch gamers who play alot you may notice them moving their head around and shifting their bodies from side to side while playing.  I do this alot and my wife makes fun of me for it but I think that's why I don't get motion sick while playing.<br />
<br />
The best solution though is to sit a little farther away from your monitor.  Just push the screen further back on your desk so that less of your field of view is taken up by the screen.  Just a little bit can help alot.</font></p>
	
