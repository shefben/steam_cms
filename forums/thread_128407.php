	<p><font face="verdana, arial, helvetica" size="2" >Sorry for the capitals... <br />
<br />
But after about 15 hours playing the Beta, the main thing that is killing the "CS Feel" is the netcode more than any in-game tweaks or "realism" additions.<br />
<br />
I was on a sub-30 ping server and was about three (game) feet from an enemy, aimed directly at his head and he didn't die. This is after emptying half a magazine of an MP5 on him as well.<br />
<br />
And before all the 12-year olds invade this thread with pointless, "You just suxors, dude!", or "Learn to aim, F*got Biatch!" posts...<br />
<br />
The problem was experienced by every other person on the 14 player server -- Meaning, this wasn't some uber 20 vs. 20 game where pings were spiking all over the place.<br />
<br />
In addition, I had these cvars set in my config as follows:<br />
<br />
cl_smooth 0<br />
cl_smoothtime 0.1<br />
cl_interp 0.1<br />
cl_predict 1<br />
cl_lagcompensation 0<br />
<br />
The in-game movement is indeed smooth... <br />
<br />
But again, actual shots registering was a common occurance and the main reason people started dropping from the game because they felt it was "no fun" to just "spray and pray" in the hopes that the server (finally) registered their shots.<br />
<br />
I know that Source is not the HL-1 engine and that Server Admins really need to upgrade a lot of their hardware to run it...<br />
<br />
But I can tell you, this is the root cause for half of the complaints and comments about this game on this forum that go like:<br />
<br />
1) "My uber X800XT PE/6800 Ultra gets sh*tty frames per second. WTF? Source Suxxors!"<br />
<br />
and...<br />
<br />
2) "Quit ruining CS you Valve Nubs! It's too random and has no skillz!"<br />
<br />
Truly.<br />
<br />
The hit detection (and object collision) needs to be of a much better quality when the final game ships, or you will have more flaming and upset CS players that actually don't know WHY they are saying the things they are (or will).<br />
<br />
Don't make the mistake UBISoft did with the Raven Shield netcode -- which is actually about at the same, subpar level as the CSS Beta -- And just leave it like this because there are much better games with better netcode that register hits and misses as they should be.</font></p>
	
