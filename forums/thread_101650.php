	<p><font face="verdana, arial, helvetica" size="2" >This is just a summary of Steam Accounts and CDKeys, in a litle more detail then the FAQ which is here:<br />
<a href="/index.php?area=faq&amp;id=1050915492,63276400,1080009373,58908500" target="_blank">/index.php?a...009373,58908500</a><br />
<br />
<font color="orange">Steam is Free</font><br />
Players do not have to pay for Steam itself.<br />
<br />
<font color="orange">Dedicated Server is Free</font><br />
Players do not have to pay for Dedicated Servers. Stand-alone Dedicated Servers need a valid Steam Account but do not require a CDKey.  Client Dedicated Servers use the Steam Account that is currently logged in.<br />
<br />
You should not use the same Steam account for your stand-alone Dedicated Server that you use for your Client.<br />
<br />
<font color="orange">Codename Gordon is Free</font><br />
Players do not have to pay for Codename Gordon.<br />
<br />
<font color="orange">CyberCafe Accounts</font><br />
CyberCafe Accounts are totally different from individual player accounts and are supported differently.<br />
<br />
<font color="orange">No Subscription Accounts for Players</font><br />
At this time there are no subscription-type accounts offered for players. Maybe sometime in the future, but not now.<br />
<br />
<font color="orange">Players Must Own a Unique and Valid CDKey</font><br />
Players have to purchase a suitable CDKey to play HL and any of the regular games.<br />
<br />
There are several types of CDKeys out there.<br />
<br />
The WON CDKey: xxxx-xxxxx-xxxx (4 X 5 X 4)<br />
<br />
WON CDKeys are single-game Retail CDKeys that get upgraded to a full Platinum pack CDKeys (in Steam) which give you CS, HL (single-player and multiplayer HLDM), OP4 (single-player and multi-player), DoD, Ricochet, DMC and TFC.<br />
<br />
Blue Shift is not available under Steam but works as a single-player mission if you have the physical CD. <br />
<br />
The Blue-Shift CDKey <font color="white">does</font> give you a full Platinum Pack set of games when you use it for a Steam Account.<br />
<br />
Opposing Force CDKeys are <font color="white">not</font> valid for use with your Steam Account, OP4 required HL (It's an add-on pack only).<br />
<br />
OP4 CDKeys are in this format: xxxx-xxxx-xxxx-xxxx-xxxx (5 X 4)<br />
<br />
<font color="orange">High Definition Packs</font><br />
High-Definition Packs do not work directly with Steam (they might with some manual installation work).<br />
<br />
<font color="orange">Online Purchases</font><br />
If you purchase CZ Retail or Online through Steam, you get CS, CZ and CZ: DS (Deleted Scenes).<br />
<br />
Retail CZ purchases have a CDKey in the format: xxxxx-xxxxx-xxxxx-xxxxx-xxxxx (5 X 5)<br />
<br />
Online CZ purchases do not give you a CDKey number, the CDKey is automatically bound to your Steam account.  You don't need to know the CDKey number, only your Steam Account. (Don't ask for the CDKey from Valve, it's never disclosed.).<br />
<br />
If you have a retail CZ CDKey, when you install Steam, you must select either CS or CZ Games, and when prompted enter your CZ CDKey. If you select any other Games they will not accept the CZ CDKey as valid.<br />
<br />
If you purchase HL online you get the "Premium Pack" which includes everything but CZ and CS. Again, this purchase does not give you a numerical CDKey, it gets bound to your Steam Account.<br />
<br />
<font color="orange">ATI HL2 CDKey</font><br />
ATI HL2 CDKeys were part of a Radeon Graphic Card Purchase and give you access to HL2 when it is released. Until then, that ATI HL2 CDKey will also give you access to the Platinum Pack selection (all the HL1 games except CZ and CZ: DS).<br />
<br />
The ATI HL2 CDKey is on a small scratch-off card that is included in your Graphics card box.<br />
<br />
The CDKey has the format: xxxxx-xxxxx-xxxxx-xxxxx-xxxxx (5 X 5).<br />
<br />
At one time, if you got the ATI HL2 CDKey, you were also able to order a physical CD of most of the Steam Platinum-Pack Games from ATI via their web-page (this was Steam only, and required you to be online to Validate the installation through Steam).<br />
<br />
I don't know if this physical CD is available any longer from ATI.<br />
<br />
The ATI physical CD did not ship with a CDKey but have a SKU number something like "180-G3D056-100" that players often try to use as a CDKey, needless to say it won't work.<br />
<br />
<font color="yellow">ATI Scratch Off - BE CAREFUL</font><br />
You need to be very careful when you scratch off the covering on the ATI HL2 CDKey or you could scratch off the numbers as well. If you damage the card and can't read the CDKey, ATI might help but I don't believe there is anything more you can do about it.<br />
<br />
<font color="orange">ATI CDKey Useage</font><br />
If you already have a Steam Account with a Premium or Platinum pack CDKey attached to it, you won't be able to add the HL2 CDKey to that Steam account right now.  However, when HL2 becomes available, you will be prompted for a valid HL2 CDKey so you can enter it at that time.<br />
<br />
<font color="orange">Only One Type of CDKey</font><br />
Last but not least, you can only add one CDKey of each type to your Steam Account.   <br />
<br />
One WON CDKey plus one CZ CDKey plus one HL2 CDKey.<br />
<br />
Trying to add a 2nd CDKey of the same type to your Steam Account will prove to be a disappointing experience.</font></p>
	
