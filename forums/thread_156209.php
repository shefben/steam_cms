	<font face="verdana,arial,helvetica" size="1" ><img src="images/icons/icon14.gif" alt="Thumbs up" border="0"> <b>List of Commands to Make Counter-Strike: Source Look Even Better!!!!</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >First off, this thread is to list commands that make Counter-Strike: Source look even better, by increasing the looks of it or other simple tweaks.<br />
<br />
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~<br />
<br />
If you want to make use of these commands the easy way then download my generic autoexec.cfg at this link<br />
<br />
<a href="http://mountaincable.net/~jameslin/autoexec.cfg" target="_blank">mountaincable.net/~jameslin/autoexec.cfg</a><br />
<br />
and place it in the directory stated below.<br />
<br />
If you'd rather put the commands directly into your config.cfg then read on.<br />
<br />
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~<br />
<br />
These commands go into your config.cfg, which is found in this directory.<br />
<br />
X:\Program Files\Valve\Steam\SteamApps\YOUR_EMAIL@WHATEVER.COM\counter-strike source\cstrike\cfg<br />
<br />
Open the config.cfg using notepad. When typing the commands in, make sure you type them in below where it says unbindall, which is at the top of the config.cfg Once you put them you will have to save the changes, and it would be a good idea to make the config.cfg a read-only file, so the game doesn't change the settings on you. You can make it a read-only file by right clicking on it and hitting proporties, then checking the box that says read-only.<br />
<br />
Please note that some of these commands may lower your FPS, I personally use the first nine of them and notice no difference in my FPS at all. But the effect is there, obviously more noticeable with the weaker machines.<br />
<br />
Now onto the list of commands!<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #1</b><br />
<br />
mp_decals "4096" <br />
<br />
This determines the maximum number of decals visible at one time, 4096 is the max number for this command, it prevents decals just suddenly disappearing, like when to much blood gets on one wall.<br />
<br />
Default: mp_decals "300"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #2</b><br />
<br />
cl_ragdoll_collide "1"          <br />
<br />
This one makes the ragdolls collide with each other, so they don't clip inside of each other. Makes body pileups look a lot better.<br />
<br />
Default: cl_ragdoll_collide "0"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #3</b><br />
<br />
cl_c4dynamiclight "1"          <br />
<br />
This one gives the C4 bomb ticking light, dynamic lighting, it makes the light on the C4 look much better.<br />
<br />
Default: cl_c4dynamiclight "0"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #4</b><br />
<br />
r_lod "-5"          <br />
<br />
This one makes it so that the models always look their best from any distance, so when you get closer they don't suddenly switch to a different mesh.<br />
<br />
Default: r_lod "-1"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #5</b><br />
<br />
r_decal_cullsize "0"          <br />
<br />
This one makes it so that decals don't just appear when you get close, it makes it so you can see them from any distance. For this command you need to put it in your config and you also need to edit the valve.rc file with notepad and change the setting in there too and save it. The valve.rc file is in the same folder.<br />
<br />
Default: r_decal_cullsize "1"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #6</b><br />
<br />
r_propsmaxdist "5000"         <br />
<br />
This command sets the maximum visible distance for props, it prevents props from fading in/out as you get closer/further away from them. I don't know if 5000 is the actual max number for this command, the default is 1200, so 5000 just seemed like a good number to choose to me.<br />
<br />
Default: r_propsmaxdist "1200"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #7</b><br />
<br />
r_rainradius "2500"<br />
<br />
This command makes the radius of rain fall around you larger, so you can see the rain from further away.<br />
<br />
Default: r_rainradius "1500"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #8</b><br />
<br />
r_rainsplashpercentage "100"<br />
<br />
This command sets the percentage that the rain makes a splash in the water.<br />
<br />
Default: r_rainsplashpercentage "20"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #9</b><br />
<br />
jpeg_quality "100"         <br />
<br />
This command works with the jpeg command, with jpeg_quality set to 100, it makes the screenshots you take 100% quality, so they look really good.<br />
<br />
Default: jpeg_quality "90"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
<b>Command #10</b><br />
<br />
mat_mipmaptextures "0"<br />
<br />
This command makes textures look their best from any distance. This command makes the game look really grainy, I don't use it myself but there are a select few that like this look. This command has quite a big impact on FPS.<br />
<br />
Default: mat_mipmaptextures "1"<br />
<br />
----------------------------------------------------------------------------------------------------------------------<br />
<br />
Theres the ten commands I know of so far to make CS:S look even better! If you find any that aren't already on the list please reply to this topic and add them to the list in a similar way as I have done here.<br />
<br />
