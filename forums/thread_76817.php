Wandering around on the forum, I noticed there is a specific thread for reporting encountered bugs and errors. Though this may be suitable in a sufficient way, do you not think it would be easier if all reporting of such errors would be centralised and consultable in a single database? In my opinion, this would be better for several reasons, those being:<br />
<ul><br />
<li>better organisation of bugs (with status for each bug, such as: ‘Critical&apos;, ‘Important&apos;, ‘Normal&apos;, ‘Minimal&apos;, ‘Resolved&apos;);<br />
<li>easier consultation of posted bugs;<br />
<li>easier and less cluttered research of posted bugs (to avoid duplicate postings, for example);<br />
<li>to a certain extent, easier moderation;<br />
<li>possible further discussion of each posted bug individually;<br />
<li>integration with Steam forum account also possible.<br />
</ul><br />
