	<font face="verdana,arial,helvetica" size="1" > <b>ATI Half-Life2 Coupon warning!</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >This is a warning to those with ATI Half-Life 2 coupons. This has been noted by Steam officially here (<a href="/forums/showthread.php?threadid=101650" target="_blank">/forums/...threadid=101650</a>).<br />
<br />
The coupon in question is EXTREMELY sensitive to scratching. Scratching it with a coin or a key could SERIOUSLY damage the key underneath. Even your fingernails or anything, really, will damage the key.<br />
<br />
Take it from me, I used a coin to lightly scratch the silver stuff concealing the key and even with a light scratching I still managed to take off three digits. I then switched to my fingernail and took off a FULL five digits to the point where they were pretty much gone.<br />
<br />
Once you take the key off the coupon, there is no way to get it back without guessing and if you take off more than one digit off, good luck on getting the right combo. The key makes no impressions on the card so holding it up to the light is of little help. Believe me on that one.<br />
<br />
The best way to take the silver stuff off the key is with a vinyl/plastic eraser. It smoothly rubs away the silver leaving a perfect rendering of the key. An example of what I used to perfectly rub the remaining silver off the coupon is here (<a href="http://www.currys.com/drafting/prodinfo.asp?SubcatID=173&amp;catID=15" target="_blank">http://www.currys.com/drafting/prod...73&amp;catID=15</a>).<br />
<br />
However, if you have damaged your key all is not lost. As long as you have five consecutive digits, that is.<br />
<br />
When I started damaging the key on my coupon, I stopped and started to panic. I gave the card to my girlfriend who proceeded to ruin it a little more before discovering that a vinyl/plastic eraser was the right way to go. Because of her discovery, we managed to save the five digits, which came out perfectly.<br />
<br />
I’ll save you the story on how her and I spent a good hour or so trying to resurrect the missing digits, but I’ll jump to the good part.<br />
<br />
Because my girlfriend has a great voice and professional demeanor, I had her call ATI on my behalf in the hopes that they’d be more sympathetic towards a female in crisis. I was right. She managed to get a hold of someone who provided her with a contact name and fax number. The request was the receipt showing the purchase of my video card and the back of the coupon with the damaged key. <br />
I faxed in this information and within two hours an ATI representative contacted me at work. We talked about this issue and she had told me that this problem has come up quite a few times. She also informed me that because the last five digits were intact, she was able to find my key in the database of keys that ATI has. Apparently, all the keys that are printed on the coupons are kept in a massive database, which is scary and cool at the same time. She read me off the full key and voila. Problem solved.<br />
<br />
I am hoping that these words will not go unnoticed for those of you who dropped a large portion of cash on an ATI product with the thought that a free copy of Half-Life2 would accompany that.<br />
<br />
Kudos and Boo-urns to ATI at the same time. Kudos to them for being so quick, courteous and understanding, they recognized the problem and were great in helping me resolve this issue. Boo-urns to them for producing a shady coupon that toys with the hearts of dedicated gamers, like myself.<br />
<br />
If you don’t have an ATI Half-Life 2 coupon, but know someone who does, please pass on this information, even if they have ruined their coupon.<br />
