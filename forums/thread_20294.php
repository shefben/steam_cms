	<font face="verdana,arial,helvetica" size="1" ><img src="images/icons/icon1.gif" alt="Post" border="0"> <b>How to Install Custom Models, Sounds, Sprites, Maps, and Sprays In CS 1.6</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >I am making this thread to help those who don't know how to instal custom models and sprays.<br />
<br />
Here is what's in this tutorial (in order).<br />
<br />
<ul><br />
<li>Installing Custom Models<br />
<li>Installing Custom Sounds<br />
<li>Installing Custom Sprites<br />
<li>Installing Custom Maps<br />
<li>Making and Installing Custom Sprays<br />
<li>Customizing Autobuy<br />
<li>Customizing Rebuy<br />
<li>Writing Your First Alias<br />
</ul><br />
<br />
**The Last 3 are on page 4 of this thread!!!**<br />
<br />
<br />
<font color="purple"><font size="3"><i><b>NOTE:</b>Through out this tutorial I refer to a directory C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.com\counter-strike\cstrike  This is simply just your counter-strike folder, just find you steam folder, then select the folder steamapps, inside that folder is you email address, select that and then inside that choose Counter-strike and finally within counter-strike is the folder cstrike.</font></font></i><br />
<br />
<br />
<font color="red"><font size="7"><u>Installing Custom Models</u></font></font><br />
<br />
<br />
<font color="blue"><font size="4"><u>Installing Custom Weapon Models</u></font></font><br />
<br />
<br />
<br />
To install custom weapon models, first go into the directory:<br />
<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.com\counter-strike\cstrike<br />
<br />
<br />
In that directory add a folder called models. Within that folder you place your custom models. If you want to install a custom shield model make a folder within your models folder called: shield.  Place the shield models in there <br />
<br />
<br />
<font color="purple"><font size="3"><i><b>NOTE:</b> There isn't a model to just replace the shield, every shield and gun combo is a different model, so if you get a five-seven shield model and the shield is different, the shield is only different for the five-seven.</font></font></i><br />
<br />
From there they should be installed and ready to be played. <br />
<br />
<font color="purple"><font size="3"><i><b>NOTE:</b> NOTE: Not all P models are not compatible with steam, if you place an incompatible P model in the model folder you will be able to start up steam although you will not be able to join a game, it will just quit joining and in the console it will say something about the P model you installed. </font></font></i><br />
<br />
<br />
<br />
<font color="blue"><font size="4"><u> Installing Custom Player Models </u></font></font><br />
<br />
<br />
<font color="green"><font size="3"><i><b>**NOTE:**</b> If you have already completed installing a custom weapon model skip ahead to the paragraph begining with "Open the models folder" </i></font></font><br />
<br />
<br />
To install Custom player models first go to the directory:<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.COM\Ycounter-strike\cstrike<br />
<br />
<br />
In that directory make a folder called Models. <br />
<br />
Open the models folder.  In that folder create another folder called player.  In that folder you will need to create a folder for each character that you are changing model.  So if you are changing the leet krew model you need to add a folder named leet.  The folder named are below<br />
<br />
<font color="orange"><br />
 Character Name - Folder Name<br />
                                      <br />
   SAS - SAS<br />
   German GSG9 - GSG9<br />
   French GIGN - GIGN<br />
   SEAL - Urban<br />
   Phoenix Connection - Terror<br />
   Leet Krew - leet<br />
   Arctic Warfare - arctic<br />
   Guerilla Warfare - guerilla<br />
   VIP -  VIP<br />
   </font><br />
<br />
From there place the player model for that character inside his specific folder.  If you want to install a hostage model place it directly inside the model folder, not the players folder!<br />
<br />
Now your ready to play with your custom player models!<br />
<br />
<br />
<font color="red"><font size="7"><u>Installing Custom Sounds</u></font></font><br />
<br />
<br />
To install custom sounds first go to the directory:<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.COM\counter-strike\cstrike<br />
<br />
In that directory creat a folder called Sounds.<br />
<br />
Inside that folder create a folder called Weapons.<br />
<br />
Within that folder you place your custom weapon sounds.  From there you are ready to use your custom sounds!<br />
<br />
<br />
<font color="red"><font size="7"><u>Installing Custom Sprites</u></font></font><br />
<br />
To install custom sprites first go to the directory:<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.COM\counter-strike\cstrike<br />
<br />
In that directory create a folder called Sprites.<br />
<br />
In that folder all you need to do is place your custom sprites and your done!<br />
<br />
<br />
<br />
<font color="red"><font size="7"><u>Installing Custom Maps</u></font></font><br />
<br />
<br />
<br />
To install a custom map first go to the directory:<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.COM\counter-strike\cstrike<br />
<br />
In the folder cstrike create a folder called: Maps<br />
<br />
(you may already have one if you have downloaded a custom map from a server)  Place all custom maps inside that folder, and your ready to play with them!<br />
<br />
<br />
<font color="red"><font size="7"><u>Custom Sprays</u></font></font><br />
<br />
<font color="orange"><font size="3"><i><b>**NOTE:**</b>  If you already have your custom spray skip the Making a custom spray section and continue to installing a custom spray (section after Making a custom spray) </i></font></font><br />
<br />
<br />
<font color="blue"><font size="4"><u>Making a custom spray</u></font></font><br />
<br />
<br />
To make a custom spray you MUST have a more advanced "coloring" program than paint.  There are some trial versions of software available online.<br />
<br />
<br />
First open the program.  Go to File - New.<br />
<br />
This is important, there is a limit on the size a spray can be, below are a few nice sizes for sprays.<br />
<br />
<font size="3"><font color="orange"><b><i>NOTE:</b> These sizes are in Pixels!!! Make sure it is in pixels or it will most likely end up being too big!</i></font></font><br />
<br />
64x128<br />
128x64<br />
256x32<br />
32x256<br />
32x32<br />
64x64<br />
<br />
(x separates two numbers it means "by" (ex: 64 by 128);  The first number is height and second is width)<br />
<br />
The are will seem very small so use magnified glass (or whatever tool is used to zoom in the program you are using) to zoom in if you want to.<br />
<br />
Now create your picture, put what ever you want on it!  The only other thing you must know is when you are creating it if you want to make a transparent (see-through) area it must be pure blue.  The code for pure blue is: 0000FF<br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut1.gif" target="_blank">Picture of Pure Blue</a>)<br />
<br />
Type that in what ever box allows for it when you are selecting colors.  So if you want blue in your picture make it slightly lighter or darker because if it is pure it will be transparent!<br />
<br />
<br />
Ok, now finish making your picture,  Go to File - Save As  and save your file as what ever name you want but make sure you save it as a .bmp<br />
<br />
If it asks you for saving options leave them at default, it should be correct.  But just to be sure it should be Windows format and 24 bit compression.<br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut2.gif" target="_blank"> Picture of Saving a BMP Correctly</a>)<br />
<br />
Now you need to download a program called Wally.<br />
<br />
Click one of the links below to begin your download:<br />
<br />
<a href="http://www.3ddownloads.com/?file_id=149727#" target="_blank">Download Wally from 3D Downloads</a><br />
<a href="http://www.fileplanet.com/dl.aspx?planethalflife/wadfather/tools/wally_155b.exe" target="_blank">Download Wally from Fileplanet</a><br />
<br />
(Although Fileplanet usually has long lines because this is not a very commonly downloaded file there are only two servers but usually no wait time)<br />
<br />
Now download and install Wally.<br />
<br />
When it is finished installing open the program.<br />
<br />
In Wally in the top menu go to Wizard - HL Color Decal<br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut3.gif" target="_blank">Picture of Wally Menu</a>)<br />
<br />
A new menu should pop up.  First it will say please select your Half-life folder (Do not select your Half-Life, Counter-Strike, Day of Defeat etc. folder!), select what ever folder you want. Next go down to where it says Source Image.<br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut4.gif" target="_blank">Picture of Menu your looking at now</a>)<br />
<br />
There are two options Clipboard and File on hard drive.  Select File on hard drive.  Then next to a big white box there is a button that says "..." Click it and browse for the picture that you just created.  Then click OK.  If you followed directions perfectly that window should close, and a new one with your picture in it should open.  <br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut5.gif" target="_blank">Picture of Window that Should Have Opened</a>)<br />
<br />
Now go to File - Save As<br />
<br />
Select your desktop and save the file as pldecal (make sure it is a .wad image)<br />
<br />
(<a href="http://home.comcast.net/~triforce89/cstut6.gif" target="_blank">Picture of Saving your File Correctly</a>)<br />
<br />
And now your ready to install your spray!<br />
<br />
<br />
<br />
<font color="blue"><font size="4"><u>Installing a custom spray</u></font></font><br />
<br />
<br />
To install your spray go to the directory:<br />
<br />
C:\Program  Files\Steam\SteamApps\YOUREMAIL@YOURSERVICEPROVIDE<br />
R.COM\counter-strike\cstrike<br />
<br />
Take your pldecal file and place it into that directory, it will have to overwrite the existing pldecal.  Then delete a file called custom.hpk<br />
<br />
Now your ready to use your spray in game!<br />
<br />
<br />
<font color="purple"><font size="3"><i><b>NOTE:</b> Do not go into the options area anymore, if your change anything you will have to put your pldecal file back in the cstrike directory!  Although, you can make stop this by right clicking the file and selecting Read-Only, then you will be able to use the options menu.</i></font></font><br />
