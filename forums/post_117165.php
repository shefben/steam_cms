	<p><font face="verdana, arial, helvetica" size="2" >Loads of people have got this error (including me).<br />
<br />
Here's how I fixed it.<br />
<br />
1. Remove the file called ClientRegistry.blob from your steam folder.<br />
<br />
2. Run steam again and let it update.<br />
<br />
3. Create a new account with a valid e-mail addy (dont use the same one you used before).<br />
<br />
4. You should be able to progress to the next screen after this account is created.<br />
<br />
From looking around the forums, it seems that some accounts have got messed up when steam was installing the first time around, or if people tried to inherit an account from the beta releases. By creating a brand new account, it seems to scrub the corrupted one and everything works fine from then on.<br />
<br />
This fix worked fine for me, (after pulling my hair out for 5 days).<br />
<br />
If this is indeed whats causing so much hassel for folks, then I think the Valve support team need to seriously pay attention to the posts on these forums, cos such a simple fix should have been posted by them ages ago.<br />
<br />
Just to make it easier for people to get this fix, please post a reply if this worked for your steam installation, that way it will keep getting bumped up to the top of the lists and will be seen by people quicker. - in fact rate it so it can be seen quicker, this must be the single most infuriating problem folks are having - so help them to see the answer.<br />
