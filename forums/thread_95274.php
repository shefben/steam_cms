	<p><font face="verdana, arial, helvetica" size="2" >Hello, are you using an Actiontec Product and recieving errors? There are a few ways to fix this.<br />
<br />
Some of the Errors that you might be seeing are:<br />
<br />
"Bad Command Character in Client Mod."<br />
<br />
"Bad Parse in Client Command."<br />
<br />
"CMD_MAXBACKUP HIT."<br />
<br />
You may also notice that you might be getting Disconnected more often if you are using your mic a lot.<br />
<br />
Things that we have tried in order to get these Problems resolved are:<br />
<br />
Contacting Actiontec to put out a FirmWare Upgrade. From what I know the last one was I think in 2002. But I guess that they said that they are not going to do it.<br />
<br />
Trying to put the Modems into DMZ Mode.<br />
<br />
Trying to put the Modems into Transparent Bridging Mode.<br />
<br />
Trying a few other things and they did not resolve these Problems.<br />
<br />
1. Get a new ISP&lt;----This is what I did, I now have Charter Internet.<br />
<br />
2. If your ISP is Qwest Call them and tell them that your Modem is giving you Errors while your playing online&lt;----I did call them but instead we just got Charter Internet. However they did say that they were sending me a new Modem for free, but mine was Broke. How I think it Broke was by setting it into Transparent Bridging Mode (my Internet just went out). If they say that they wont send you a new one because of the Error go set it in Transparent Bridging Mode. If you have a day or so you might want to try this.<br />
<br />
What Transparent Bridging Mode is, is I guess like a Super DMZ Mode.<br />
<br />
3. If your ISP is not Qwest Call your current ISP and ask them about the Error and see if they send you a new Modem for free, if not set it in Transparent Bridging Mode.<br />
<br />
4. Buy a new Modem&lt;----Check out your Local Computer Store(s).<br />
