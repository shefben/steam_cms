	<p><font face="verdana, arial, helvetica" size="2" >We've released a new version of Counter-Strike and the dedicated servers (Win32 and Linux).<br />
<br />
You don't need to exit Steam to get the update, it will automatically download when you launch Counter-Strike. Your dedicated servers should automatically update as well.<br />
<br />
There are a few items from yesterday's reverted release that will have to wait until next week. Here is the actual changelog for today's:<br />
<br />
Changes:<br />
- Bots now buy armor more often. <br />
- Added "BOT" to the scoreboard for players who are bots on the server. <br />
- Reduced entity count in Aztec to improve server performance. <br />
- Improved Bot navigation on Aztec. <br />
- Improved Bot navigation on Airstrip. <br />
<br />
Bug Fixes:<br />
- Fixed bug where radar would disappear during gameplay. <br />
- Fixed bug where tactical shield wasn't removed during a round restart. <br />
- Fixed friends and serverbrowser settings not being saved correctly if edited in-game. <br />
- Fixed bug where sounds weren't played properly when using Friends. <br />
- Fixed bug where progress bar would only show the first file you're downloading from a game server. <br />
- Fixed bug in keyboard settings list where you could scroll selection bar off the visible page using the arrow keys. <br />
- Fixed bug where the first mouse click wouldn't work after exiting the UI. <br />
- Fixed bug where the scoreboard would be incorrect when users were connecting. <br />
- Fixed bug where the M4A1 silencer animation wouldn't play the sound at the correct time.</font></p>
	
