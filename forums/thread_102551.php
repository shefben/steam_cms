	<font face="verdana,arial,helvetica" size="1" > <b>The TFC Bug List - Fix Please</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >This is a comprehensive list of TFC bugs that <b>NEED</b> fixing, some of these bugs are over 4 years old and its about time Valve fixed them instead of ignoring TFC.<br />
This list has been created by Whit.<br />
<br />
<br />
<b>Name:</b> Nail Gren &amp; MIRV Bug.<br />
<br />
<b>Explanation:</b> The nail bug is the chief problem. This particular bug is rather nasty and can be achieved by tossing a nail grenade upwards into a ceiling just before the first stage is activated. Upon beginning its routine, the nail grenade moves upwards by a little without checking to determine whether there is a solid surface overhead. As a consequence, it &#8220;burrows&#8221; into the ceiling or other surface and carries on its routine as normal. However, upon exploding, the detonation gains many times more explosive power than normal, both in terms of range and damage. The resultant bug can destroy level one and two sentries through a massive area of solid geometry. A good example of this bug in action can be seen with the maps well (a nail grenade in the cap point ceiling can destroy the entire flag room defence) and bases_r2 (a nail grenade in the ramp room ceiling can destroy the flag room defence).<br />
<br />
The MIRV bug operates in a similar fashion, except it is tougher to reproduce and less effective.<br />
<br />
<br />
<b>Name:</b> Lift bug.<br />
<br />
<b>Explanation:</b> You go up a lift and you just get stuck at the top &#8211; you can&#8217;t get of the lift. This normally happens towards the end of the map when a lift has been used a lot, the only known way to escape the &#8220;lift bug&#8221; is to jump-crouch constantly, but this isn&#8217;t a sure way to get off the lift as the lift may have descended by the time you have negated this effect.<br />
<br />
<br />
<b>Name:</b>Ghost Nail grenade bug..<br />
<br />
<b>Explanation:</b> The ghost nail grenade bug is a particularly annoying and less than consistent quirk. Sometimes, upon a nail grenade exploding after spinning on the ground as normal, one can tread upon the ground the nail grenade occupied, only to lose health to the long departed grenade&#8217;s &#8220;ghost&#8221;. This can occur over three seconds afterwards. The only noticeable property surrounding this bug is that when multiple nail grenades are thrown in the same spot it is much more likely to occur, but even then it is not a guaranteed occurrence. A double dose of ghost nail grenades can kill a fully stocked medic, it is infuriating at times!<br />
<br />
<br />
<b>Name:</b> Timeleft doesn&#8217;t display when dead.<br />
<br />
<b>Explanation:</b> Die, press your timeleft button and it won&#8217;t show the timeleft.<br />
<br />
<br />
<b>Name:</b> Gren entity bug.<br />
<br />
<b>Explanation:</b> This is an infuriating bug if I&#8217;ve ever witnessed one (particularly for the engineers out there). If a grenade lands under the sentry tripod, it assumes qualities similar to that of the nail grenade bug. The grenade multiplies in power and size causing huge damage. This bug also possesses the capacity to destroy a level three sentry gun with a single grenade. All an engineer can attempt to do is dismantle or detonate the sentry before the grenade detonates. In addition to this bug, grenades can become 'trapped' under other surfaces. For example, if a pillar is located one mapping unit from the floor leaving a miniscule gap (a practice frequently used to lower r_speeds by stopping csg cutting up the map so badly) a grenade can fit under it, despite the grenade's model being much larger. This then creates a very large blast in a similar manner to the sg/nailgren bug. I had to incur higher r_speeds on sm_reloaded because of this bug (since all pillars touch the floor, thus cutting the floor up).<br />
<br />
<br />
<b>Name:</b> _special<br />
<br />
<b>Explanation:</b> Special is a bug that has a great deal of applications and uses. I am not particularly sure about the way in which it works, but I have been informed it is something to do with an overloaded operator. Basically, special abuse allows the player to create scripts that loop indefinitely and without performance penalties (typical looping scripts are unworkable almost solely because wait statements are required, consequently &#8220;lagging&#8221; the player executing the script). With a special script under one&#8217;s control, a multitude of shady applications are available. Some examples of _special in action:<br />
&#8226; Perfect bunny hop jump timing whilst holding a key indefinitely<br />
&#8226; Switch press script employed mid-air on maps like openfire<br />
&#8226; Flag throw scripts to toss the flag on the first instance of touching it (very useful for maps like well if a sentry is operating in the flag room)<br />
&#8226; Fast pipe det script for the demoman<br />
&#8226; Assault cannon spinning script to help with jumping whilst maintaining the AC in a state of readiness to fire<br />
&#8226; Chopping indefinitely<br />
<br />
There are many more uses of _special (and no doubt some of which I am currently unaware).<br />
<br />
<br />
<b>Name:</b> Chopping<br />
<br />
<b>Explanation:</b> Chopping, as I&#8217;ve put it in the past, is bunnyhopping&#8217;s less intelligent, uglier and less entertaining cousin. Chopping is a name given to the act of repeatedly ducking and then releasing the duck to stand up once more. Because the valve solution to the bunnyhopping cap was to check whether the player is above the cap upon pressing the jump button (or similar), the chop hopping exploit works because there is no jump involved. The legs are pulled up into the body whilst crouching, so by looping the action multiple times in a second, the ground is not &#8220;touched&#8221; so to speak. Chopping is useful in places, but is not nearly as effective as bunnyhopping due to the fact that, when performing the action, one cannot turn much at all. Chopping also looks eminently stupid.<br />
<br />
<br />
<b>Name:</b> Fullupdate &#8211; Anti-Conc<br />
<br />
<b>Explanation:</b> The fullupdate anti-conc hack is something which can be explained better by someone in possession of the technical facts regarding it. My guess is that the client polls the server repeatedly using the fullupdate command, causing some kind of a processing request overrule the conc effect being called. It is easy to spot in first person demos due to entities flickering when the cheat is activated. Cheaters have also been known to use this hack in CS to avoid the white-out flashbang effect. (I can get hold of the script that enables this to happen if a member of valve wants to see it).<br />
<br />
<br />
<b>Name:</b> Death bug.<br />
<br />
<b>Explanation:</b> The death bug is a long-standing and highly amusing problem that presents itself at the most inopportune of times. Upon reaching a low value of health close to zero, the client seems to decide that the player has died and pushes the view down to the death style camera (parallel to the ground rather than perpendicular as normal). The server, however, knows better. As a result, the player can wander around with a tipped view and even &#8220;resurrect&#8221; themselves using a health pack or friendly medic. It is a rarity but a bug nonetheless.<br />
<br />
<br />
<b>Name:</b> Disappearing grenades.<br />
<br />
<b>Explanation:</b> The disappearing grenades bug is another old school remnant that really should have been taking care of three to four years ago. Still, it remains. One used to lose grenades when picking up the flag with a primed primary or secondary grenade; this was fixed. However, it is still very common to lose a grenade whilst priming and throwing the flag, especially if said grenade is the player&#8217;s last remaining grenade of that type (i.e. if one was to prime a frag grenade and throw the flag, it (the grenade) would be likely to disappear). Some players have also reported that capping with a grenade primed on many maps (well included) causes the primed grenade to disappear in a similar manner. I have not been able to reproduce this, though.<br />
<br />
<br />
<b>Name:</b> Respawn grenades.<br />
<br />
<b>Explanation:</b> This is another strange and rather inconsistent bug. The respawn grenade bug occurs when an unfortunate player has a grenade primed upon dying. When the respawn occurs, the grenade appears to travel with the player into the respawn and explodes causing damage to the player and his or her team mates.<br />
<br />
<br />
<b>Name:</b> Dud Concussion grenade.<br />
<br />
<b>Explanation:</b> It&#8217;s quite hard to reproduce this bug, but the faulty concussion grenade can be chiefly witnessed on maps like shutdown2. I repeatedly get this problem when defending the plank and an enemy offence player concussion jumps from below. The affected (conced) player must be on the very edge of the blast, because they are thrown high into the air, yet no concussion effect is administered.<br />
<br />
<br />
<b>Name:</b> Client Concussed Animation Anomalies.<br />
<br />
<b>Explanation:</b> This is a slightly confusing problem for those that do not fully understand the concussion effect and client decal system whilst concussed. When shocked, the player&#8217;s client decals appear under the crosshair every time a shot is fired. However, these decals are not real but can still trigger certain events. An example of this happens on the map well. A concussed player can shoot the well front door button causing it to light up and emanate the activated sound. However, no action is forthcoming as both of these properties were just triggered client-side, not server-side.</font></p>
	
