<br />
CPU: 866 Mhz Pentium III <br />
RAM: 256 MB PC-133 SDRAM <br />
VIDEO CARD: geForce 2 Ultra <br />
WINDOWS VERSION: Windows 98 SE <br />
DIRECT X: Direct X 9.0b <br />
VIDEO DRIVERS: nVidia 45.23 Reference Drivers <br />
VIDEO METHOD: OpenGL <br />
GAME RESOLUTION: 1024x768 <br />
GAME COLOR DEPTH: 32-bit <br />
V-SYNC: ON <br />
ADDITIONAL FEATURES: 4xAA <br />
INTERNET CONNECTION: Cable <br />
<br />
In CS 1.5, I got max FPS no matter what. My computer NEVER slowed down except when there were a large number of smoke grenades on screen. It was smooth, enjoyable gaming all the way. After installation of CS 1.6, my FPS droped substantially. It wouldn't matter what map I was on, some parts of the map would give me choppy FPS. Smoke grenades would render my FPS to unplayable levels. I would get major slowdowns whenever there were more then 5 people on screen or intense firefights. In some circumstances and on certain levels, my FPS would drop so low, it would make the game simply unplayable and I would close Counter-Strike. Here is what I have tried to remedy my FPS problem: <br />
<br />
Switched to a variety of resolution and color depth combinations <br />
Turned V-SYNC off <br />
Turned AA off <br />
Tweaked my video card settings in Windows <br />
Tweaked my BIOS settings <br />
MASSIVE TWEAKING IN THE CS CONSOLE WITH EVERY COMMAND <br />
<br />
After all these changes and tweaking, I got a very MINIMAL FPS increase, nothing substantial enough to warrant a "fix" or "remedy". <br />
<br />
As of now, I do not know if the FPS issue in CS 1.6 is isolated or widespread but, I can tell you this. I spent most of the day yesterday and today researching this topic on the internet, I have visited numerous CS sites and entered there forums. What I found was consistent, in every forum there was at least ONE AT THE VERY MINIMUM topic about FPS drops in 1.6. This leaves me to believe that this problem is affecting more people then most think. Now I know that my computer is on the lower end of the PC gaming spectrum, but I also know from reading forums and player responses in Counter-Strike, that people with much more powerful computers and video cards are experiencing FPS drops as well, sometimes severe. After careful thinking, I think I have come to a conclusion: <br />
<br />
The graphical updates (textures and models) that were implemented into the CS 1.6 engine are UNOPTIMIZED. <br />
<br />
I understand that VALVe is a very busy company, with the release of STEAM and CS 1.6 along with the development of Half-Life 2, they must have there hands tied. That is my most logical explanation as to why they did not have the time nor resources to fully optimize the CS engine. Hopfully this thread will get the attention of somebody in association with VALVe software, so they can come up with a possible fix in the near future. Here is what I want the people who care to do. If you are experiencing and FPS issues with CS 1.6, please list the following specifications and FPS problems you have: <br />
