INTERNET CONNECTION<br />
<br />
Like I mentioned in the beginning of the thread, there have been other posts started about this topic but, they were inconsistent or did not display the full information needed to correctly diagnose this problem.  I think with enough participation in this thread, we can get the attention of somebody associated with VALVe and hopfully get this issue solved.  Thank you guys for taking the time to read through this horribly long post, I just felt I needed to be specific.  Thanks again!<br />
