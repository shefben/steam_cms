<a href="/forums/showthread.php?s=&amp;threadid=11287" target="_blank">/forums/show...;threadid=11287</a><br />
<br />
where steam games switch my recording line (in my case, line-in) to microphone every time they are loaded/change maps. With a bit of poking around the windows mixer APIs, I made a tiny tool that should help people suffering from this problem, removing the need to alt-tab to switch your settings back. It is located here:<br />
<br />
<a href="http://fa.glassfish.net/mixerfixer.zip" target="_blank">http://fa.glassfish.net/mixerfixer.zip</a><br />
<br />
Basically what it does is reset the recording input device to a value you specify at a time interval (say every 30 seconds or so, but you can tell it to do it faster/slower).<br />
<br />
Note that I do not have an SB Live or any such other card. I have a Santa Cruz Turtle Beach. So if this does not work for you (and you are sure you are setting it correctly), please copy the full output of the program and e-mail it to me. It works for me just fine though.<br />
<br />
It was also a very quick utility to program, and looking at the dates on those threads, I'm surprised Valve hasn't fixed such a glaring issue after all these months. It really just takes a few lines of code once you have the mixer device open... I thought steam was supposed to <b>encourage</b> these kind of quick fixes?</font></p>
	
