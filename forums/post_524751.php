<a href="/forums/showthread.php?s=&amp;threadid=13208" target="_blank">/forums/show...;threadid=13208</a><br />
<a href="/forums/showthread.php?s=&amp;threadid=43849" target="_blank">/forums/show...;threadid=43849</a><br />
