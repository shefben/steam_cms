Hope You Have Patients Waiting<br />
<br />
-------------------------------------------------------------------<br />
<br />
Key features of the Official Counter-Strike Bot include:<br />
<br />
Selectable difficulty levels that provide a fun and exciting experience to everyone from first-time <br />
game players to seasoned veterans of the Counter-Strike online community. <br />
An automated learning system that allows the bots to play on almost any map without requiring<br />
human intervention. <br />
Extremely realistic and human-like behavior, resulting in opponents that feel "real" - a subtle,<br />
yet critical prerequisite for creating an exciting gaming experience.<br />
<br />
A unique "navigation mesh" system that provides a more robust representation of the<br />
"walkable" areas of the environment than is possible with traditional waypoints. <br />
Complex 3D navigation: Bots smoothly navigate the Counter-Strike world, jumping up on ledges, climbing ladders, crawling through ducts, breaking through windows, opening doors, and even <br />
jumping down from higher levels to surprise their enemies. <br />
Team tactics and cooperation. Terrorists work together to plant and guard the bomb, while <br />
Counter-Terrorists radio enemy locations and request backup. Bots also respond to radio <br />
commands issued by human players. <br />
The ability to play all of the Counter-Strike scenarios: Plant the Bomb, Rescue the Hostages, <br />
Escort the VIP, as well as good 'ol "deathmatch". <br />
Proficiency with the full Counter-Strike arsenal, including Smoke, Flashbang, and HE Grenades, knives, and all weapons. In addition, the bots understand how to use different classes of weapons to good effect (ie: knives vs. pistols vs. shotguns vs. rifles vs. sniper rifles)<br />
<br />
A human-like attention system: Bots can only see and hear what a real player would in the same<br />
situation . They check corners and hiding spots, "lead" their aim around corners, investigate noises, <br />
and in general act on incomplete information. This results in bots that can be surprised and even panic. <br />
A visual and auditory pipeline that delays incoming stimuli to simulate human reaction times. <br />
Smooth and realistically imperfect weapon aiming, based on each bot's individual skill level.</font></p>
	
