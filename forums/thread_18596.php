<br />
Due to the popularity of the Official Counter-Strike Bot, Valve and Turtle Rock Studios have been hard at work on a major enhancement involving them, which will be announced soon. <br />
<br />
The Official Counter-Strike Bots have learned a lot during their time away. We think you'll have even more fun with them when they return. <br />
<br />
<br />
