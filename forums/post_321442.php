<br />
If your server is listening on port 27015, the port forwarding rule in your router should forward UDP 27015 to the internal IP address of the server.  <br />
<br />
Typically, whatever port you are listening on is the port you need to forward. You really should be able to pick any 'high' port (above 1024 I think) and it should work ok.  <br />
<br />
You can add a server with an 'oddball' port in your Steam Favorites list just add ip/dns name:port.  (mine is tcsrats.game-host.org:27015, but any port that the server is listening on can replace the 27015)<br />
<br />
Why do you say your port is never 27015, are you picking a different port, or are you seeing other 'high' ports being used when you connect to external internet game servers?  When you are running behind a router you are making use of Network Address Translation.  <br />
<br />
Let me delve into NAT a little, I hope I don't bore anybody.<br />
<br />
The way NAT works is that the router is a gateway: on the outside is one external internet IP address, on the inside is a DHCP server to distribute IP address's to the internal client pc's.   (you can also choose to not make use of DHCP and assign a  static IP on each client)  <br />
<br />
I'm going to use a Half-Life connection as an example of how NAT routes packets.<br />
<br />
Lets say you have 2 pc's behind a router that want to play CS through the same external ip.  Both PC's are going to need to connect through port 27015 outbound to connect to the game server on the other end right?<br />
<br />
The router will need to be able to sort out the traffic as it echo's back gameplay to the client pc's behind the router.  The router cannot simply echo the traffic back on a single common port when passing through the router because it would only get passed to one destination.  That would obviously cause a problem because you need to both send, and receive data when playing a game.  <br />
<br />
The router will take care of these connections via the NAT mechanism.  Internally, the traffic will look normal, a conversation will take place between source 192.168.1.100:27005, and external server 206.156.254.48:27015. (these are only examples btw)  Externally, the router has to manage these concurrent sessions, so the router externally has a conversation with the destination server on a 'high' port designated by the router. I see my LinkSys router typically uses ports in the 43000's for the NAT'd traffic back to each pc.<br />
<br />
An example single conversation may look like this:<br />
<br />
Internal:<br />
<br />
192.168.1.100:27005 -&gt; 206.156.254.48:27015 (outbound packet)<br />
206.156.254.48:27015 -&gt; 192.168.1.100:27005 (inbound)<br />
<br />
(notice how this server knows how to talk to your internal, non-internet address?)<br />
<br />
The external conversation looks a little different due to the functionality of NAT<br />
<br />
External:<br />
 <br />
64.252.163.179:43620 -&gt; 206.156.254.48:27015 (outbound)<br />
206.156.254.48:27015 -&gt; 64.252.163.179:43620 (inbound)<br />
<br />
In reality those examples may reflect the same packet, the router through NAT takes care of moving the traffic back and forth through the gateway and getting it to the appropriate locations.<br />
<br />
<br />
The router is actually behaving as a switchboard operator in this sense making sure that the appropriate traffic moves back and forth through the gateway without a hitch.  (thank god these routers are doing all this work without us end-users having to intervene with additional configuration right? ;-P)<br />
<br />
NAT routing certainly makes life a lot easier since we can now take a single external internet IP and share the connection seamlessly among many PC's behind the gateway.  <br />
<br />
We, as server operators make use of port forwarding by telling the router to send all traffic on a specific port to a single destination through the gateway.(router)  In essence you are allowing users on the internet to look straight down the internet pipe to you, pass through the router as if it wasn't there, and have direct access to the PC behind it on that port.  This is why I always recommend limiting the amount of ports a user opens. What if you needed NAT to echo traffic back to a different PC behind the router, but the traffic always went to a single box?(maybe the wrong one)  This can cause strange connection problems, sometimes hard to diagnose if you don't know what to look for.  Limit the port forwarding rules to exactly what is needed, it will help the PC's maintain a higher level of security, and help avoid any technical issues by not misdirecting traffic to the wrong destination behind the router.<br />
<br />
Sorry for the long-winded tangent, I tried to keep the explanation simple, yet maybe help some people take away a better understanding of what exactly the router is doing for you.<br />
