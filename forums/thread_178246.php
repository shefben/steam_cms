	<p><font face="verdana, arial, helvetica" size="2" >For those of you especially who think the story is weak:<br />
<br />
*don't read unless you beat both, or don't care to spoil the ending.. you may want to read it when you finish hl2. There are no cliffs, so dont ask, and i dont care if you dont read it*<br />
<br />
As we all know, Gordon doesn't talk. This was a design feature, so that you know what he knows and he knows what you know. Especially in HL2 where you are dropped in the middle of a world with <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> going on you have no clue on. The only reason we even have reason to believe it is Earth, is because all your old friends are there, and no one mentions "traveling to another world." The thing I love about this story, is because (now I'm playing hl2 a second time) I notice things the second time around I missed completely, same with the first, and now that I'm play hl1 source, I'm noticing a few other things as well.<br />
<br />
Hl1 starts out with you in the infamous tram. As you near the destination, some information is revealed to you about Gordon. MIT Graduate in theoretical physics, your clearance, and your sponser, who is unknown. Weird as that may be, when HL2 starts, gman says "The right man in the wrong place can make a world of difference." and even though at the end of HL1 he says "I like a survivor, reminds me of myself." It SOUNDS like he didn't know you, but WHY do you see him all the time in both games? Following you. Not to mention near the end of HL2 the Administrator says something about how as a baby, you were given something (I can't remember right now) that explains why a mere scientist can blow up entire human/alien/combine armies, but still not completely clear.<br />
<br />
Anyway, the sponser wouldn't surpirse me at all, if it turned out to be Gman. He anonomously sponsers you to work at black mesa, cuz he KNOWS <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> is going to go down.<br />
<br />
Now, why were they trying to get aliens in other dimensions in the first place? Well in the first, you kill the Nihilath, which controls them all. Therefor leading them all leaderless. I would think the administrator wanted this, as in the second you see many vortgornauts (sp?) used as slaves. The dude with the broom right as you get off the train.<br />
<br />
When you get to black mesa east, in HL2, as you descend the elevator, there are Vort's cooking (your friends, clearly.. perhaps they like humans cuz you saved them from being slaves to the nihilath.) headcrabs. Then later, at ravenholm, you hear the priest say "I must tend to my flock." It may be possible, after a time, they brought over the aliens for food (Maybe something to do with humans using all their resources on earth, so we looked for alternatives). Also, clearly, they use them as weapons when "shelling" an area.<br />
<br />
As for the Gman, something tells me he knew you before Black Mesa. Something also tells me he is not only fully aware of what is going to happen, but also, perhaps he holds a greater purpose in this war.<br />
<br />
<a href="http://www.phenylshouse.com/members/ritalin/Pictures/HL2/d2_coast_030000.jpg" target="_blank">http://www.phenylshouse.com/members...oast_030000.jpg</a><br />
<br />
In that pic on the coast. You are looking through a combine binocular set up. Though it seems like an ordinary "Hey, the gman is there" situation. I zoomed in on that pic (try it yourself) he is talk to the colonol (the dude with the british accent who gives you the RPG) Check out the leet toque on his head. I'm sure it is him. So, then you drive over to that town and everyone says "We are expecting a Gunship attack." Now how the <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> do they know that? Could be a few ways, intercepted radio transmissions, or maybe the Gman said it. Maybe the Gman SOLD the RPG to them (Perhaps he makes profit on war. As he says at the end of the game, "I have numerous employers who want to buy you." or whatever.) Also, when going on the underground railway (in the canel) you drop down into a broken down rail cart, with a Vort and a Resistance member. If you are quick enough, you can see the Vort, by the TV, is watching the Gman... Why? <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> if I know.<br />
<br />
Anyway, when you are in nova prospekt and teleport out, and then, even though, your travel instantly to the Dr. Klieners Lab, he says it's been a week. I heard some moron on another board say "It's because they couldn't figure out how to start the rebellion, and wanted to get the game out." Well, maybe, but I figure it's to prove that the teleporters dont only transport you, but can go through time. Though, kliener says it's a "Slow teleport", perhaps it is an accelerated teleport. Maybe thats why gordon went straight from blackmesa, to city17 without anything in between, even though its been about 10 years.<br />
<br />
How do I know it has to be about 10 years? When you get to Eli Vance, in Black Mesa East, and you can check out a picture of him, his wife, and Alyx. In that pic, she looks about 8-10. (Maybe younger) and Eli says "Ahh, you remember my wife don't you? That was the only thing I could save from Black Mesa". Well clearly Alyx is no little girl anymore.. and is at LEAST 20, if not older. So yes, a fair bit of time has passed.<br />
<br />
As for the combine, what are they? I noticed while inside the citadel, there were "Mechanics" of some sort. Looked humanish, but with legs (shorter) like striders (little stick legs). The Administrator is also constantly talking about immortality and "Evolution". It SOUNDS like he is doing something to the captured humans to accelerate evolution. Being the combine? Could be.. I wouldn't doubt those Engineer's seen working on <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> in the Citadel, have their own Combine uniform.<br />
<br />
Now for the "Benefactor's". When you do the teleport at the beginning, the admin says "Who are you? How did you get here?" You teleport away. When you come back you see him talking on a Video thing, with .. something, saying "I Think it was Gordon Freeman" or whatever. Well, later in the game, when you sneak up on him, he is talking to that "thing" again. It looks alien, maybe a robot? I'm thinking when he says he is going to teleport to another "Universe" that is where he is going and that is who the "Benefactors" are, and they are the one's who gave him the citadel, and who gave him this power over everyone.<br />
<br />
As for all the people, you notice a few people you talk to mention "Scientists" and when you get to the "Sandtrap" chapter, one guy lies by his dead friend saying "Why? He was the finest mind of this age". They are capturing all the bright minds to use them to evolve to greater beings to create a master race? Maybe.<br />
<br />
Lastly, the Gman is always seen everywhere, and disapears. Even at dead ends, he more than likely teleports somewhere else. He aslo, as you see in the end, holds some serious power/technology. He <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font>ing stops time. Who knows what happens to alyx, I'm sure she is fine though. Right at the end, he pulls a "matrix" and steps out of the darkness through a door (like the hallways full of doors in the matrix, though cooler and less <font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font><font color="pink"><font face="symbol">&#169;</font></font> than reloaded.) I'm assuming he has some great technology at his disposle, for whoever his "empolyers" are, and whatever his purpose is and whatever his job actually is.<br />
<br />
Yeah, I'm done.<br />
<br />
Edit: I'd like to add the part at the Sand Trap level, he says "he was the finest mind of HIS generation" So  I wouldn't be surprised to find out that these teleporters do involve time travel and they took brilliant minds from all places and times on earth.</font></p>
	
