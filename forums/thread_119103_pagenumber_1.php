	<p><font face="verdana, arial, helvetica" size="2" >Counter-Strike: Source<br />
<br />
Gameplay changes.<br />
<br />
-] Boosting is back, you can boost just like in 1.6.<br />
-] A lot of things can be moved. For example in de_dust there are a lot of barrels lying around. A t/ct can be hiding behind one that has been moved to a spot you wont expect.<br />
-] If you fall from a high place, you cant start walking right away, for about 1-2 seconds you stay still.<br />
-] It seems like you jump higher and longer. I jumped from this car infront of ct spawn to the top part of ct spawn (where you plant) I also jumped on a few boxes that you werent able to in 1.6.<br />
-] Shadows are very helpfull. I was in tunnels faceing front side (as a t) I was awping, all of a sudden i see a shadow on the floor comeing up so i prepared my shot prefired and got the guy was awesome.<br />
-] You can pick up nades from people that have died, but you cant drop nades.<br />
-] When i tryed to bind stopsound it said it was a cheat and that i couldnt use it in multiplayer<br />
-] Shooting through walls is different. Walls that you were able to shoot through in Cs:1.6 you cant shoot through now. You can only shoot through walls that are very thin.<br />
<br />
Bunny Hopping/Strafe jumping<br />
<br />
-] Bunny Hopping is back, it even seems easier then 1.6.<br />
-] You can still do a 90 degree strafe jump <br />
<br />
Grenades/Smokes/Flashes.<br />
<br />
-] Grenades arent He-Grenades anymore they are Frag Grenades. If a nade blows up near you, you will go deaf and a ringing sound goes on. You also stop in your tracks and cant move anymore, for about 1-2 secs. Granades also push stuff away like barrels,  guns, bodies.<br />
-] Smokes look amazing now. They arent just a cloud of smoke anymore. The smoke fills up a lot more area, looks very real, and changes color depending on what light is hitting it. For example If you throw a smoke outside it looks gray you can see through it in some spots, but if you throw it in the tunnels it looks black/gray. Another thing i noticed was. When i was looking into the tunnels with smoke filling the inside, A gun fight broke out and the gun flashes look amazing in the smoke, lights flashing through the cracks in the smoke. Simply amazing. Another thing about smokes is all the smokes doesnt come out at once, like a explosion, its more like a gas leak.<br />
-] Flashes are different also. You cant duck flashes anymore, if a flash lands behind you, you still get flashed. The flash effect is crazy. The 1st thing that happens is your screen turns completely white. Then you start to see a snapshot of the last thing you saw puts it over the place your looking at now and a fog drag effect takes place. Looks like your high :P. If you manage to duck behind a wall just enough so that the flash bang itself isnt visible, its not simply a explosion like cs:1.6 its a burst of light. The flash also makes you go deaf if its close to you.<br />
-] All nades where able to go over certain walls in de_dust. If you throw the nade to high or at a place that a nade cant go past, it bounces back.<br />
-] All nades show up on the model when not in the players hands. (If you only have one flash, only one flash will be shown on the model)<br />
-] Nades dont do damage through walls<br />
<br />
Physics System.<br />
<br />
-] The physics system adds so much fun, eyecandy and new gameplay. Bodys lean on stairs correctly. When you die it looks great always different depending where you die. Barrels can be moved to spots people wont expect, tires can be pushed around (If you run into a tire the rubber pushes you back). A lot of other things are in the maps like bottles, carts, garbage etc. <br />
-] If barrels run into you they push you back.<br />
-] When you shoot a person in the head with the awp, the person that got shot flies back realistically.<br />
-] If you jump on a tire from a high spot you loss one to none hp.<br />
-] You can shoot away guns<br />
-] Nade explosions push guns on the floor<br />
<br />
Hitboxes/netcode.<br />
<br />
-] ex_interp is not a command anymore<br />
-] The hitboxes felt better, everything seemed to hit perfect and i mean perfect.<br />
-] The ping was fine same as cs1.6<br />
<br />
Game options/looks.<br />
<br />
-] There are a lot more video options/windows are light gray see through. (I like the new look a lot) It shows what directx your card is and what directx you have installed.<br />
-] Model detail<br />
-] Antialiasing mode<br />
-] Texture detail<br />
-] Filtering mode<br />
-] Water detail<br />
-] Shader detail (no options yet)<br />
-] Shadow detail<br />
-] Wait for vertical sync (on or off)<br />
-] You can only bind one key in the options menu<br />
-] The overview of maps look a lot better. You can zoom in and out. You can also make it so these lines show the path of the players.<br />
-] You can bind a key to take jpegs. (bind "f6" jpeg)<br />
-] You can also adjust the quality by typeing jpeg_quality 0-100 (50 default)<br />
<br />
<br />
Console <br />
<br />
-] Console looks a lot neater<br />
-] Typing status in console will now give you a lot more information. It shows the true ping/ms, name, time, in/out kbs, build, and drop for each player separately.<br />
<br />
Guns/recoil/options.<br />
<br />
-] Awp delay seems shorter. The awp zoom is also really cool now. The glass is alittle dirty while being zoomed in. There is no more red dot its just 2 lines forming a cross. (I liked this  a lot) While unzoomed the glass on the scope reflects whats behind you. While zooming in with the awp you actually see the zoom in its not like a instant zoom.<br />
-] You can still quick shoot with the awp (tested it again and was able to)<br />
-] The recoil seemed a little different for all guns.<br />
-] If you get shot it seemed like your accuracy got a little lower then what it did in Cs:1.6<br />
-] The duals seemed to be a lot better lol. I got 2 kills 1 being completely flashed Headshot.<br />
-] The zoom for the sig/aug wasnt instant, you see the zoom like the awp.<br />
<br />
Gun models/Character models.<br />
<br />
-] The gun models look really good. A lot more detailed.<br />
-] The player models also looked very detailed.<br />
-] When looking at someone who runs with a knife it looked insanely detailed.<br />
-] When switching teams the dead players model doesnt change <br />
-] If you have the usp silenced and take the silencer off, the silencer doesnt come off in 3rd person. <br />
-] When you have the silencer on no flash comes out of the gun in 3rd person.<br />
-] When you dont have the silencer on a flash appears from the end of the gun<br />
<br />
Sounds<br />
<br />
-] When getting shot players make "hurting" sounds<br />
-] Some guns (m4, para, nades) sound a lot better/real<br />
-] The map extra sounds add to the feel of the game and environment<br />
<br />
Map detail.<br />
<br />
-] The Cs:s version of dust is wow amazing. The box, car, barrel placement is perfect. The extra places you can go help a lot. The map looks a lot more realistic, looks like a real place in real life. The small details they added, add to the eyecandy. Simply looks amazing.<br />
<br />
Extra.<br />
<br />
-] When you get a headshot the icon is new and looks cool<br />
-] You can enable console and hud fast switch in game options now <br />
-] You can stand on movable objects like barrels<br />
-] You can still shoot though walls<br />
-] The real time bomb timer wasn't in Cs:s<br />
-] When you shoot the sky debris and sparks dont come out<br />
-] When the bomb blows up, with you standing looking at it you fly back realistically<br />
-] When the bomb is dropped you cant move it<br />
-] The text on the bottom of the screen now fades away <br />
-] Alt Tabing the beta will make it crash almost 75% of the time for me (While i was taking notes it crashed about 12 times)<br />
-] When you make a listen server your ping is shown as 5 and not 0<br />
-] You can put html and chinese characters in your name now.<br />
-] If you type the % symbol in public chat it now appears<br />
-] You can now use capslock in the main chat<br />
-] You can see the numbers getting decoded on the bomb while defusing.<br />
-] cl_radartype 1 is lighter in Cs:s <a href="http://img3.exs.cx/img3/4705/Radartype.png" target="_blank">http://img3.exs.cx/img3/4705/Radartype.png</a><br />
<br />
Cs:s is simply amazing. Even tho its a beta it plays very well.<br />
<br />
If you have any questions plz post and ill try to answer them. I might have missed some stuff/forgot so if you remember something plz post.<br />
