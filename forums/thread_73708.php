For ALL OS's:<br />
<br />
The most important thing to remember about dial up is that the maximum you can download ANYTHING under absolute perfect conditions is 6.7kb/s, so don't be frustrated with your speed, remember it is only 56K (actually 53333). To understand a little better, most dial up connections have a connect speed of somewhere in the mid 40K range, which is very good, so say you connect at 48000, to find your maximum throughput divide that by 8 which gives you 6kb/s, which actually very good for dial up. The tweaks I have provided will help you maximize that throughput as best possible, don't expect super speeds by tweaking your dial up, be happy if you are getting 6kb/s. <br />
