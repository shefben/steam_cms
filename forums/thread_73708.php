<br />
 HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Contro<br />
l\PriorityControl (if PriorityControl is not there, make the value) - on the right hand side right click and select new DWORD value, name it IrqXPriority (X = Irq assigned to your modem), then right click it and select modify and give it a decimal value of 1. <br />
