<b>56k Tweaks For All OS's <br />
<br />
--------------------------------------------------------------------------------<br />
<br />
I've seen a number of people asking for some 56k tweaks lately and thought this might help out.<br />
<br />
For 98 &amp; ME:<br />
<br />
1. First and most important make sure you have the latest drivers for your modem, from the manufacturer.<br />
<br />
2. Open modem properties in Control Panel and under advanced port settings slide both all the way to the right.<br />
<br />
3. Set maximum port speed to 115,2000.<br />
<br />
4. Open Internet Options in IE and under temp internet files - settings select - Automatically.<br />
<br />
5. Under extra settings (where init strings go) put in s11=55 to make your modem dial faster or s10=60 if you have frequent disconnects.<br />
<br />
6. Make sure you have a good "clean" phone line, the #1 reason 56K modems don't connect at a good transfer rate is static on your phone line, and remember your throughput is more important than what your connection says it is connected at. If you are connected between 44,000 - 50,000 that is as good as it gets (you are limited to 53,333 by the FCC)<br />
<br />
7. Download TCPOptimizer and put in the following settings:<br />
<br />
Under the "settings" tab select "dial up" and "custom settings", in "network adapter selection" choose "dial up adapter". Then use the following settings:<br />
<br />
MaxMTU - 1500<br />
TCP Receive Window - 8760<br />
MTU Discovery - Yes<br />
Black Hole Detect - No<br />
Selective ACKs - Yes<br />
Max Dup ACKs - 2<br />
Time to Live (TTL) - 128<br />
TCP 1323 Options - uncheck both boxes<br />
<br />
"Other settings" tab:<br />
Max Connections per Server - 4<br />
Max Connections per 1.0 Server - 8<br />
<br />
Then select "Apply changes" and reboot to take effect.<br />
<br />
8. Open your registry (start - run - type regedit) and change the following registry entries:<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\Class\Net\000X (where X is a number between 1 and 9)there is a value named SLOWNET, change it from 01 to 00.<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\VxD\COMBUFF it has a value called Start, change its value from 00 to 01.<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\VxD\VCACHE, it has a value named Start, change it from 00 to 01.<br />
<br />
9. Open your sys.ini file (start - run -sysedit - ME type system.ini) close the boxes until you are at the one with the title C:\WINDOWS\SYSTEM.INI, under [386enh] add the line ComXXIrqXXbuffer=4096, XX=Com port your modem is on and Irq assigned to your modem, it should look like Com03Irq07buffer=4096. Then select file - save and reboot to take effect.<br />
<br />
<br />
For 2K &amp; XP:<br />
<br />
For 2K &amp; XP users I suggest using Cablenut to tweak your settings due to the fact it installs the AFD parameters and allows you to tweak them. These values utilize memory for buffers that help speed up and stabilize your connection and latency. The following ae my suggestions for settings to use in Cablenut:<br />
<br />
DefaultReceiveWindow = 8192 <br />
DefaultSendWindow = 4096 <br />
DisableAddressSharing = 1 <br />
InitialLargeBufferCount = 10 <br />
InitialMediumBufferCount = 24 <br />
InitialSmallBufferCount = 32 <br />
LargeBufferSize = 4096 <br />
MaxFastTransmit = 6400 <br />
MediumBufferSize = 1504 <br />
PriorityBoost = 0 <br />
SmallBufferSize = 128 <br />
TransmitWorker = 32 <br />
FastSendDatagramThreshold = 1024 <br />
EnableFastRouteLookup = 1 <br />
EnablePMTUDiscovery = 1 <br />
IgnorePushBitOnReceives = 0 <br />
GlobalMaxTcpWindowSize = 8760 <br />
MaxFreeTcbs = 2000 <br />
MaxHashTableSize = 4096 <br />
MaxNormLookupMemory = 5000000 <br />
SackOpts = 1 <br />
SynAttackProtect = 1 <br />
Tcp1323Opts = 0 <br />
TcpLogLevel = 1 <br />
MaxDupAcks = 2 <br />
TcpMaxHalfOpen = 100 <br />
TcpMaxHalfOpenRetried = 80 <br />
TcpRecvSegmentSize = 1460 <br />
TcpSendSegmentSize = 1460 <br />
TcpTimedWaitDelay = 30 <br />
TcpUseRFC1122UrgentPointer = 0 <br />
TcpWindowSize = 8760 <br />
MaxConnectionsPer1_0Server = 8 <br />
MaxConnectionsPerServer = 4 <br />
DefaultTTL = 128 <br />
DisableUserTOSSetting = 0 <br />
TcpMaxDataRetransmissions = 6 <br />
DefaultTOSValue = 92<br />
<br />
Also add the following entry to your registry:<br />
<br />
 HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Servic<br />
es\AFD\Parameters <br />
<br />
Add the parameter BufferMultiplier and give it a value of 400 hexidecimal which will make it 1024.<br />
