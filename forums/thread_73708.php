	<p><font face="verdana, arial, helvetica" size="2" >56k Tweaks For All OS's <br />
<br />
--------------------------------------------------------------------------------<br />
<br />
I've seen a number of people asking for some 56k tweaks lately and thought this might help out.<br />
<br />
For 98 &amp; ME:<br />
<br />
1. First and most important make sure you have the latest drivers for your modem, from the manufacturer.<br />
<br />
2. Open modem properties in Control Panel and under advanced port settings slide both all the way to the right.<br />
<br />
3. Set maximum port speed to 115,2000.<br />
<br />
4. Open Internet Options in IE and under temp internet files - settings select - Automatically.<br />
<br />
5. Under extra settings (where init strings go) put in s11=55 to make your modem dial faster or s10=60 if you have frequent disconnects.<br />
<br />
6. Make sure you have a good "clean" phone line, the #1 reason 56K modems don't connect at a good transfer rate is static on your phone line, and remember your throughput is more important than what your connection says it is connected at. If you are connected between 44,000 - 50,000 that is as good as it gets (you are limited to 53,333 by the FCC)<br />
<br />
7. Download TCPOptimizer and put in the following settings:<br />
<br />
Under the "settings" tab select "dial up" and "custom settings", in "network adapter selection" choose "dial up adapter". Then use the following settings:<br />
<br />
MaxMTU - 1500<br />
TCP Receive Window - 8760<br />
MTU Discovery - Yes<br />
Black Hole Detect - No<br />
Selective ACKs - Yes<br />
Max Dup ACKs - 2<br />
Time to Live (TTL) - 128<br />
TCP 1323 Options - uncheck both boxes<br />
<br />
"Other settings" tab:<br />
Max Connections per Server - 4<br />
Max Connections per 1.0 Server - 8<br />
<br />
Then select "Apply changes" and reboot to take effect.<br />
<br />
8. Open your registry (start - run - type regedit) and change the following registry entries:<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\Class\Net\000X (where X is a number between 1 and 9)there is a value named SLOWNET, change it from 01 to 00.<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\VxD\COMBUFF it has a value called Start, change its value from 00 to 01.<br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\VxD\VCACHE, it has a value named Start, change it from 00 to 01.<br />
<br />
9. Open your sys.ini file (start - run -sysedit - ME type system.ini) close the boxes until you are at the one with the title C:\WINDOWS\SYSTEM.INI, under [386enh] add the line ComXXIrqXXbuffer=4096, XX=Com port your modem is on and Irq assigned to your modem, it should look like Com03Irq07buffer=4096. Then select file - save and reboot to take effect.<br />
<br />
<br />
For 2K &amp; XP:<br />
<br />
For 2K &amp; XP users I suggest using Cablenut to tweak your settings due to the fact it installs the AFD parameters and allows you to tweak them. These values utilize memory for buffers that help speed up and stabilize your connection and latency. The following ae my suggestions for settings to use in Cablenut:<br />
<br />
DefaultReceiveWindow = 8192 <br />
DefaultSendWindow = 4096 <br />
DisableAddressSharing = 1 <br />
InitialLargeBufferCount = 10 <br />
InitialMediumBufferCount = 24 <br />
InitialSmallBufferCount = 32 <br />
LargeBufferSize = 4096 <br />
MaxFastTransmit = 6400 <br />
MediumBufferSize = 1504 <br />
PriorityBoost = 0 <br />
SmallBufferSize = 128 <br />
TransmitWorker = 32 <br />
FastSendDatagramThreshold = 1024 <br />
EnableFastRouteLookup = 1 <br />
EnablePMTUDiscovery = 1 <br />
IgnorePushBitOnReceives = 0 <br />
GlobalMaxTcpWindowSize = 8760 <br />
MaxFreeTcbs = 2000 <br />
MaxHashTableSize = 4096 <br />
MaxNormLookupMemory = 5000000 <br />
SackOpts = 1 <br />
SynAttackProtect = 1 <br />
Tcp1323Opts = 0 <br />
TcpLogLevel = 1 <br />
MaxDupAcks = 2 <br />
TcpMaxHalfOpen = 100 <br />
TcpMaxHalfOpenRetried = 80 <br />
TcpRecvSegmentSize = 1460 <br />
TcpSendSegmentSize = 1460 <br />
TcpTimedWaitDelay = 30 <br />
TcpUseRFC1122UrgentPointer = 0 <br />
TcpWindowSize = 8760 <br />
MaxConnectionsPer1_0Server = 8 <br />
MaxConnectionsPerServer = 4 <br />
DefaultTTL = 128 <br />
DisableUserTOSSetting = 0 <br />
TcpMaxDataRetransmissions = 6 <br />
DefaultTOSValue = 92<br />
<br />
Also add the following entry to your registry:<br />
<br />
 HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Servic<br />
es\AFD\Parameters <br />
<br />
Add the parameter BufferMultiplier and give it a value of 400 hexidecimal which will make it 1024.<br />
<br />
<br />
1. First and most important make sure you have the latest drivers for your modem, not Microsoft's, but the one's from the manufacturer.<br />
<br />
2. Open modem properties in Control Panel and under advanced port settings slide both all the way to the right.<br />
<br />
3. Set maximum port speed to 115,2000.<br />
<br />
4. Under extra settings (where init strings go) put in s11=55.<br />
<br />
5. Make sure you have a good "clean" phone line, the #1 reason 56K modems don't connect at a good transfer rate is static on your phone line, and remember your throughput is more important than what your connection says it is connected at. If you are connected between 44,000 - 50,000 that is as good as it gets (you are limited to 53,333 by the FCC)<br />
<br />
6. Download TCPOptimizer and put in the following settings:<br />
<br />
Under the "settings" tab select "dial up" and "custom settings", in "network adapter selection" choose "dial up adapter". Then use the following settings:<br />
<br />
MaxMTU - 1500<br />
TCP Receive Window - 8760<br />
MTU Discovery - Yes<br />
Black Hole Detect - No<br />
Selective ACKs - Yes<br />
Max Dup ACKs - 2<br />
Time to Live (TTL) - 128<br />
TCP 1323 Options - uncheck both boxes<br />
<br />
"Other settings" tab:<br />
Max Connections per Server - 4<br />
Max Connections per 1.0 Server - 8<br />
<br />
Then select "Apply changes" and reboot to take effect.<br />
Leave all other boxes blank, select save to registry and reboot.<br />
<br />
7. Open Internet Options (IE)and under temp internet files - settings select - Every visit to page.<br />
<br />
8. Open your Network Connections from the Control Panel – right click on your Cable connection and select properties, if you have the “QoS Packet Scheduler” installed highlight it and select uninstall. <br />
<br />
9. Open up regedit, navigate to HKEY_LOCAL_MACHINE/Software/Microsoft/Windows/Current Version/Explorer/RemoteComputer/NameSpace, find the key named &#123;D6277990-4C6A-11CF-8D87-00AA0060F5BF}. Right click on it and delete it. <br />
<br />
Also add the following entry:<br />
<br />
 HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Contro<br />
l\PriorityControl (if PriorityControl is not there, make the value) - on the right hand side right click and select new DWORD value, name it IrqXPriority (X = Irq assigned to your modem), then right click it and select modify and give it a decimal value of 1. <br />
<br />
<br />
For ALL OS's:<br />
<br />
The most important thing to remember about dial up is that the maximum you can download ANYTHING under absolute perfect conditions is 6.7kb/s, so don't be frustrated with your speed, remember it is only 56K (actually 53333). To understand a little better, most dial up connections have a connect speed of somewhere in the mid 40K range, which is very good, so say you connect at 48000, to find your maximum throughput divide that by 8 which gives you 6kb/s, which actually very good for dial up. The tweaks I have provided will help you maximize that throughput as best possible, don't expect super speeds by tweaking your dial up, be happy if you are getting 6kb/s. <br />
<br />
FOR FASTER WEB PAGE LOADING: <br />
<br />
This tweak will help web pages load faster, it has no real effect on download speeds, just helps your pc look up websites faster thereby speeding up how fast the page loads and making websurfing more enjoyable. <br />
<br />
Navigate to this registry entry and change the following settings: <br />
<br />
For XP &amp; 2K <br />
<br />
 HKEY_LOCAL_MACHINE\SYSTEM\CurrentControlSet\Servic<br />
es\Tcpip\ServiceProv ider <br />
<br />
For 98, 98SE &amp; ME <br />
<br />
 HKEY_LOCAL_MACHINE\System\CurrentControlSet\Servic<br />
es\VxD\MSTCP\Service Provider <br />
<br />
<br />
On the right for ALL OS's change these entries: (ALL values are HEXIDECIMAL) <br />
<br />
Class - 1<br />
DnsPriority - 1 <br />
HostsPriority - 1 <br />
LocalPriority - 1 <br />
NetbtPriority - 1 <br />
<br />
Write down the current values PRIOR to changing them in case it doesn't work so you can revert back. To change the value right mouse click on the value and select modify and enter the values above once you have done all of them reboot to take effect and see how fast your pages load. <br />
<br />
<br />
P.S. If you have a dial up connection I HIGHLY recommend using a download manager, it will help maximize your download speed, resume downloads if you are disconnected and you can even schedule downloads for the middle of the night (it will automatically dial up and download the desired file and hang up when done) when you aren't using your pc.<br />
