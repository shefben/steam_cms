	<p><font face="verdana, arial, helvetica" size="2" >V2.3<br />
<br />
<b>Q</b>-How come there are only two player models?!<br />
<b>A</b>-VALVe has decided to wait for an update to give us more player models. Expect the other ones in an update soon.<br />
<br />
<b>Q</b>-How come when I try to add a bot into my server it says that they are only available in the beta?<br />
<b>A</b>-VALVe has not yet enabeled the bots and forgot to remove the beta message. Expect bots in a future update.<br />
<br />
<b>Q</b>-What is up with the hostages? They are HUGE!!<br />
<b>A</b>-Yea, they appear to be huge. Through first and third-person view. We can only hope that they are scaled down in an update in the future.<br />
<br />
<b>Q</b>-I got hit from 2 feet behind me! Whats up with that?<br />
<b>A</b>-The hitboxes in CS:S are somewhat laggy. Hopefully, they will be updated or changed in a future version. For now, try to play on a server with a low ping.<br />
<br />
<b>Q</b>-Where are the maps such as militia and prodigy!<br />
<b>A</b>-Sadly, VALVe only re-made a couple of the classic maps. They might be working on remaking other ones, but now that the official Hammer is released, expect them to be remade by users.<br />
<br />
<b>Q</b>-My game freezes/crashes/locks up! What should I do?!?<br />
<b>A</b>-Don't worry, you are not alone here. Use the <a href="/forums/search.php" target="_blank">Search</a> feature to search for a thread already created about that problem.<br />
<br />
<b>Q</b>-My FPS is HORRIBLE! What can I do?<br />
<b>A</b>-<a href="/forums/search.php" target="_blank">Search</a> the forums with your video card name and hope someone found a way to increase FPS already. If not, try changing all the advanced video settings.<br />
<br />
<b>Q</b>-Where is my CD-Key! Did I get scammed?!<br />
<b>A</b>-No, you did not. When purchasing games off of Steam you are not given a CD-Key, but rather, the game is "tied" to your Steam account.<br />
<br />
<b>Q</b>-How come I bounce off tables and chairs when I walk into them?<br />
<b>A</b>-This might be a bug, or it could be intentional on VALVe's part, but either way, there is one thing you can do. If you run a server, you can change these CVARs in console:<br />
<i>sv_pushaway_force<br />
sv_pushaway_max_force</i><br />
<br />
<b>Q</b>-I see people saying they are using a cracked version of CS:S! Is this true!?<br />
<b>A</b>-Untill VALVe implements VAC (Valve Anti-Cheat) into CS:S, yes. A future update may prevent these cracked versions from working though.<br />
<br />
<b>Q</b>-I have an ATi coupon. How to I redeem/upgrade it?<br />
<b>A</b>-To redeem it, open the Browse Games window in Steam. Then, click the "Register a Product I Already Own" button on the bottom of the screen. Enter in the CD-Key behind the part you had to scratch off and you will get the Bronze HL2 package. If you wish to upgrade to Silver or Gold, open the Browse Games window and click on the "Purchase This Package" for the Silver or Gold packages. Your $49.99 will be deducted automatically from the total price.<br />
<br />
<b>Q</b>-How come when I add lines to config.cfg they are overwritten when I start Steam?<br />
<b>A</b>-Steam overwrites the config.cfg file each time it is started, to execute your own commands at start-up of CS:S, make a file called autoexec.cfg with the commands you want CS:S to execute at startup and put it in the same folder as the config.cfg.<br />
<br />
<b>Q</b>-Odd...I can't seem to push the table in cs_italy along with other objects?<br />
<b>A</b>-The physics in the game have been changed from what was seen in certain videos a while ago. At the moment, it is not possible to push some objects with your body, but they may be moveable by shooting them.<br />
<br />
<b>Q</b>-How come I am getting long lag freezes even though I have cable/dsl/T1?<br />
<b>A</b>-This is most likely caused by background applications using the internet, such as firewalls or spyware. Try running a spyware scanner or configuring your firewall to allow Steam ports.<br />
<br />
<b>Q</b>-What DirectX level should I run my game at?<br />
<b>A</b>-Directly from Gary McTaggart:<br />
DirectX 7.0 - RADEON 7200, GeForce2 MX, GeForce4 MX<br />
DirectX 8.0 - GeForce3, GeForce4 Ti, GeForce FX 5200<br />
DirectX 8.1 - RADEON 8500, RADEON 9200, GeForce FX 5700, GeForce FX 5950<br />
DirectX 9.0 - RADEON 9500 and up, RADEON X800, GeForce 6800<br />
<br />
<b>Q</b>-What visual difference is there between DirectX modes?<br />
<b>A</b>-DirectX 7.0: No model decals, no detail props, no refective water, no bumpmaps, reduced model level of detail<br />
DirectX 8.0: No local specular on water, no water blending into land, shadows are not as soft<br />
DirectX 8.1: No local reflections on water, no water blending into land, soft shadows<br />
DirectX 9.0: Water reflections, water refraction, smooth shorelines, soft shadows, blended bumpmaps<br />
<br />
<b>Q</b>-How can I disable the VGUI menus and go back to the old school buy menus?<br />
<b>A</b>-Using the "old school" text buy menus is not possible in CS:S.<br />
<br />
<b>Q</b>-How are other people using arabic and other symbols in their names?<br />
<b>A</b>-They are using the extended character set. To use these symbols, open the Character Map (Start&gt;Run... "charmap"), and select Courier New as the font. Then, just double click on the symbols you would like to use and copy them from the box on the bottom and paste them in CS:S.<br />
<br />
<b>Q</b>-How come I can't see servers in the server list?<br />
<b>A</b>-First, try making sure you are on the internet tab, then under filters, make sure Anti-Cheat is set to All.<br />
<br />
<b>Q</b>-My console doesn't work.<br />
<b>A</b>-First check that you have it enabled. Go into <i>Options -&gt; Keyboard -&gt; Advanced -&gt; </i> and check Enable Developers Console. Then use the ` key to open the console.<br />
<br />
<b>Q</b>-When I bring up the console with ` and type something the console disappears.<br />
<b>A</b>-This can happen if you have a non-us keyboard layout/input configued in windows. One possibility is after ` type a T, the console will stay open and you can enter commands. Or, you can set your language in windows regional settings to US - english.<br />
<br />
<b>Q</b>-How do I change DirectX levels?<br />
<b>A</b>-In your autoexec.cfg put:<br />
"mat_dxlevel 70" for DirectX 7.0<br />
"mat_dxlevel 80" for DirectX 8.0<br />
"mat_dxlevel 81" for DirectX 8.1<br />
"mat_dxlevel 90" for DirectX 9.0<br />
Or in your Launch Options put:<br />
"-dxlevel 70" for DirectX 7.0<br />
"-dxlevel 80" for DirectX 8.0<br />
"-dxlevel 81" for DirectX 8.1<br />
"-dxlevel 90" for DirectX 9.0<br />
<br />
<b>Q</b>-What should I set my rates to?<br />
<b>A</b>-Here are some good rates/guildlines:<br />
rate = 10% of your download speed<br />
cl_cmdrate = 100 is a good one for broadband<br />
cl_updaterate = 30 is also good for broadband<br />
<br />
<b>Q</b>-I get crackling sounds, especially in confined areas like hallways and tunnels. I even have a high end sound card (i.e. Audigy) it happens. How to fix it?<br />
<b>A</b>-In c:\Program Files\Valve\Steam\SteamApps\youremail\counter-strike source beta\cstrike there are some ".cache" files in there. Delete the modelsounds.cache and scene.cache files. CS:S will rebuild these when it is started agian.<br />
<br />
<b>Q</b>-How come I have pink/purple all over everything?<br />
<b>A</b>-This is most likely caused by having your AA or AF up too high. Try turning them down to a lower setting.<br />
<br />
If your answer is not in here, remember to use <a href="/forums/search.php" target="_blank">Search</a> before creating a new thread!</font></p>
	
