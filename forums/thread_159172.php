<br />
L 10/16/2004 - 22:35:23: "&lt;attacker name&gt;" connected, address "&lt;address&gt;:27005"<br />
<br />
1. Take the &lt;address&gt; field and go to <a href="http://www.arin.net" target="_blank">http://www.arin.net</a><br />
2. Click on WHOIS Search<br />
3. Enter the IP address and hit the search button<br />
4. This will show the ISP and owner of the IP address space<br />
5. In some cases you'll have to click again on a specific IP address block.<br />
6. There will be contact info, usually an abuse email address.<br />
7. Structure your email like the following (will vary by ISP):<br />
<br />
date - 10/16/2004<br />
time -  11:05:19<br />
timezone - Central<br />
source IP address - &lt;attacker IP&gt;<br />
destination IP address - &lt;your server IP&gt;<br />
sending port - 27005<br />
receiving port - 27015<br />
type of protocol - UDP<br />
frequency -  once <br />
specifics of the attack if applicable - The username is question is &lt;attacker name&gt; connecting from IP address &lt;address&gt; which is leased from Classic Net Communications (verified via ARIN). The user initiates the attack by &lt;attack details, removed for obvious reasons&gt;. The user in question knows that what they are doing will cause users to be "owned" because this is an active attack meaning the user has to issue a series of commands. The user also says (which shows intent and knowledge): "man, this server is full" and "perhaps we should clear it out". The attack then causes all attached user machines to crash and the server itself to "lag out" requiring a restart of the daemon. The user reconnected approximately 40 minutes later and repeated this exploit. Logs available on request.  Thank you for your immediate attention to this matter.  <br />
