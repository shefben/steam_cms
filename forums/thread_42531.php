Fixed being able to jumpshoot if the minimap was open fullscreen<br />
Fixed a cut off label in scoreboard<br />
Now cannot +use grenades while deployed<br />
"hud_draw 0" now hides the spectator bar<br />
Fixed player animation being 90 degrees off on mg sandbag deploy<br />
Fixed spectator angles on minimap player icons<br />
Fixed strange health numbers drawing in the spec bar<br />
Fixed gunshot decal on subsequent bullet hits<br />
Added control point name to log file cap messages<br />
Fixed hud reset on stop demo recording<br />
Fixed ammo on mg42 and 30cal view model not showing above 8 bullets<br />
Fixed player models jittering because of animation blend<br />
Fixed an evil evil hack that stopped the player from shooting when their mg42 overheated<br />
Fixed some more empty cells in the scoreboard showing up as squares<br />
<br />
