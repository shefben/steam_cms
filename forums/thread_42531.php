Added timeleft and number of hltv spectators to the spec bar<br />
Fixed weather effects not drawing<br />
No vgui menus in hltv spec<br />
<br />
<b>COUNTER-STRIKE:</b> <br />
Fixed shield exploit that involved dropping a weapon and buying a pistol in a specific order; you must restart your dedicated server to pick up this change<br />
<br />
