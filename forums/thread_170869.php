	<p><font face="verdana, arial, helvetica" size="2" ><font color="yellow"><b>Some console commands you'll want to use.</b></font><br />
<br />
There are a number of new and useful console commands you'll want to use.  I'm just typing these up off the top of my head, so I may be off on a few.<br />
<br />
<b>+showbudget</b><br />
Bind some key to this.  This is your new "r_speeds" display.  I have it bound to "mouse4" (side button) on my mouse.  This shows you a bar chart breakdown of exactly where the engine is spending it's time drawing each frame.  Use it to decide how efficient your map is, and also to decide where to spend time trying to improve it's performance.  If you see "world brush" as taking up too much time, you know you need to simplify or modify your brushwork.  If it's prop models, you know you need to remove or simplify your prop models, etc.<br />
<br />
<b>mat_leafvis 1</b>  <br />
Draws a box around the vis leaf you are currently in.    Useful for hard core vis reduction work.<br />
<br />
<b>mat_wireframe 1</b> <br />
Draws wireframe lines on everything - great to show you just what is being drawn at times.<br />
<br />
<b>mat_specular 0</b> <br />
Turns off specular reflection stuff.  Great to see if that is what is killing your performance.  If you turn it off and your map runs smooth, maybe you've got too much specular stuff in the map?<br />
<br />
<b>mat_bumpmap 0]</b> <br />
Turns off bumpmap display.  Useful like specular switch above<br />
<br />
<b>find</b> <br />
Coolest comand of all!  Type "find " followed by what you want to find and it will dump out the console comamnds matching.  Do like "find mat" and you'll get all the commands with "mat" in the name.  Also you get short help text for some.</font></p>
	
