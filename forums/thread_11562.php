	<font face="verdana,arial,helvetica" size="1" > <b>Steam Status</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The Steam beta is now drawing to an end. This means that there are some significant changes coming to the way that Steam will be run.<br />
<br />
The next major release of Steam will be the first step towards converting the entire Half-Life user base to Steam. This means that we will be resetting the Steam account database from scratch. After this, you will need to have a valid CD-Key to access games via Steam, so you will want to treat your Steam account as permanent. One CD-Key will grant you access to all of the games currently available in the Steam beta. The Friends database will remain intact, so if you create an account with the same name when the beta ends, you will retain your Friends list.<br />
<br />
The next step will be to release the remaining three Half-Life 2 movies, fix the remaining issues with the Riot Shield in Counter-Strike, and any remaining issues with Steam. This will happen almost immediately after the Steam beta ends.<br />
<br />
Some of the other features coming after the beta resets are:<br />
<br />
- New version of Cobble reworked by David Johnston.<br />
- Full Skin support in Steam.<br />
- More detailed information regarding the cache file on your drive.<br />
- Vastly more efficient use of disk space and bandwidth in Steam.<br />
- New conversion wizard to make better use of existing content on your drive.<br />
<br />
After this, we&apos;ll release an update to the entire user base that will convert them to Steam. Hopefully, the majority of the community will have converted before this. This will happen roughly two weeks from the time that the beta period of Steam has ended. Keep in mind, this does not mean we will be stopping or slowing development of Steam or our games that run upon them.<br />
<br />
Thanks to everyone who has participated in the beta up until now. I&apos;ll let everyone know when we&apos;re ready for the next release here.<br />
<br />
Erik Johnson<br />
