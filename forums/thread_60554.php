	<font face="verdana,arial,helvetica" size="1" > <b>March 3rd Update</b></font>
	<p><font face="verdana, arial, helvetica" size="2" >The update release tomorrow will include a new HldsUpdateTool version. It will have a number of improvements. The tool should update itself automatically when you run it (after the update is released).<br />
<br />
The changes are:<br />
<br />
<b>* New command-line syntax *</b><br />
<br />
The old "-update game dir username..." syntax will still work, so don't worry. <br />
<br />
However, a more powerful syntax was necessary for some new features, and to prevent common mistakes. With the new syntax, each parameter is explicitly listed (instead of assuming they are all in the correct order). For instance:<br />
<br />
hldsupdatetool -command update -game cstrike -password mypassword -username user ... etc<br />
<br />
You can run "hldsupdatetool -?" to get the full syntax list.<br />
<br />
<b>* New "-verify_all" flag *</b><br />
<br />
If you run an "update" command with "-verify_all", it will check that each file in the install matches the latest Steam version. If, somehow, your installation gets corrupted and will not run properly, this should bring it back to working order.<br />
<br />
<b>* "Resume" download feature*</b><br />
<br />
Previously, if the tool lost connection to the Steam servers in the middle of an update, the next time you ran it, it would re-download some of the files it got the last time. This should be fixed now, it will pick up where it left off.</font></p>
	
