	<p><font face="verdana, arial, helvetica" size="2" >WeТve released a new version of Steam, Counter-Strike, and Team Fortress Classic to the Steam beta.<br />
<br />
Exit Steam and restart to receive the update.<br />
<br />
Changes in this update (also available here <a href="/platform/update_history/counter-strike.html" target="_blank">/platfor...ter-strike.html</a>)<br />
<br />
Changes/Additions:<br />
 <br />
- Server browser will display an icon next to servers that have bots playing in them. <br />
- Player list dialog now shows the friends name of all the other players on the server. <br />
- Counter-Strike new game dialog now remembers the map you selected and uses that as the default for next time. <br />
- Made knife secondary attack do significantly more damage if hitting someone in the back. <br />
- Optimized new weather effects. <br />
- Disabled Video Mode drop down menu if the video card can't display Widescreen modes. <br />
- Fixed random characters appearing at the end of the map overview description (on the team page) <br />
- Changed Counter-Strike text chat so Spectators can chat with dead players. <br />
- Bots only swing knife when in range to hit now. <br />
- Bots use both primary and secondary knife attacks. <br />
- Bots will try to sneak up behind victims if they are using a knife. <br />
- Bots crouch more when shooting at distant targets. <br />
- Bots jump much less during combat now. <br />
- If a bot sees a friend get killed by an unseen enemy, and it has no enemy, it acquires the killer as its enemy. <br />
- Throttled how often nav meshes are drawn (confused fast video cards). <br />
- All games are ready for Steam Linux server release. <br />
- Team Fortress Classic dedicated server will now appear in server list.<br />
<br />
Bug Fixes:<br />
<br />
- Fixed font drawing problems with Windows 98/ME. <br />
- Fixed splash screen not drawing correctly in software mode. <br />
- Fixed HUD sprites not drawing correctly in OpenGL. <br />
- Fixed progress bar for launching dedicated server. <br />
- Fixed Shield/Knife model playing wrong idle animation while deployed. <br />
- Fixed Shield/Knife model using the wrong knife model. <br />
- Fixed bug where bots were finding hiding spots on floors above/below that took far too long to reach. <br />
- Bots no longer aim at enemy's leg with sniper rifles. <br />
- Bots wont jump after using hostages now. <br />
- Fixed bug where bots would aim at the ground if in combat with an enemy and the enemy went behind cover.</font></p>
	
