	<p><h3><a href="index.php?area=news&id=415" style="text-decoration: none; color: #BFBA50;">Steam Client and SourceTV Updates Available</a></h3>
<span style="font-size: 9px;">May 5, 2005, 3:30 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">Alfred Reynolds</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
An update for the Steam Client (and the HLDS Update Tool) has been released, along with some improvements to SourceTV. (SourceTV is for broadcasting Source games to large numbers of spectators.) The update will be applied automatically when your Steam client is restarted. The specific changes include:<br> <br><b>Steam Client</b><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li>Fixed backup wizard creating invalid backup files for Source games<br><li>Improved content server selection on WinXP SP2<br></ul><br><b>HLDS Update Tool (Windows Version)</b><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li>Improved content server selection on WinXP SP2</ul><br><b>SourceTV <i>(part of the Source Dedicated Server)</i></b><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li>Added hostname to tv_status output<br><li>Changed the behavior of "tv_relay" command to automatically stop any running map<br><li>Added command "tv_clients" to list connected spectators/proxy<br><li>Added -tvmasteronly command line parameter to restrict tvmaxclients to 1 - for use when feeding a single proxy server (not broadcasting)<br><li>Fixed high CPU usage when running a non standard tick rate<br><li>Fixed Linux build not correctly initializing, and then not running properly<br><li>Fixed "tv_maxclients 0" working if SourceTV is used for recording demos only<br><li>Fixed a couple other crashes<br></ul><br></p>

