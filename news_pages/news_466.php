Updates to Day of Defeat: Source and the Source Engine have been released. The updates will be applied automatically when your Steam client is restarted. The specific changes include:<br><br><br><b>Source Engine</b><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li>cl_predict is now treated as a cheat<br><li>Disallow the '%' character in names and the '#' character when at the start of a name<br><li>Removed debug cvar snd_mixvol</ul><br><br><b>Day of Defeat Source</b><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li>Added defense scoring bonus. Players now get a point for blocking an area cap that is over 50%, either by killing a player required for the capture, or by entering the capture area<br><li>Fixed an issue with players not respawning on round restart<br><li>Changed to new arm hitbox code - uses hitbox groups to swap between hitbox sets with and without arms. This will prevent shots to the chest area from hitting the target player's arms<br><li>Increased Thompson and MP40 melee attack rate to match knife/spade<br><li>Fixed situation where melee attacks weren't hitting target when they should have<br><li>Fixed accuracy on semi-auto and full-auto for bar and MP44 being swapped<br><li>Semi automatic is now more accurate than fully automatic<br><li>Darkened background of scoreboard to make it easier to read<br><li>K98 iron sights no longer re-zooms after a shot<br><li>Fixed rag doll pose when player went prone with Panzerschreck<br><li>Reduced maximum number of players on a server to 32<br><li>Fixed issue with bullet penetration wasn't happening correctly with displacements<br><li>Optimization related to grenade explosions<br><li>Network optimizations for player, player resource<br><li>Restrict which shaders are allowed to be applied to player models.<br><li>Fixed sniper rifle playing zoom out animation out if the player was not on the ground when they tried to zoom in ( e.g., falling off of a small ledge )<br><li>Friendly fire is now turned off for the bonus round. Be nice to your winning teammates<br><li>Crosshair scale setting is now saved across sessions<br><li>Added log event for tick points<br><li>Added log event for changeclass<br><li>Added log event for flag caps<br><li>Added log event for game over condition<br><li>Added damage, hitbox, weapon info to player_hurt event<br><li>Fixed log events for clan restart events<br><li>Added cvar mp_logdetail ( 0 = no damage logged, 1 = enemy damage, 2 = teammate damage, 3 = both )<br><li>Made input to voice menus all client side<br></ul><br></p>

