	<p><h3><a href="index.php?area=news&id=179" style="text-decoration: none; color: #BFBA50;">Steam status</a></h3>
<span style="font-size: 9px;">August 26, 2003, 1:23 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
The Steam beta is now drawing to an end. This means that there are some significant changes coming to the way that Steam will be run.<br><br>The next major release of Steam will be the first step towards converting the entire Half-Life user base to Steam. This means that we will be resetting the Steam account database from scratch. After this, you will need to have a valid CD-Key to access games via Steam, so you will want to treat your Steam account as permanent. One CD-Key will grant you access to all of the games currently available in the Steam beta. The Friends database will remain intact, so if you create an account with the same name when the beta ends, you will retain your Friends list.<br><br>The next step will be to release the remaining three Half-Life 2 movies, fix the remaining issues with the Riot Shield in Counter-Strike, and any remaining issues with Steam. This will happen almost immediately after the Steam beta ends.<br><br>Some of the other features coming after the beta resets are:<br><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li> New version of Cobble reworked by David Johnston.<br><li> Full Skin support in Steam.<br><li> More detailed information regarding the cache file on your drive.<br><li> Vastly more efficient use of disk space and bandwidth in Steam.<br><li> New conversion wizard to make better use of existing content on your drive.</ul><br>After this, we&apos;ll release an update to the entire user base that will convert them to Steam. Hopefully, the majority of the community will have converted before this. This will happen roughly two weeks from the time that the beta period of Steam has ended. Keep in mind, this does not mean we will be stopping or slowing development of Steam or our games that run upon them.<br><br>Thanks to everyone who has participated in the beta up until now. I&apos;ll let everyone know when we&apos;re ready for the next release here.<br><br>Erik Johnson<br>Valve<br></p>

