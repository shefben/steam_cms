	<p><h3><a href="index.php?area=news&id=342" style="text-decoration: none; color: #BFBA50;">Half-Life® 2 Books Set to Ship</a></h3>
<span style="font-size: 9px;">November 10, 2004, 2:44 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
<b>Raising the Bar and Official Game Guide Set to Ship</b><br><br>Roseville, California – (November 10, 2004) – Prima Games and Valve today announced the availability of the first two official Half-Life 2 publications – Half-Life 2: Raising the Bar and the official Half-Life 2 game guide – both scheduled to arrive in retail outlets around the world on Friday, November 12, 2004.<br><br>Half-Life 2: Raising the Bar is a 288-page, hard-bound publication that chronicles the origins of Valve, development of Valve&apos;s first games, and covers the making of Half-Life 2 in great detail with hundreds of exclusive images, interviews and more. <br><br>The Half-Life 2 Official Game Guide, a 320-page full-color guide covering the most anticipated PC game of all time, features full-color maps, weapons details, bestiary tactics, hidden game elements, and more.<br><br>Visit your local or favorite online retailer now to purchase a copy of both publications. Or visit www.primagames.com for more information.<br><br>About Prima Games<br>Prima Games, a division of Random House Inc., is the world's leading publisher of strategy guides for computer and console video games, with more than 1,100 titles published and over 70 million guides in print.  High quality strategy guides, extensive retail distribution and relationships with top game developers and publishers have allowed Prima Games to claim the highest market share among strategy guide publishers.  More information can be found on the Web at www.primagames.com.  <br><br>About Valve<br>Founded in 1996, Valve creates entertainment software titles, including Half-Life®, Counter-StrikeTM, and Team Fortress®. Valve's portfolio accounts for over 12 million retail units sold worldwide, and over 88% of the online action market. More information about Valve is available through the company&apos;s Web site at www.valvesoftware.com.<br><br></p>

