	<p><h3><a href="index.php?area=news&id=446" style="text-decoration: none; color: #BFBA50;">Rag Doll Kung Fu Available Via Steam October 12th</a></h3>
<span style="font-size: 9px;">September 19, 2005, 12:44 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">Chris Bokitch</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Rag Doll Kung Fu, a wildly entertaining new game from designer Mark Healey, will be made available to gamers everywhere on October 12, 2005 via Steam. <br><br>Featuring a wide collection of single and multiplayer games, either online or with multiple mice, Rag Doll Kung Fu provides players with one of the most creative and unique game experiences delivered in years. Additionally, between game rounds, the challenges of an aspiring Kung Fu master are revealed via outlandish cut scenes inspired by classic Kung Fu cinema.<br> <br>The game is the brainchild of artist and designer Mark Healey (who moonlights as a lead artist at Lionhead Entertainment). During last year's Game Developer Conference, Healey presented Rag Doll to industry colleagues. Immediately after this presentation, several Valve employees invited Healey to visit Valve and discuss Steam distribution of Rag Doll Kung Fu.<br><br>"Rag Doll was originally just an idea for me and some friends to have some fun making an old style Kung Fu movie," said Healey. "It's amazing to think that a tongue in cheek film project has now evolved into a full game being distributed via Steam. I hope everyone enjoys it as much as my friends and I enjoyed making it." <br><br>Steam offers developers and publishers a direct distribution channel to over 5.5 million customers. Rag Doll Kung Fu will be made available for purchase via Steam on October 12, 2005 for $12.95. For more information on Steam, please visit <a href="http://www.steampowered.com/">www.steampowered.com</a>. For more information on Rag Doll Kung Fu, please visit <a href="http://www.ragdollkungfu.com">http://www.ragdollkungfu.com</a>.<br></p>

