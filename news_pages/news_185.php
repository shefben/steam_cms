	<p><h3><a href="index.php?area=news&id=185" style="text-decoration: none; color: #BFBA50;">Status Update: New Installers to be Released Tomorrow</a></h3>
<span style="font-size: 9px;">September 15, 2003, 5:06 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Well, the release process last week and over the weekend was pretty rocky. The community managed to get 500,000 accounts created and are currently seeing about 15,000 people playing Counter-Strike 1.6 at any given time. We know that the conversion process has been anything but smooth for many of our users. We're going to get this fixed. We've done our best to support the Half-Life community over the past 5 years, and we're still committed to doing whatever it takes to make the community happy. We've listened to the response, and we'll be repairing the problems with Steam until it performs the way we all want it to.<br><br>Tomorrow we will release new installers which include the cache files for all of the games available via Steam (Day of Defeat, Team Fortress Classic, the dedicated server, etc). If you haven't already done so, please use the installer that includes Counter-Strike and Half-Life.<br><br>Here's what's going on at Steam HQ:<br><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li> We ran into some pretty serious bandwidth and server limitations after the release last week, which has caused several other problems like long wait times and UI unresponsiveness. The load is starting to die down now and we're getting more content servers lined up, so these problems are becoming less frequent. As more people begin using the new installers which include the game caches, their experience using Steam will improve greatly. If you're having problems connecting to Steam or launching Counter-Strike, please download the latest version of the installer and give it a try. <br><li> Some other Steam problems -- "JIT" error messages, expired ticket errors, etc -- will be fixed with a Steam update this week.<br></ul><br>Other stuff:<br><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><li> Counter-Strike 1.5 is still available, and will be for a couple of weeks. After we're confident that the Steam authentication servers are functioning as they should (and not before), we'll be turning off the WON servers and migrating everyone over to the new system. <br><li> There have been some reports of the Conversion Wizard failing during account creation. If this happens for you, please try downloading and installing the most recent version of the Steam client from one of the mirrors listed on this site. <br><li> We're preparing a new installer on CD for cyber-cafe owners. Contact biz@steampowered.com if you're interested in receiving this. <br><li> We've heard from several people who are having trouble getting their MODs working with Steam. We've updated our FAQ with new information about this. Check it out and then email us if you're still having trouble.<br></ul><br>Feel free to get in touch with us you're having difficulty, or for whatever reason. Thanks for hanging in there with us through these first few days.<br></p>

