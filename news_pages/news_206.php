	<p><h3><a href="index.php?area=news&id=206" style="text-decoration: none; color: #BFBA50;">Condition Zero, and Other Steam News</a></h3>
<span style="font-size: 9px;">October 23, 2003, 12:32 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Several exciting things are happening in the coming weeks.<br><br><strong style="text-transform:uppercase;">Changes to the Steam Network</strong><br><br>Things have stabilized quite a bit since the Steam release last month. Over the weekend the number of simultaneous Steam users in-game reached an all time high. We are, however, definitely still stamping out bugs.<br><br>We are about to make some significant additions and changes to the Steam network in order to guarantee quality of service during the Counter-Strike: Condition Zero rollout. We&apos;ll be adding lots of new content servers and plenty of bandwidth to handle the load in the next couple of weeks.<br><br>Before we release CS:CZ, we will need to test these changes to see how they hold up under lots of stress. Stay tuned for details of how you can help us test the network (and get some free stuff in the process).<br><br><strong style="text-transform:uppercase;">Offline/LAN play</strong><br><br>Work on offline/LAN play is now nearing completion. To play any Steam game while not connected to the Internet it will only be necessary to let Steam finish downloading all of that game&apos;s content, at which point you&apos;ll be able to play games without connecting to Steam.<br><br><strong style="text-transform:uppercase;">WON</strong><br><br>Roughly 50% of the community has migrated to using Steam as the way to play our games. The WON authentication system won&apos;t go away until Steam is ready for the entire user base. We don&apos;t yet have an exact date when this will happen.<br><br><strong style="text-transform:uppercase;">Banning, Cheaters, etc.</strong><br><br>The process of banning Steam accounts with bad (or duplicate) CD keys was behind for a while, but the servers which handle these transactions have caught up with the backlog. This means that many of the people currently using cheats in CS are dissappearing.<br><br><strong style="text-transform:uppercase;">Counter-Strike: Condition Zero</strong><br><br>Valve is about to ship Counter-Strike: Condition Zero, and the game will soon be available through Steam. The in-store date and the Steam release date will be the same day. Watch this space (or your Steam Games list) for details.<br></p>

