<p><h3><a href="index.php?area=news&id=108991776730978200" style="text-decoration: none; color: #BFBA50;">WON Shutdown Timing</a></h3>
<span style="font-size: 9px;">July 15, 2004, 11:56 am &middot; <a href="mailto:cliffe@valvesoftware.com" style="text-decoration: none;">cliffe</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Starting July 26th the shutdown of the WON authentication servers will begin. This process will be completed on July 31st. This means players will no longer be able to access Internet features of Valve games, such as Counter-Strike 1.5, using the WON authentication servers. Users can continue using Valve games on the Steam service by downloading the Steam installer and setting up a Steam account.  For more information visit www.steampowered.com. Counter-Strike 1.5 players will be upgraded to Counter-Strike 1.6 free of charge through Steam.<br>
<br><br></p>

