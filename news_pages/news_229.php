	<p><h3><a href="index.php?area=news&id=229" style="text-decoration: none; color: #BFBA50;">Counter-Strike and Game Engine Update</a></h3>
<span style="font-size: 9px;">January 15, 2004, 11:32 am &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
A Counter-Strike update and a game engine update have just been released. Server admins who have difficulty with the HLDS update tool can get the new dedicated server updates <a href="/?area=dedicated_server">here</a>.<br><br>Here's a list of the changes included in the update.<br><br><ul style="padding-bottom: 0px; margin-bottom: 0px;" ><b>COUNTER-STRIKE:</b><br><li>Fixed bug where shield would be invisible if a player picked the shield up immediately after throwing a grenade. </li><br><li>The round timer is now disabled when the bomb is planted. </li><br><li>The winning defensive team now gets the same amount of money whether they win by round timeout or not ($3250 instead of $2000) </li><br><li>Players on the offensive team who survive the round due to a round timeout no longer receive money the next round. </li><br><li>The Terrorists get an additional $800 per player if they plant the bomb but lose the round. </li><br><li>Fixed exploit where when a player died they could sometimes join the other team during the same round.</li><br><br><b>ENGINE:</b><br><li>Fixed custom decals not being downloaded from the server correctly.</li><br><li>Fixed custom decals drawing incorrectly for the host of a listen server after another player joins.</li><br><li>Fixed vgui textures being corrupted after going over a number of level changes.</li><br><li>Fixed problem where player would be sometimes be forced to look straight up after task switching in and out of game in fullscreen d3d mode.</li><br><li>Fixed problem in sound system where it wouldn&apos;t properly be shutdown at the end of a .wav file.</li><br><li>Fixed banner picture not working correctly in HLTV.</li><br><br><b>VGUI:</b><br><li>Changed the way that Asian fonts are displayed in game for compatibility on more platforms. </li><br><li>Optimized VGUI to use less system resources when running on the desktop.</li><br><br><b>GAMEUI:</b><br><li>Fixed error message that was cut off when a player was disconnected from a game server.</li><br><br><b>SERVER BROWSER:</b><br><li>Fixed Opposing Force not showing up in the server browser game filter.</li><br></ul><br></p>

