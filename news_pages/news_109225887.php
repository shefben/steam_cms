<p><h3><a href="index.php?area=news&id=109175803763788200" style="text-decoration: none; color: #BFBA50;">Namco Named Valve's Official Cyber Cafe Reseller In Japan</a></h3>
<span style="font-size: 9px;">August 5, 2004, 7:07 pm &middot; <a href="mailto:ChrisB@valvesoftware.com" style="text-decoration: none;">Chris Bokitch</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Tokyo, Japan -- August 3, 2004 - Valve(tm) Corporation, one of the world's largest developers of online computer games, has selected Japan-based Namco, Ltd. as its representative to distribute its already famous games, including Half-Life(r), Counter-Strike(TM), and Day of Defeat(TM), to the cyber cafe community in Japan. Namco is currently the only authorized cyber cafe reseller in Japan for Valve games.<br>
<br>
Valve's Cyber Caf&eacute; program is the only way to legally obtain Valve's current and coming titles for commercial use in cyber cafes. Valve licensed cyber cafe operators also receive the benefits of Steam, an online network that insures licensed cybercafe operators have the most up to date versions of Valve games for their customers. <br>
<br>
Additionally, Valve recently revealed a series of new games and features being added to the Valve Cyber Caf&eacute; Program over the coming months. Counter-Strike: Source and Half-Life 2 will be added to the program for active subscribers in the Valve Cyber Cafe Program. These new games will be included as part of the regular subscription fee as soon as they are released. Valve is also readying a Cyber Cafe License Server. Among other features, the Cyber Cafe License Server will allow cafe's to manage their licenses on a concurrent user basis, locally update individual computers on a cafe's local area network (reducing bandwidth needs for updates), and allow individual users with personal Steam accounts to access their account on any computer station located at a cyber cafe site.<br>
<br>
"We are delighted by the success we've experienced working with Namco," said Gabe Newell, president and founder of Valve, "And we look forward to extending that relationship as we work to meet the growing demand for action games in Japan." <br>
<br>
"Namco has a long tradition in the development of location based entertainment businesses," said Tetsuo Tsuchiya, Namco's Manager. "We are eager to extend that tradition to the Japan cyber cafe community by working with Valve to develop this business in Japan. We are hoping to hear feedback from the cyber cafes so we can continue to improve the program and better understand how we can best support our customers."<br>
<br>
About Valve<br>
Founded in 1996, Valve creates entertainment software titles, including Half-Life&reg;, Counter-Strike(tm), and Team Fortress&reg;. Valve's portfolio accounts for over 11 million retail units sold worldwide, and over 88% of the online action market. More information about Valve is available through the company's Web site at <a href="/valvesoftware/">www.valvesoftware.com</a>.<br>
<br>
About Namco<br>
Established in 1955. Namco has a long tradition in a field of Coin-Operated Game Machines in Japan. In addition, we carry famous game titles such as the Pac-Man game, a massive hit in the 80s, and Soul Calibur, a worldwide famous game. More information about us is available at <a href="http://www.namco.co.jp/english/">www.namco.co.jp/english/</a>.<br>
<br><br></p>

