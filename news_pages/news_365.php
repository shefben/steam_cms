	<p><h3><a href="index.php?area=news&id=365" style="text-decoration: none; color: #BFBA50;">Notes on the Half-Life 2: Deathmatch release</a></h3>
<span style="font-size: 9px;">December 1, 2004, 3:50 pm &middot; <a href="mailto:contact@valvesoftware.com" style="text-decoration: none;">valve</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
Last night at about 9:00 PST, Half-Life 2: Deathmatch was released. Earlier today there were about 12,000 simultaneous HL2DM players. People seem to have gotten the hang of tossing toilets and radiators and cars at each other pretty quickly.<br> <br>Immediately after the release of HL2DM last night, users who run Source Engine games (including Half-Life 2 and Counter-Strike: Source) under DirectX 7 or who run with less than the recommended amount of RAM were affected by a game crash. This caused them to be unable to run HL2 or CS:S for a short time. Shortly after midnight PST, we released a fix which cleared the problem up.<br> <br>We're planning a small dedicated server release soon to improve weapon re-spawn behavior.<br> <br>Also, If you're handy with Hammer (Valve's level editor), be sure to enter the <a href="/?area=map_contest">HL2DM map contest</a>. <br> <br>We hope everyone's enjoying the game.<br></p>

