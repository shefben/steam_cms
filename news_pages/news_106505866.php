	<p><h3><a href="index.php?area=news&id=106505866935034300" style="text-decoration: none; color: #BFBA50;">New Steam Update Released</a></h3>
<span style="font-size: 9px;">October 1, 2003, 6:37 pm &middot; <a href="mailto:greg@valvesoftware.com" style="text-decoration: none;">Greg Coomer</a><table width="100%" cellpadding="0" cellspacing="0"><tr><td height="1" width="100%" bgcolor="#808080"></td></tr><tr><td height="10" width="100%"></td></tr></table></span>
A Steam update has just been released -- it will take effect automatically when you exit and restart Steam. Here's what's new:<br>
<ul>
<li>fixed bug where favorite servers would not get saved to disk if filtered out</li>
<li>increased max servers listed from 10,000 to 20,000</li>
<li>fixed 'add friends' and 'game info' dialogs showing up over gameplay in game</li>
<li>fixed problem with not always being able to re-add friends after removing them</li>
<li>fixed case where authorization request dialog could appear multiple times from the same user</li>
<li>fixed chat messages & authorization requests stealing focus and not starting minimized</li>
<li>fixed auto-away not working</li>
<li>removed 'about' dialog</li>
<li>fixed add-on (checkers, etc) invites when in game appearing over the top of the game, instead of just in the gameUI area</li>
<li>removed unnecessary bind() calls for client UDP sockets</li>
<li>chat dialogs now add a message into the text whenever the other user changes status</li>
<li>fixed spurious character in Game Info dialog</li>
<li>change 'time' info in game info player list to be in hours:minutes:seconds instead of just seconds</li>
<li>changed game info player list to default to sorting by score, then time</li>
</ul><br>
<br><br></p>

