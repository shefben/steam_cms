<h3 style="text-transform:uppercase;">For Immediate Release</h3>
<br>
Monday, November 29 at 4:52 pm PST.<br>
<br>
VALVE WINS SUMMARY JUDGMENT MOTIONS IN COPYRIGHT INFRINGEMENT CASE<br>
<br>
Valve today announced the U.S. Federal District Court in Seattle, WA granted its motion for summary judgment on the matters of Cyber Caf&eacute; Rights and Contractual Limitation of Liability in its copyright infringement suit with Sierra/Vivendi Universal Games.<br>
<br>
Judge Thomas S. Zilly ruled that Sierra/Vivendi Universal Games, and its affiliates, are not authorized to distribute (directly or indirectly) Valve games through cyber caf&eacute;s to end users for pay-to-play activities pursuant to the parties' current publishing agreement. Valve games such as Counter-Strike, Counter-Strike: Condition Zero and the recently released Half-Life 2 and Counter-Strike: Source are all popular in cyber caf&eacute;s.<br>
<br>
In addition, Judge Zilly ruled in favor of the Valve motion regarding the contractual limitation of liability, allowing Valve to recover copyright damages for any infringement as allowed by law without regard to the publishing agreement's limitation of liability clause. <br>
<br>
"We're happy the court has affirmed the meaning of our publishing contract. This is good news for Valve and its cyber caf&eacute; partners around the world," said Gabe Newell, founder and CEO of Valve. "We continue to add value to our program and we look forward to working with caf&eacute;s to get them signed up and offering Valve's latest games to their customers." <br>
<br>
The Valve Cyber Caf&eacute; Program is the only legal way to use Valve games in your cyber caf&eacute; or gaming center. There are currently thousands of cyber caf&eacute;s participating in the program throughout the world. <br>


