<h3>TECHNICAL SUPPORT</h3>

<br />

<p><a name="#technical1"><strong><em>I'm getting an error saying I can't connect to Steam right now. Why?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>Please check your internet connection to make sure it is functioning properly.</p>

<p>If you're sure that your 'Net connectivity is functioning but you are still unable to log in to Steam, it may be because the required ports are blocked by a device on your network (such as a router).</p>

<p>The following ports must be "open":<br />
UDP 1200<br />
UDP 27000 to 27015 inclusive<br />
TCP 27030 to 27039 inclusive<br /></p>

<p>Finally, if you've checked all of the above and are still having trouble, you may want to double check Steam's network status to make sure the system is online.</p>

<p><sup><a href="#">top</a></sup></p>
</div>

<p><a name="#technical2"><strong><em>Do I have to be connected to the Internet when I play Steam games?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>The short answer is yes.</p>

<p>All Steam-enabled games require you to be connected to the Internet in order to play.</p>

<p>Note: some Steam-enabled single-player games (such as Half-Life 2) purchased at a retail store will allow offline play until the user decides to play online or enable other Steam functionality. From that point on, an Internet connection will be required.</p>
<p><sup><a href="#">top</a></sup></p>
</div>

<p><a name="#technical3"><strong><em>I've got Steam installed - why can't I connect?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>If you get an error message saying that Steam cannot connect properly, please check your ports. Steam requires that your firewall have the following ports open:</p>

<p>UDP 1200<br />
UDP 27000 to 27015 inclusive<br />
TCP 27030 to 27039 inclusive<br /></p>

<p>For information about how to control your networking device to open those ports, check the documentation that came with your device or the manufacturer's web site.</p>

