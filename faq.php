<p><a name="#basics1"><strong><em>What is Steam?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>Steam is Valve's new broadband platform for direct software delivery and content management.</p>

<p>Steam lets you:</p>

<ul>
<li style="list-style-image:url(img/square.gif);"> Buy Valve games via the Internet, and start playing within minutes of confirming your purchase
<li style="list-style-image:url(img/square.gif);"> Access your games from any PC
<li style="list-style-image:url(img/square.gif);"> Stop worrying about product updates and patches (they're all handled automatically!)
<li style="list-style-image:url(img/square.gif);"> Instant-message your friends, even while playing games.
<li style="list-style-image:url(img/square.gif);"> Browse and connect to game servers
<li style="list-style-image:url(img/square.gif);"> See when your friends are playing, and join their games
</ul><br>

<p>In addition to using Steam to deliver games, Valve is also making Steam available to other software developers. Steam is a distributed file system and shared set of technology components that can be implemented into any software application. With Steam, software developers are given integrated tools for direct-content publishing, flexible billing, ensured-version control, anti-cheating, anti-piracy, and more. The Steam SDK also includes an integrated set of communications tools and Valves Graphic User Interface (V-GUI) that provide built-in support for a variety of services such as instant messaging, configuration, and server browsing.</p>

