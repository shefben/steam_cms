<p><a name="#technical8"><strong><em>How can I have a LAN party or similar event using Steam?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>Any Steam game, when played on a LAN or otherwise, requires that each individual client be able to authenticate via the Internet. </p>

<p>At a LAN party, this means that Internet access must be available to every machine on which Steam games are being played. </p>

<p>If desireable, the network administrator of such an event can limit internet access to only the TCP/IP ports which Steam needs. These are:</p>

<p>UDP 1200<br />
UDP 27000 to 27015 inclusive<br />
TCP 27030 to 27039 inclusive<br /></p>

<p>At large events, it will be necessary for each attendee to let their games fully update before attending to avoid taxing the shared internet connection with too much content delivery.</p>

<p><sup><a href="#">top</a></sup></p>
</div>

<p><a name="#technical9"><strong><em>How do I install and use mods in Steam?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>If you have a Half-Life mod installed before installing Steam, Steam will automatically convert and install the game for you. It will then be accessible in the Steam "My Games" list under "Third Party Games."</p>

<p>If you want to add a mod after installing Steam, you need only to put it in the Steam/SteamApps/your_email_address/Half-Life directory. The next time you open the "My Games" list it will be listed under "Third Party Games."</p>

<p><sup><a href="#">top</a></sup></p>
