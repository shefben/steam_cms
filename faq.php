<p><a name="#technical0"><strong><em>Why is there non-Steam information in my Steam cache files? Is Steam Spyware?</em></strong></a></p>

<div style="padding-left: 40px;">
<p>Some users have seen non-Steam related information (like names of private files on their computer) show up inside their Steam cache files, raising privacy concerns. This is actually a result of the way Windows handles files. The cache file gets created on the client using the Windows CreateFile() command. This command will create the file but not initialize it. Due to the large size of the file, the Steam client does not initialize the empty data blocks in it as this can take an appreciable amount of time. On NTFS machines, files created with this method happen to have the property of appearing empty (initialized to zero) but for FAT32 machines, it shows up as whatever old data was sitting on drive in the sectors that just got allocated for the cache. This data is never uploaded or even read by the Steam client. Eventually, as Steam downloads more data, the old data is overwritten.</p>

