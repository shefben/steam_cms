	<p><b>Est-ce que la saisie d'informations sur la carte de crédit est sécurisée dans Steam ?</b> Oui, toutes les informations de facturation sont codées pour la transmission et le stockage. Votre code de sécurité (CVV2) est conservé jusqu'à ce que la carte soit débitée, puis est supprimé.<br><br><b>Vais-je recevoir un exemplaire physique du jeu commandé par l'intermédiaire de Steam ?</b> Si vous avez commandé un jeu par l'intermédiaire de Steam, vous ne recevrez pas de CD ni de clé CD, car vous n'en aurez pas besoin ! Les jeux achetés sont disponibles en permanence par l'intermédiaire de votre compte Steam. Steam les installe et les met à jour automatiquement, quel que soit l'ordinateur sur lequel vous exécutez votre compte Steam. Attention, vous ne pouvez vous connecter à votre compte et jouer qu'à partir d'un seul ordinateur à la fois.<br><br><b>Puis-je utiliser une carte American Express (ou tout autre type de carte de crédit) pour acheter un jeu par l'intermédiaire de Steam ?</b> Non, Steam accepte uniquement les cartes Visa et MasterCard.</p>

