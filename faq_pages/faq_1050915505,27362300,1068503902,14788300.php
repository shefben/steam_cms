	<h3>TECHNICAL SUPPORT</h3>
<ul><li><em>Sometimes my mouse locks up or stops working while I'm playing Steam games. What should I do?</em></li></ul>
<p><br>Although this problem is not directly related to Steam or Steam games, enough people have reported it that we thought we'd offer one solution. If you're experiencing mouse lockups or failures, it may be because your computer is automatically turning off the power to a USB Root Hub.<br><ul><br><li>Go to your Control Panel > System > Hardware tab > and click on the "Device Manager" button.</li><br><li>In the Device Manager window, find the item or items labeled "Universal Serial Bus controllers". Open it, and doubleclick the first item called "USB Root Hub"</li><br><li>In the USB Root Hub Properties dialog, click on the "Power Management" tab.</li><br><li>Note that "Allow the computer to turn off this device to save power" is checked by default. Uncheck this box, and click "Ok".</li><br></ul></p>
