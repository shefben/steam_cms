	<h3>TECHNICAL SUPPORT</h3>
<ul><li><em>When I try to play a Steam game, I get "Steam Error 108: The Local Steam service is not running." What should I do?</em></li></ul>
<p><br>This error can occur if there is a copy of steam.dll in the <em>game</em> directory. To check, go to the folder where you installed Steam. Inside that folder, go to SteamApps > your@email.address > Half-Life, and look for steam.dll there. If you find it there, delete it.<br><br>(This same error can also occur if there is a copy of steam.dll in a system folder, or in any folder named in the system PATH.)<br><br>If you've found steam.dll where it shouldn't be, it could be because the HLDS Update Tool was run on your computer with an incorrect output directory. The HLDS Update Tool is a stand-alone program for acquiring and updating a dedicated server. It is independent from the full Steam client, so they are not meant to be used together. If you currently launch your dedicated server from within the Steam client, it is not necessary to use the separate Update Tool.</p>
