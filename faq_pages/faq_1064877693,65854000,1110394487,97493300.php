	<h3>CAFE QUESTIONS</h3>
<ul><li><em>What is my IP address: What is it?  Where can I find it?</em></li></ul>
<p><br>The external IP address is the IP address your ISP assigns you for internet use. This is the address that the world sees when you go out onto the internet. I need to know what this address is so that I can unlock your CAS on our master server here. If I don't unlock it for you then your CAS will not run and will complain that it can't find the "heartbeat server".<br><br>What is termed the "internal address" is the address of the machines on your network, NOT the address that the outside world sees. Typically people use a router or some other piece of hardware between their PC's and their cable/dsl modems and they run a network for those PC's. The addresses of the PC's on this "internal" network are usually something like 192.168.0.xxx. These are the internal addresses.<br><br>You can usually get your external IP address by calling your ISP or by checking with your router (it will have the address used for the internet in a setting somewhere inside it).   Or you can search on <a href="http://www.findmyip.com">www.findmyip.com</a>.</p>
