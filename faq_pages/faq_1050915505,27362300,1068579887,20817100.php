	<h3>TECHNICAL SUPPORT</h3>
<ul><li><em>Why do I get a message saying "Invalid STEAM UserID Ticket" whenever I connect to my LAN server?</em></li></ul>
<p><br>This error is most commonly caused by a combination of factors related to LAN game servers and NAT (Network Address Translation) devices.<br><br>You will have a problem connecting to your server if:<br>- You are connected to the Internet through a NAT device<br>- You are trying to connect to a game server on your LAN<br>- To an outside (Internet) observer, the IP address of your LAN server and your own address <strong>do not</strong> match<br><br>Depending on your network architecture and particular NAT device, you may be able to work around this problem by connecting to the *other* address of the server (i.e. if you get the error when connecting to the internal IP address try connecting via the external IP address and vice versa). Note that you can connect to your server's external address by finding it on the "Servers" tab of the Steam server browser, and you can connect to its internal address by using the "LAN" tab.</p>
