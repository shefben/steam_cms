<div class="narrower">

<img src="img/valve_maizelogo.gif" align="left">If you run a cybercafe or other gaming venue, Valve makes it easy for you to bring our games to your customers. When you <a href="cafe_signup.php">sign up</a> and become a member of our Cybercaf&eacute; program, you'll enjoy the following benefits:<br>
<br>
<h3 style="text-transform:uppercase;">Current &amp; future products</h3>
Cyber Caf&eacute; subscribers automatically receive access to newly released products in the Cyber Caf&eacute; program as long as they continue their regular monthly subscription.<br>
<br>

Products currently included in the Cyber Caf&eacute; Program are:<br>
<ul>
	<li>Half-Life 2</li>
	<li>Half-Life 2: Deathmatch</li>
	<li>Counter-Strike: Source</li>
	<li>Counter-Strike: Condition Zero</li>
	<li>Deathmatch Classic</li>
	<li>Day of Defeat</li>
	<li>Half-Life</li>
	<li>Opposing Force</li>
	<li>Half-Life Deathmatch</li>
	<li>Ricochet</li>
	<li>Team Fortress Classic</li>
</ul>
<br>

<h3 style="text-transform:uppercase;">Automatic Updates Delivered Via Steam</h3>
All of your game subscriptions will be kept up-to-date with the latest versions using Steam's distribution system. New content, bug fixes, and other items which have traditionally been distributed as "patches" will be handled automatically.<br>
<br>
<h3 style="text-transform:uppercase;">Caf&eacute; Administration Server (CAS)</h3>
The Steam Caf&eacute; Administration Server will allow caf&eacute;s to locally update individual computers on a caf&eacute;'s local area network (reducing bandwidth needs for updates), allow individual users with personal Steam accounts to access their account on any computer station located at a cybercaf&eacute;, and manage licenses on a concurrent user basis.<br>
<br>
<h3 style="text-transform:uppercase;">Fully licensed software and product keys</h3>
When you sign up and become a member of our Cybercaf&eacute; program, Valve will provide you with software and product keys for each computer you have licensed. <br>
<!-- All commercial licensing of Valve's software is done directly with Valve Corporation -- games purchased at a retail store do not include a license for commercial cyber caf&eacute; use. -->
<br>
<h3 style="text-transform:uppercase;">Access to Valve Product Support</h3>
Members will have priority access to Valve's product support services, for Steam issues and any game-related problems.<br>
<br>
<h3 style="text-transform:uppercase;">Product Key Protection</h3>
Valve's Cyber Caf&eacute; program also protects members against banned accounts. We understand that it is sometimes difficult to prevent users from misusing your computers. To help combat this problem, caf&eacute;s in the program can contact Valve to correct problems with "banned" accounts.<br>
<br>
<h3 style="text-transform:uppercase;">promotional materials</h3>
We've got plenty of posters for your walls and windows, and other fun stuff that we'll send you when you sign up.<br>
<br>
<h3 style="text-transform:uppercase;">Free Tournament License</h3>
Any time you'd like to host a gaming tournament or other local event, just drop us a line and let us know the details. We'll send you a tournament license right away, free of charge.<br>
